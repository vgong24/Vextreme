'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SOURCE_DIR = path.join(ROOT, 'data', 'strings', 'source', 'pages');
const INDEX_OUT = path.join(ROOT, 'data', 'strings', 'compiled', 'identity-index.json');
const REPORTS_DIR = path.join(ROOT, 'data', 'strings', 'reports');

// ── Pure computation ─────────────────────────────────────────────────────────

// collectIdentityRecords — reads every pages/*.json source file, returns a
// flat list of records for every key that has committed identity metadata
// (keys with no `identity` block yet are simply not indexed — not an error;
// most pages have none until piloted). `texts` carries the actual per-language
// strings alongside identity metadata purely for the markdown report's
// side-by-side display (PR #97, dossier hardening) — identity-index.json's
// own byX maps still never include it, staying pointers-only per Vex's rule;
// this field is dropped before anything is written to that file.
function collectIdentityRecords(sourceFiles) {
  const records = [];
  for (const { file, data } of sourceFiles) {
    for (const [key, entry] of Object.entries(data)) {
      if (key === '_meta' || !entry.identity) continue;
      const texts = {};
      for (const [lang, val] of Object.entries(entry.strings || {})) texts[lang] = val.text;
      records.push({
        key,
        file,
        languages: Object.keys(entry.strings || {}),
        texts,
        ...entry.identity,
      });
    }
  }
  return records;
}

// buildIndexes — pointer indexes only. byStringId is the one exception that
// carries a thin cross-reference object instead of a bare key array, since
// the string key IS the index's own lookup key there — an array of itself
// would be trivial, and this repo's real debugging need (see pe-014, the
// sibling reverse-traversal effort) is "what does this key resolve to",
// which a bare key list can't answer.
function buildIndexes(records) {
  const byCanonicalElementId = {};
  const byLegacyElementId = {};
  const byContentNodeKey = {};
  const byField = {};
  const byStatus = {};
  const byStringId = {};

  for (const r of records) {
    if (r.canonicalElementId) {
      (byCanonicalElementId[r.canonicalElementId] ??= []).push(r.key);
      (byLegacyElementId[r.canonicalElementId] ??= []).push(r.key);
    }
    (byContentNodeKey[r.contentNodeKey] ??= []).push(r.key);
    (byField[r.fieldKey] ??= []).push(r.key);
    (byStatus[r.status] ??= []).push(r.key);
    byStringId[r.key] = {
      contentNodeKey: r.contentNodeKey,
      canonicalElementId: r.canonicalElementId,
      status: r.status,
    };
  }

  return { byCanonicalElementId, byLegacyElementId, byContentNodeKey, byField, byStatus, byStringId };
}

// renderMarkdownReport — grouped by content node, matching the "stacked
// context" shape Vex's relay asked the eventual review view to show
// (page → content node → legacy/current id → fields → languages → status).
// PR #97 (dossier hardening, per Vex's relay) added: a language summary,
// an aggregated Known Limitations section (needs_review/unresolved nodes
// surfaced once at the top, not just buried per-node), side-by-side text
// per field, screenshot refs, and pointers to the generated index and
// validator — all read from data already committed/generated, nothing new
// discovered or parsed. `meta` is optional ({ screenshotRefs }) so existing
// callers/tests that don't pass it still work.
function renderMarkdownReport(slug, records, indexes, meta) {
  const screenshotRefs = (meta && meta.screenshotRefs) || [];
  const nodeOrder = [...new Set(records.map(r => r.contentNodeKey))];
  const byNode = new Map();
  for (const r of records) {
    if (!byNode.has(r.contentNodeKey)) byNode.set(r.contentNodeKey, []);
    byNode.get(r.contentNodeKey).push(r);
  }
  const allLanguages = [...new Set(records.flatMap(r => r.languages))].sort();
  const limitedNodes = nodeOrder.filter(n => byNode.get(n)[0].status !== 'aligned');

  const lines = [];
  lines.push(`# String identity report — ${slug}`);
  lines.push('');
  lines.push(`Generated by \`lib/build-string-identity-index.js\`. This is a projection — not source truth.`);
  lines.push(`Source truth: \`data/strings/source/pages/${slug}.json\`'s per-key \`identity\` blocks.`);
  lines.push('');

  // 1. Page summary
  lines.push(`## Page summary`);
  lines.push('');
  lines.push(`${records.length} keys across ${nodeOrder.length} content nodes — ` +
    `${(indexes.byStatus.aligned || []).length} aligned, ` +
    `${(indexes.byStatus.needs_review || []).length} needs review, ` +
    `${(indexes.byStatus.unresolved || []).length} unresolved.`);
  lines.push('');

  // 2. Language summary
  lines.push(`## Languages`);
  lines.push('');
  lines.push(allLanguages.join(', ') || '_none_');
  lines.push('');

  // 3. Known limitations — aggregated, not just buried in per-node notes
  lines.push(`## Known limitations`);
  lines.push('');
  if (limitedNodes.length === 0) {
    lines.push('None — every content node resolved cleanly.');
  } else {
    for (const node of limitedNodes) {
      const first = byNode.get(node)[0];
      lines.push(`- **${node}** (${first.status}): ${first.note || 'no canonical/current identity match found'}`);
    }
  }
  lines.push('');
  lines.push('---');
  lines.push('');

  // 4-5. Content node table, side-by-side text, legacy/current/canonical
  for (const node of nodeOrder) {
    const nodeRecords = byNode.get(node);
    const first = nodeRecords[0];
    lines.push(`## ${node}`);
    lines.push('');
    lines.push(`- **Canonical element ID:** ${first.canonicalElementId ?? '_none_'} (source: ${first.canonicalSource})`);
    lines.push(`- **Current element ID:** ${first.currentElementId ?? '_not found in HTML_'}`);
    lines.push(`- **Status:** ${first.status}${first.note ? ` — ${first.note}` : ''}`);
    lines.push(`- **Languages:** ${[...new Set(nodeRecords.flatMap(r => r.languages))].join(', ')}`);
    lines.push('');
    if (allLanguages.length > 0) {
      lines.push(`| Field | ${allLanguages.join(' | ')} |`);
      lines.push(`|---|${allLanguages.map(() => '---').join('|')}|`);
      for (const r of nodeRecords) {
        const cells = allLanguages.map(lang => (r.texts && r.texts[lang] ? r.texts[lang].replace(/\|/g, '\\|').replace(/\n/g, ' ') : '_missing_'));
        lines.push(`| ${r.fieldKey} | ${cells.join(' | ')} |`);
      }
      lines.push('');
    }
  }

  // 6. Screenshot refs
  lines.push('---');
  lines.push('');
  lines.push(`## Screenshot refs`);
  lines.push('');
  if (screenshotRefs.length === 0) {
    lines.push('None recorded.');
  } else {
    for (const ref of screenshotRefs) lines.push(`- \`${ref}\``);
  }
  lines.push('');

  // 7. Generated index + validation pointers
  lines.push(`## Generated index`);
  lines.push('');
  lines.push(`\`data/strings/compiled/identity-index.json\` — pointer indexes only (\`byCanonicalElementId\`, \`byLegacyElementId\`, \`byContentNodeKey\`, \`byField\`, \`byStatus\`, \`byStringId\`), regenerated by this same script.`);
  lines.push('');
  lines.push(`## Validation`);
  lines.push('');
  lines.push('Run `node lib/check-string-identity.js` for live drift/conflict/coverage status — informational, not embedded here to avoid this file going stale independently of the check.');
  lines.push('');

  return lines.join('\n');
}

// ── I/O — only runs when executed directly ────────────────────────────────────

function readSourceFiles() {
  return fs.readdirSync(SOURCE_DIR)
    .filter(f => f.endsWith('.json'))
    .map(f => ({
      file: path.join('data', 'strings', 'source', 'pages', f),
      slug: f.replace(/\.json$/, ''),
      data: JSON.parse(fs.readFileSync(path.join(SOURCE_DIR, f), 'utf8')),
    }));
}

if (require.main === module) {
  const sourceFiles = readSourceFiles();
  const records = collectIdentityRecords(sourceFiles);
  const indexes = buildIndexes(records);

  const output = {
    _meta: {
      generatedAt: new Date().toISOString(),
      recordCount: records.length,
      sourceFiles: sourceFiles.filter(f => records.some(r => r.file === f.file)).map(f => f.file),
    },
    ...indexes,
  };

  fs.writeFileSync(INDEX_OUT, JSON.stringify(output, null, 2) + '\n');
  console.log(`[build-string-identity-index] Wrote ${path.relative(ROOT, INDEX_OUT)} (${records.length} records)`);

  if (!fs.existsSync(REPORTS_DIR)) fs.mkdirSync(REPORTS_DIR, { recursive: true });

  // One markdown report per slug that has any identity records.
  const slugs = [...new Set(records.map(r => {
    const m = r.key.match(/^pages\.([^.]+)\./);
    return m ? m[1] : null;
  }).filter(Boolean))];

  for (const slug of slugs) {
    const slugRecords = records.filter(r => r.key.startsWith(`pages.${slug}.`));
    const slugSource = sourceFiles.find(f => f.slug === slug);
    const meta = { screenshotRefs: (slugSource && slugSource.data._meta && slugSource.data._meta.screenshotRefs) || [] };
    const reportPath = path.join(REPORTS_DIR, `string-identity-${slug}.md`);
    fs.writeFileSync(reportPath, renderMarkdownReport(slug, slugRecords, buildIndexes(slugRecords), meta));
    console.log(`[build-string-identity-index] Wrote ${path.relative(ROOT, reportPath)}`);
  }
}

module.exports = { collectIdentityRecords, buildIndexes, renderMarkdownReport };

