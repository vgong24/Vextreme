#!/usr/bin/env node
/**
 * VEXTREME — lib/session-bootstrap.js
 *
 * One-shot state report for the start of a work session. Read-only —
 * changes nothing. Written in response to a standing observation in
 * docs/culture.md ("What an AI instance actually needs here"): reconstructing
 * "what's the current state" at the start of a session meant reading several
 * files and running several commands by hand, every time. This consolidates
 * git log, test status, drift/violation checks, and open-item counts into
 * one report.
 *
 * Not a replacement for docs/continuity — this is the fast, mechanical
 * "is anything currently broken, what changed recently, what's the open
 * item count" glance; the continuity log is still the place for the reasoning
 * behind any of it.
 *
 * Run manually:   node lib/session-bootstrap.js
 *                 node lib/session-bootstrap.js --json
 *
 * Pure functions exported for testing:
 *   parseLastSession(indexMd) → string|null
 *   parseBatchRegistry(indexMd) → [{ batch, file, sessions, status }]
 *   latestSessionFile(fileNames) → string|null (newest YYYY-MM-DD-session-0NN.md)
 *   summarizeStatus(status) → { totalOpen, byCategory: { [key]: count } }
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : one-shot session-start state report — read-only, gathers what would otherwise be several manual checks into one command
 *   reads     : docs/continuity/INDEX.md (last logged session, batch registry)
 *               docs/continuity/batch-00N/ directory listing (newest YYYY-MM-DD-session-0NN.md file, when the active batch is a directory)
 *               data/status.json (open item counts per category)
 *               git log / git status (via subprocess)
 *               node lib/build-lattice-headers.js --check (via subprocess)
 *               node lib/check-design-tokens.js (via subprocess)
 *               node --test tests∕*.test.js (via subprocess, with VEX_BOOTSTRAP_NESTED=1 to prevent recursion)
 *   writes    : stdout only — read-only, changes nothing
 *   loaded-by : manual run: node lib/session-bootstrap.js
 *               tests/14-session-bootstrap.test.js
 *   tested-by : tests/14-session-bootstrap.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     docs/continuity/INDEX.md's 'Last updated' line or Batch Registry table format changed:
 *       - parseLastSession() / parseBatchRegistry() here — both parse that exact markdown shape by regex
 *     data/status.json's notices schema changed (a category added/removed):
 *       - summarizeStatus() here reads status.notices generically, but confirm the shape still matches
 *     a new tests∕*.test.js file added that itself spawns this script:
 *       - it must check process.env.VEX_BOOTSTRAP_NESTED and skip its own integration test when nested, same pattern as tests/14 — otherwise the same recursion hazard recurs for a new file
 *     the per-session filename convention (YYYY-MM-DD-session-0NN.md) or the directory-batch form changed:
 *       - latestSessionFile() here — filters and sorts by that exact filename shape; docs/continuity/batch-003/README.md and INDEX.md's batch rules document the convention it assumes
 * LATTICE:END
 */

'use strict';

const fs   = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const ROOT        = path.join(__dirname, '..');
const INDEX_MD     = path.join(ROOT, 'docs', 'continuity', 'INDEX.md');
const STATUS_JSON  = path.join(ROOT, 'data', 'status.json');

// ── Pure computation ─────────────────────────────────────────────────────────

// parseLastSession — extracts the session number from INDEX.md's
// "*Last updated: Session NNN — ...*" line.
function parseLastSession(indexMd) {
  const m = indexMd.match(/Last updated: Session (\d+)/);
  return m ? m[1] : null;
}

// parseBatchRegistry — reads the Batch Registry markdown table rows.
function parseBatchRegistry(indexMd) {
  const rows = [];
  const re = /\|\s*(\d+)\s*\|\s*`([^`]+)`\s*\|\s*([\d–—-]+)\s*\|\s*([^|]+?)\s*\|/g;
  let m;
  while ((m = re.exec(indexMd))) {
    rows.push({ batch: m[1], file: m[2], sessions: m[3], status: m[4].trim() });
  }
  return rows;
}

// latestSessionFile — given the filenames inside a batch directory, returns
// the newest session file, or null. Session files are named
// YYYY-MM-DD-session-0NN.md (see docs/continuity/batch-003/README.md), so
// plain lexicographic order IS chronological order — the whole point of the
// date prefix. Non-session files (README.md etc.) are ignored.
function latestSessionFile(fileNames) {
  const sessions = fileNames.filter(f => /^\d{4}-\d{2}-\d{2}-session-\d+\.md$/.test(f)).sort();
  return sessions.length ? sessions[sessions.length - 1] : null;
}

// summarizeStatus — pulls open-item counts per category out of a parsed
// data/status.json object.
function summarizeStatus(status) {
  const byCategory = {};
  for (const [key, notice] of Object.entries(status.notices || {})) {
    byCategory[key] = notice.count;
  }
  return { totalOpen: status._meta ? status._meta.totalOpen : null, byCategory };
}

// ── Command running (I/O, but isolated for readability) ────────────────────

function runCommand(cmd, args, extraEnv) {
  try {
    const output = execFileSync(cmd, args, { cwd: ROOT, encoding: 'utf8', env: { ...process.env, ...extraEnv } });
    return { ok: true, output: output.trim() };
  } catch (e) {
    return { ok: false, output: ((e.stdout || '') + (e.stderr || '')).trim() };
  }
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const jsonOut = process.argv.includes('--json');

  const indexMd = fs.readFileSync(INDEX_MD, 'utf8');
  const lastSession = parseLastSession(indexMd);
  const batchRows   = parseBatchRegistry(indexMd);

  // From batch-003 on, the active batch is a directory of per-session files;
  // batches 001–002 are legacy single files. Resolve the newest session file
  // when the registry row points at a directory.
  const activeBatch = batchRows[batchRows.length - 1] || null;
  if (activeBatch) {
    const batchPath = path.join(ROOT, activeBatch.file);
    if (fs.existsSync(batchPath) && fs.statSync(batchPath).isDirectory()) {
      activeBatch.latestSessionFile = latestSessionFile(fs.readdirSync(batchPath));
    }
  }

  const status = JSON.parse(fs.readFileSync(STATUS_JSON, 'utf8'));
  const statusSummary = summarizeStatus(status);

  const gitLog    = runCommand('git', ['log', '--oneline', '-10']);
  const gitStatus = runCommand('git', ['status', '--short']);

  // VEX_BOOTSTRAP_NESTED guards against unbounded recursion: this script
  // runs the full test suite, which includes tests/14-session-bootstrap.test.js,
  // whose integration test runs this script — without this guard, that
  // integration test would spawn a full suite run every time it's reached,
  // which spawns another, without bound. The env var tells that one test
  // file "you're already inside a bootstrap run, don't spawn another."
  const testFiles = fs.readdirSync(path.join(ROOT, 'tests')).filter(f => f.endsWith('.test.js')).map(f => path.join('tests', f));
  const tests     = runCommand('node', ['--test', ...testFiles], { VEX_BOOTSTRAP_NESTED: '1' });
  const testLine  = (tests.output.match(/# (tests|pass|fail) \d+/g) || []).join(', ');

  const latticeCheck = runCommand('node', ['lib/build-lattice-headers.js', '--check']);
  const tokenCheck   = runCommand('node', ['lib/check-design-tokens.js']);

  const report = {
    lastLoggedSession: lastSession,
    activeBatch,
    git: { recentCommits: gitLog.output.split('\n'), uncommittedChanges: gitStatus.output ? gitStatus.output.split('\n') : [] },
    tests: { ok: tests.ok, summary: testLine || tests.output },
    latticeDrift: { clean: latticeCheck.ok, summary: latticeCheck.output.split('\n').filter(l => l.includes(':')).join(' | ') },
    designTokens: { clean: tokenCheck.ok, summary: tokenCheck.output.split('\n').pop() },
    openItems: statusSummary,
  };

  if (jsonOut) {
    console.log(JSON.stringify(report, null, 2));
  } else {
    console.log('[session-bootstrap] State report — read-only, changes nothing.');
    console.log('');
    console.log(`Last logged session : ${report.lastLoggedSession}`);
    if (report.activeBatch) {
      console.log(`Active batch         : ${report.activeBatch.file} (${report.activeBatch.sessions}) — ${report.activeBatch.status}`);
      if (report.activeBatch.latestSessionFile) {
        console.log(`Latest session file  : ${report.activeBatch.file}${report.activeBatch.latestSessionFile}`);
      }
    }
    console.log('');
    console.log('Recent commits:');
    for (const line of report.git.recentCommits.slice(0, 5)) console.log('  ' + line);
    console.log('');
    console.log(`Uncommitted changes  : ${report.git.uncommittedChanges.length ? report.git.uncommittedChanges.length + ' file(s)' : 'none'}`);
    console.log(`Tests                : ${report.tests.ok ? 'PASS' : 'FAIL'} — ${report.tests.summary}`);
    console.log(`Lattice drift        : ${report.latticeDrift.clean ? 'clean' : 'DRIFT DETECTED'} — ${report.latticeDrift.summary}`);
    console.log(`Design tokens        : ${report.designTokens.clean ? 'clean' : 'VIOLATIONS FOUND'} — ${report.designTokens.summary}`);
    console.log('');
    console.log('Open items:');
    for (const [key, count] of Object.entries(report.openItems.byCategory)) {
      console.log(`  ${key.padEnd(16)} : ${count}`);
    }
    console.log(`  ${'totalOpen'.padEnd(16)} : ${report.openItems.totalOpen}`);
  }

  if (!tests.ok || !latticeCheck.ok || !tokenCheck.ok) process.exit(1);
}

module.exports = { parseLastSession, parseBatchRegistry, latestSessionFile, summarizeStatus };

