'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const STANDARD_PATH = path.join(ROOT, 'data', 'registry', 'documentation-standard.json');
const VXG_MARKER = '<!-- [VXG RealForever] -->';

function duplicates(values) {
  const seen = new Set();
  const dupes = new Set();
  for (const value of values) {
    if (seen.has(value)) dupes.add(value);
    seen.add(value);
  }
  return [...dupes];
}

function finding(kind, message, file) {
  return file ? { kind, file, message } : { kind, message };
}

function validateStandard(standard) {
  const findings = [];

  if (!standard || typeof standard !== 'object') {
    return [finding('standard-shape', 'standard must be a JSON object')];
  }

  if (!Array.isArray(standard.completionLevels)) {
    findings.push(finding('standard-shape', 'completionLevels must be an array'));
  } else {
    const ids = standard.completionLevels.map(level => level && level.id).filter(Boolean);
    for (const duplicate of duplicates(ids)) {
      findings.push(finding('completion-level-duplicate', `duplicate completion level "${duplicate}"`));
    }
    for (const level of standard.completionLevels) {
      if (!level.id || !level.name || !level.meaning) {
        findings.push(finding('completion-level-shape', 'each completion level needs id, name, and meaning'));
      }
    }
  }

  if (!Array.isArray(standard.architectureDocs)) {
    findings.push(finding('standard-shape', 'architectureDocs must be an array'));
  } else {
    const paths = standard.architectureDocs.map(doc => doc && doc.path).filter(Boolean);
    for (const duplicate of duplicates(paths)) {
      findings.push(finding('architecture-doc-duplicate', `duplicate architecture doc "${duplicate}"`));
    }
    for (const doc of standard.architectureDocs) {
      if (!doc.path) findings.push(finding('architecture-doc-shape', 'architecture doc entry needs path'));
      if (!doc.completionLevel) findings.push(finding('architecture-doc-shape', 'architecture doc entry needs completionLevel', doc.path));
      if (!Array.isArray(doc.requiredHeadings)) {
        findings.push(finding('architecture-doc-shape', 'architecture doc entry needs requiredHeadings array', doc.path));
      }
    }
  }

  return findings;
}

function completionLevelIds(standard) {
  return new Set((standard.completionLevels || []).map(level => level.id));
}

function completionLevelOf(markdown) {
  const match = markdown.match(/\*\*Completion Level:\*\*\s*(L\d+)/);
  return match ? match[1] : null;
}

function missingHeadings(markdown, headings) {
  return headings.filter(heading => !markdown.includes(heading));
}

function checkDocContent(docConfig, markdown, allowedLevels) {
  const findings = [];
  const level = completionLevelOf(markdown);

  if (!level) {
    findings.push(finding('completion-level-missing', 'doc must declare **Completion Level:**', docConfig.path));
  } else if (!allowedLevels.has(level)) {
    findings.push(finding('completion-level-unknown', `doc declares unknown completion level "${level}"`, docConfig.path));
  } else if (docConfig.completionLevel && level !== docConfig.completionLevel) {
    findings.push(finding('completion-level-mismatch', `doc declares "${level}" but standard expects "${docConfig.completionLevel}"`, docConfig.path));
  }

  for (const heading of missingHeadings(markdown, docConfig.requiredHeadings || [])) {
    findings.push(finding('heading-missing', `missing required heading "${heading}"`, docConfig.path));
  }

  if (docConfig.requiresVxgMarker && !markdown.trimEnd().endsWith(VXG_MARKER)) {
    findings.push(finding('marker-missing', 'doc must end with VXG RealForever marker', docConfig.path));
  }

  return findings;
}

function checkRegistryDocs(root = ROOT, standardPath = STANDARD_PATH) {
  const findings = [];
  const standard = JSON.parse(fs.readFileSync(standardPath, 'utf8'));
  findings.push(...validateStandard(standard));

  const allowedLevels = completionLevelIds(standard);
  const checkedDocs = [];

  for (const doc of standard.architectureDocs || []) {
    if (!doc.path) continue;
    const abs = path.join(root, doc.path);
    checkedDocs.push(doc.path);
    if (!fs.existsSync(abs)) {
      findings.push(finding('doc-missing', 'registered architecture doc does not exist', doc.path));
      continue;
    }
    const markdown = fs.readFileSync(abs, 'utf8');
    findings.push(...checkDocContent(doc, markdown, allowedLevels));
  }

  return {
    ok: findings.length === 0,
    standardPath: path.relative(root, standardPath).replace(/\\/g, '/'),
    checkedDocs,
    findings,
  };
}

if (require.main === module) {
  const report = checkRegistryDocs();
  if (process.argv.includes('--json')) {
    console.log(JSON.stringify(report, null, 2));
  } else if (report.ok) {
    console.log(`[check-registry-docs] OK - ${report.checkedDocs.length} registry architecture doc(s) satisfy ${report.standardPath}`);
  } else {
    console.log(`[check-registry-docs] ${report.findings.length} issue(s) found.`);
    for (const item of report.findings) {
      const prefix = item.file ? `${item.file}: ` : '';
      console.log(`  [${item.kind}] ${prefix}${item.message}`);
    }
  }
  if (!report.ok) process.exit(1);
}

module.exports = {
  validateStandard,
  completionLevelOf,
  missingHeadings,
  checkDocContent,
  checkRegistryDocs,
};

