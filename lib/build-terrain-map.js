// ── Pure computation ─────────────────────────────────────────────────────────

// clusterOf — groups a lattice node path into a coarse cluster by its
// top-level directory (and json/root files into their own bucket). This is
// intentionally coarse: it's a map-scale grouping, not a semantic taxonomy.
function clusterOf(nodePath) {
  if (nodePath.startsWith('lib/'))     return 'lib';
  if (nodePath.startsWith('widgets/')) return 'widgets';
  if (nodePath.startsWith('tests/'))   return 'tests';
  if (nodePath.startsWith('config/'))  return 'config';
  if (nodePath.startsWith('data/'))    return 'data';
  if (nodePath.startsWith('docs/'))    return 'docs';
  return 'root';
}

// STAGES — the lifecycle a file actually plays a role in, not the folder it
// happens to live in. Ordered so the layout reads left-to-right as a real
// pipeline: nothing is upstream of SOURCES; RUNTIME is what a browser loads.
// Covers every clusterOf() output, including tests/config/docs — currently
// zero-population per pe-009 (partial lattice coverage), but the rule must
// not silently misplace those nodes once coverage expands.
const STAGES = [
  { key: 0,   name: 'SOURCES',    sub: 'content & config — nothing upstream of these' },
  { key: 1,   name: 'GENERATE',   sub: 'build/apply/append scripts that read sources' },
  { key: 1.5, name: 'UTILITIES',  sub: 'other lib/ helpers, config, tracing' },
  { key: 2,   name: 'CHECK',      sub: 'drift detectors and tests — run after generation' },
  { key: 3,   name: 'OUTPUT',     sub: 'generated artifacts: dist, pages, index, sw' },
  { key: 4,   name: 'RUNTIME',    sub: 'loaded by pages in the browser' },
];
const STAGE_ORDER = STAGES.map(s => s.key);

// stageOf — a naming/field-pattern heuristic, not a designed taxonomy.
// Deliberately kept simple enough to eyeball against real filenames.
function stageOf(nodePath) {
  const cluster = clusterOf(nodePath);
  if (cluster === 'data' || cluster === 'config' || cluster === 'docs') return 0;
  if (cluster === 'lib' && /^lib\/(build|apply|append)-/.test(nodePath)) return 1;
  if (cluster === 'lib' && /^lib\/check-/.test(nodePath)) return 2;
  if (cluster === 'lib') return 1.5;
  if (cluster === 'tests') return 2;
  if (cluster === 'root') return 3;
  if (cluster === 'widgets') return 4;
  return 1.5;
}

// isEngineerFocus / isAuditorFocus — first-pass role lenses over real fields
// (naming pattern, computed status, debt references). Not validated against
// any real engineer or auditor's actual priorities — each node's tags render
// visibly in the UI specifically so this heuristic can be challenged, not
// trusted silently. See docs/lattice-map.json's changeMap for this file.
function isEngineerFocus(nodePath) {
  return /^lib\/(build|apply|append)-/.test(nodePath) || clusterOf(nodePath) === 'data';
}
function isAuditorFocus(status, debtCount, nodePath) {
  return status !== 'good' || debtCount > 0 || /check-|audit/.test(nodePath);
}

// KNOWN_EXECUTION_ORDER — real, sourced directly from where each stage's
// members actually run in sequence: GENERATE from .github/workflows/
// build-index.yml's literal step order, CHECK from the order
// lib/session-bootstrap.js itself runs the checks in. This is ground truth
// where it exists, not a guess extended to cover everything — a node absent
// from these lists (e.g. lib/build-lattice-headers.js, which build-index.yml
// never calls directly; lib/check-key-alignment.js / lib/check-lattice-edges.js,
// which session-bootstrap.js doesn't run) falls back to alphabetical order,
// appended after the known sequence. If this list and the workflow it's
// sourced from drift apart, the map's column order silently goes stale —
// nothing currently detects that; see this file's own lattice changeMap.
const KNOWN_EXECUTION_ORDER = {
  1: [ // GENERATE — .github/workflows/build-index.yml step order
    'lib/build-arc-bundles.js', 'lib/build-index.js', 'lib/build-vextreme.js',
    'lib/build-sw.js', 'lib/build-archives.js', 'lib/build-sitemap.js',
    'lib/build-index-page.js', 'lib/build-lessons.js', 'lib/build-status.js',
    'lib/build-roles.js', 'lib/build-ecosystem-hub.js', 'lib/build-roles-page.js',
    'lib/build-terrain-map.js',
  ],
  2: [ // CHECK — lib/session-bootstrap.js's own check sequence
    'lib/build-lattice-headers.js', 'lib/check-design-tokens.js', 'lib/check-map-bindings.js',
  ],
};

// computeExecutionOrder — sorts a stage's members by real known execution
// order first, then alphabetically for anything with no such evidence. This
// is what makes scrolling down a stage column read like "the order these
// scripts actually run in," not an alphabetical accident.
function computeExecutionOrder(members, stageKey) {
  const known = KNOWN_EXECUTION_ORDER[stageKey] || [];
  const rank = new Map(known.map((p, i) => [p, i]));
  return members.slice().sort((a, b) => {
    const ra = rank.has(a) ? rank.get(a) : Infinity;
    const rb = rank.has(b) ? rank.get(b) : Infinity;
    if (ra !== rb) return ra - rb;
    return a.localeCompare(b);
  });
}

// layoutStages — deterministic column layout: nodes grouped by lifecycle
// stage (not folder), ordered within each stage by real execution sequence
// where known (computeExecutionOrder), stacked in a fixed-width column per
// stage. Same node set always produces the same coordinates — no
// simulation, no randomness, reproducible and diffable.
function layoutStages(nodePaths) {
  const byStage = {};
  for (const key of STAGE_ORDER) byStage[key] = [];
  for (const p of nodePaths.slice().sort()) {
    byStage[stageOf(p)].push(p);
  }
  for (const key of STAGE_ORDER) byStage[key] = computeExecutionOrder(byStage[key], key);

  const COL_W = 340, ROW_H = 60;
  const positions = {};
  const stageMeta = [];

  STAGES.forEach((stage, colIndex) => {
    const members = byStage[stage.key];
    members.forEach((p, i) => {
      positions[p] = {
        cluster: clusterOf(p),
        stage: stage.key,
        stageName: stage.name,
        x: colIndex * COL_W + 60,
        y: i * ROW_H + 90,
      };
    });
    // rect bounds for this stage's column, in the same coordinate space as
    // node x/y — the read side fits its camera to these directly instead of
    // re-deriving COL_W/ROW_H itself (one source of truth for the layout).
    const maxY = members.length ? Math.max(...members.map((_, i) => i * ROW_H + 90)) : 400;
    stageMeta.push({
      key: stage.key, name: stage.name, sub: stage.sub, count: members.length,
      rect: { x: colIndex * COL_W + 10, y: 40, width: COL_W - 40, height: maxY + 60 },
    });
  });

  return { positions, stageMeta };
}

// findDebtReferences — an item's JSON blob mentions this node's basename.
// Same heuristic Session 025's terrain-map pilot used by hand; generalized
// here so it runs over every lattice node, not a curated 13.
function findDebtReferences(nodePath, items) {
  const base = path.basename(nodePath).replace(/\.(js|json)$/, '');
  if (base.length < 4) return []; // too short — avoid false positives
  const refs = [];
  for (const item of items) {
    if (JSON.stringify(item).includes(base)) {
      refs.push({ id: item.id, title: item.title });
    }
  }
  return refs;
}

// computeStatus — same three-bucket rule the pilot used: untested + open
// reference is critical; tested-but-referenced or untested-with-no-reference
// is a middle ground the pilot called warning/good respectively.
function computeStatus(node, debtRefs) {
  const tested = (node.testedBy || []).length > 0;
  const hasDebt = debtRefs.length > 0;
  if (!tested && hasDebt) return 'critical';
  if (tested && hasDebt) return 'warning';
  return 'good';
}

// computeEdges — an edge exists between two lattice nodes when one's
// reads/writes/loadedBy text mentions the other's basename. Deliberately
// coarse (same posture as lib/check-lattice-edges.js's mention check) —
// this is a map for orientation, not a certified dependency graph.
function computeEdges(latticeNodes) {
  const paths = Object.keys(latticeNodes);
  const basenames = {};
  for (const p of paths) {
    const base = path.basename(p).replace(/\.(js|json)$/, '');
    if (base.length >= 6) basenames[p] = base;
  }

  const seen = new Set();
  const edges = [];
  for (const p of paths) {
    const node = latticeNodes[p];
    const text = [...(node.reads || []), ...(node.writes || []), ...(node.loadedBy || [])].join(' ');
    for (const q of paths) {
      if (q === p || !basenames[q]) continue;
      if (!text.includes(basenames[q])) continue;
      const key = [p, q].sort().join('|');
      if (seen.has(key)) continue;
      seen.add(key);
      edges.push([p, q]);
    }
  }
  return edges.sort((a, b) => (a[0] + a[1]).localeCompare(b[0] + b[1]));
}

// findScreenshots — groups docs/screenshots/*.png by slug per the
// {slug}-{lang}.png convention scripts/screenshot-page.js writes. Files that
// don't match (like a one-off named capture) are silently skipped — this is
// discovery, not a integrity check.
function findScreenshots(files) {
  const bySlug = {};
  const re = /^(.+)-([a-z]{2})\.png$/;
  for (const f of files.slice().sort()) {
    const m = f.match(re);
    if (!m) continue;
    const [, slug, lang] = m;
    bySlug[slug] = bySlug[slug] || {};
    bySlug[slug][lang] = `docs/screenshots/${f}`;
  }
  return bySlug;
}

// layoutArcs — the content-side mirror of layoutStages: real curated pages
// (data/nodes.json) grouped into columns by arc (priority asc, key asc) —
// the same fractal-column technique the code side uses, applied to a
// different real hierarchy (arc, not lifecycle stage). Within an arc,
// ordered by each node's own real `id` field (the registry's actual
// chronological/curation order) rather than alphabetically — the content
// equivalent of "real sequence," since pages don't have a build order but
// do have a real add-order. A node with no arcKeys lands in an 'unsorted'
// column (same concept build-archives.js's own "Unsorted (No Arc)" section
// already names); a real pages/*.html file with no nodes.json entry at all
// — auto-discovered but never curated — lands in a distinct 'uncurated'
// column instead of being silently absent, per "reveal state, never hide."
function layoutArcs(nodesJson, arcsDef, pageSlugs) {
  const COL_W = 340, ROW_H = 60;
  const arcIds = Object.keys(arcsDef || {}).filter(k => !k.startsWith('_'));
  const orderedArcKeys = arcIds.slice().sort((a, b) => {
    const pa = arcsDef[a].priority != null ? arcsDef[a].priority : 99;
    const pb = arcsDef[b].priority != null ? arcsDef[b].priority : 99;
    return pa !== pb ? pa - pb : a.localeCompare(b);
  });

  const byArc = {};
  for (const k of orderedArcKeys) byArc[k] = [];
  const unsorted = [];
  const curatedSlugs = new Set();
  for (const n of (nodesJson || [])) {
    curatedSlugs.add(n.slug);
    const key = n.arcKeys && n.arcKeys[0];
    if (key && byArc[key]) byArc[key].push(n); else unsorted.push(n);
  }
  for (const k of orderedArcKeys) byArc[k].sort((a, b) => a.id - b.id);
  unsorted.sort((a, b) => a.id - b.id);

  const uncuratedSlugs = (pageSlugs || []).filter(s => !curatedSlugs.has(s)).sort();

  const positions = {};
  const arcMeta = [];
  let colIndex = 0;
  function layColumn(key, name, sub, members) {
    members.forEach((n, i) => {
      positions[n.slug] = { x: colIndex * COL_W + 60, y: i * ROW_H + 90, arc: key, arcName: name };
    });
    const maxY = members.length ? (members.length - 1) * ROW_H + 90 : 400;
    arcMeta.push({ key, name, sub, count: members.length, rect: { x: colIndex * COL_W + 10, y: 40, width: COL_W - 40, height: maxY + 60 } });
    colIndex++;
  }
  for (const k of orderedArcKeys) {
    const def = arcsDef[k];
    layColumn(k, (def.parent && def.parent.title) || k, 'priority ' + (def.priority != null ? def.priority : '—'), byArc[k]);
  }
  if (unsorted.length) layColumn('unsorted', 'UNSORTED', 'curated pages with no arc yet', unsorted);
  if (uncuratedSlugs.length) {
    const synth = uncuratedSlugs.map((slug, i) => ({ id: 1e9 + i, slug, title: slug, arcKeys: [] }));
    layColumn('uncurated', 'UNCURATED', 'auto-discovered pages, no nodes.json entry yet', synth);
    for (const s of synth) positions[s.slug].uncurated = true;
  }

  return { positions, arcMeta };
}

// buildContentPages — the content-side mirror of buildTerrainMap's code
// node assembly: every curated page plus every uncurated one, positioned by
// layoutArcs, each carrying its real screenshot state (never fabricated —
// a page with no captured shot says so, it isn't given a fake one) and a
// status computed from that same real state, reusing the good/warning/
// critical vocabulary meaningfully for content instead of code health.
function buildContentPages(nodesJson, arcsDef, screenshotsBySlug, pageSlugs) {
  const { positions, arcMeta } = layoutArcs(nodesJson, arcsDef, pageSlugs);
  const uncuratedSlugs = Object.keys(positions).filter(slug => positions[slug].uncurated);
  const realPageSlugs = new Set(pageSlugs || []);

  // A nodes.json entry is a real registry record, not proof a pages/*.html
  // file exists yet — most of this repo's 91 curated nodes are unported v1
  // content (build-archives.js's own "ported : N / 76" line says the same
  // thing about the page-registry side). live=false is a real, common
  // state here, not an edge case — shown honestly, not hidden or implied.
  const curatedPages = (nodesJson || []).map(n => {
    const pos = positions[n.slug];
    const shots = screenshotsBySlug[n.slug] || {};
    const hasScreenshot = Object.keys(shots).length > 0;
    const live = realPageSlugs.has(n.slug);
    return {
      id: n.slug, slug: n.slug, title: n.title, arcKeys: n.arcKeys || [],
      arc: pos.arc, arcName: pos.arcName, x: pos.x, y: pos.y,
      live, liveUrl: live ? `${GH_PAGES_BASE}/${n.slug}.html` : null,
      screenshots: shots, hasScreenshot,
      status: !live ? 'critical' : (hasScreenshot ? 'good' : 'warning'),
      curated: true,
    };
  });
  const uncuratedPages = uncuratedSlugs.map(slug => {
    const pos = positions[slug];
    const shots = screenshotsBySlug[slug] || {};
    const hasScreenshot = Object.keys(shots).length > 0;
    return {
      id: slug, slug, title: slug, arcKeys: [],
      arc: pos.arc, arcName: pos.arcName, x: pos.x, y: pos.y,
      live: true, liveUrl: `${GH_PAGES_BASE}/${slug}.html`, // a real file — just missing a nodes.json entry
      screenshots: shots, hasScreenshot,
      status: hasScreenshot ? 'good' : 'warning',
      curated: false,
    };
  });

  return { arcs: arcMeta, pages: [...curatedPages, ...uncuratedPages] };
}

// buildTerrainMap — assembles the full deterministic output object.
function buildTerrainMap(latticeMap, statusItems, nodesJson, screenshotFiles, arcsDef, pageSlugs) {
  const latticeNodes = latticeMap.nodes || {};
  const paths = Object.keys(latticeNodes);
  const { positions, stageMeta } = layoutStages(paths);
  const edges = computeEdges(latticeNodes);

  const nodes = paths.slice().sort().map(p => {
    const node = latticeNodes[p];
    const debts = findDebtReferences(p, statusItems);
    const status = computeStatus(node, debts);
    return {
      id: p,
      cluster: positions[p].cluster,
      stage: positions[p].stage,
      stageName: positions[p].stageName,
      x: positions[p].x,
      y: positions[p].y,
      role: node.role || '',
      reads: node.reads || [],
      writes: node.writes || [],
      loadedBy: node.loadedBy || [],
      testedBy: node.testedBy || [],
      debts: debts.map(d => d.id),
      status,
      engineerFocus: isEngineerFocus(p),
      auditorFocus: isAuditorFocus(status, debts.length, p),
    };
  });

  // auditorRank — an alternate within-stage ordering, computed alongside the
  // default execution-order position rather than replacing it: worst status
  // first, then most debt references, then id. The read side swaps to this
  // order (recomputing y, x unchanged) only while the auditor lens is
  // active — auditor mode changes what the data itself says is first, not
  // just which dots recede, the same way engineer mode's order is "what
  // runs first," not a cosmetic filter.
  const STATUS_RANK = { critical: 0, warning: 1, good: 2 };
  const byStageForAuditor = {};
  for (const n of nodes) (byStageForAuditor[n.stage] = byStageForAuditor[n.stage] || []).push(n);
  for (const stageNodes of Object.values(byStageForAuditor)) {
    stageNodes
      .slice()
      .sort((a, b) => {
        if (STATUS_RANK[a.status] !== STATUS_RANK[b.status]) return STATUS_RANK[a.status] - STATUS_RANK[b.status];
        if (a.debts.length !== b.debts.length) return b.debts.length - a.debts.length;
        return a.id.localeCompare(b.id);
      })
      .forEach((n, i) => { n.auditorRank = i; });
  }

  const debtTitles = {};
  for (const item of statusItems) debtTitles[item.id] = item.title;

  const screenshotsBySlug = findScreenshots(screenshotFiles);
  const nodesBySlug = {};
  for (const n of (nodesJson || [])) nodesBySlug[n.slug] = n;

  const screens = Object.keys(screenshotsBySlug).sort().map(slug => {
    const node = nodesBySlug[slug];
    return {
      slug,
      title: (node && node.title) || slug,
      arcKeys: (node && node.arcKeys) || [],
      liveUrl: `https://vgong24.github.io/Vextreme/pages/${slug}.html`,
      screenshots: screenshotsBySlug[slug],
    };
  });

  const content = buildContentPages(nodesJson, arcsDef || {}, screenshotsBySlug, pageSlugs);

  return {
    stages: stageMeta,
    nodes,
    edges,
    debtTitles,
    screens,
    arcs: content.arcs,
    pages: content.pages,
  };
}

