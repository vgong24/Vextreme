#!/usr/bin/env node
/**
 * VEXTREME — lib/build-terrain-map.js
 *
 * Write side of the terrain map — a navigable health/dependency atlas over
 * this repo's own lattice, piloted in Session 025 as a hand-authored Artifact
 * over 14 hardcoded nodes. This generalizes that pilot into a real,
 * self-updating artifact: every mapped lattice node, every real screen with
 * a captured screenshot, computed fresh from source data every build.
 *
 * Deliberately follows the same CQRS split as data/status.json ->
 * pages/ecosystem-hub.html and data/roles.json -> pages/roles-index.html:
 * this script is the write side (data/terrain-map.json); pages/terrain-map.html
 * is hand-authored once and live-fetches that JSON at load time. Adding a
 * page, a lattice node, or a screenshot never requires touching the HTML —
 * only this generator needs to run again, same as every other dashboard here.
 *
 * Deterministic by construction (config/lessons/generated-artifacts-must-be-
 * content-deterministic.json): no wall-clock timestamp, no randomness. Layout
 * is a fixed grid per cluster, nodes sorted by id — same inputs always
 * produce a byte-identical file.
 *
 * Run manually:   node lib/build-terrain-map.js
 * Auto-run via:   .github/workflows/build-index.yml, after build-status.js
 *                 (reads its cross-referenced debt/discussion data indirectly
 *                 via the same source files build-status.js also reads)
 *
 * Pure functions exported for testing:
 *   clusterOf(nodePath) → clusterId
 *   layoutClusters(nodePaths) → { [nodePath]: {x, y, cluster} }
 *   computeStatus(node, debtRefs) → 'good' | 'warning' | 'critical'
 *   findDebtReferences(nodePath, debtItems) → [{id, title}]
 *   computeEdges(latticeNodes) → [[a, b]]
 *   findScreenshots(screenshotFiles) → { [slug]: { [lang]: relPath } }
 *   buildTerrainMap(latticeMap, statusItems, nodesJson, screenshotFiles) → terrain map object
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : terrain-map write side — computes layout, health, edges, and real screenshots into one deterministic JSON
 *   reads     : docs/lattice-map.json (every node's role/reads/writes/loadedBy/testedBy)
 *               data/status/tech-debt.json (health cross-reference)
 *               data/status/open-discussions.json (health cross-reference)
 *               data/status/planned-enhancements.json (health cross-reference)
 *               data/nodes.json (real page titles/arcKeys for the screens layer)
 *               docs/screenshots∕*.png (directory listing — which pages have a real per-language screenshot)
 *   writes    : data/terrain-map.json
 *   loaded-by : manual run: node lib/build-terrain-map.js
 *               .github/workflows/build-index.yml (CI)
 *               pages/terrain-map.html (live fetch of its output)
 *               tests/28-build-terrain-map.test.js
 *   tested-by : tests/28-build-terrain-map.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     docs/lattice-map.json schema changed (reads/writes/loadedBy/testedBy shape):
 *       - computeEdges() / findDebtReferences() here read those fields directly
 *     data/terrain-map.json schema changed (nodes/edges/clusters/screens shape):
 *       - pages/terrain-map.html (the only consumer — live-fetches this exact shape)
 *       - tests/28-build-terrain-map.test.js
 *     screenshot filename convention ({slug}-{lang}.png) changed:
 *       - findScreenshots() here — the regex encodes that exact shape
 *       - scripts/screenshot-page.js (writes that exact shape)
 * LATTICE:END
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT              = path.join(__dirname, '..');
const LATTICE_MAP_PATH   = path.join(ROOT, 'docs', 'lattice-map.json');
const TECH_DEBT_PATH     = path.join(ROOT, 'data', 'status', 'tech-debt.json');
const OPEN_DISC_PATH     = path.join(ROOT, 'data', 'status', 'open-discussions.json');
const PLANNED_ENH_PATH   = path.join(ROOT, 'data', 'status', 'planned-enhancements.json');
const NODES_PATH         = path.join(ROOT, 'data', 'nodes.json');
const SCREENSHOTS_DIR    = path.join(ROOT, 'docs', 'screenshots');
const OUT_PATH           = path.join(ROOT, 'data', 'terrain-map.json');

// ── Pure computation ─────────────────────────────────────────────────────────

// clusterOf — groups a lattice node path into a coarse cluster by its
// top-level directory (and json/root files into their own bucket). This is
// intentionally coarse: it's a map-scale grouping, not a semantic taxonomy.
function clusterOf(nodePath) {
  if (nodePath.startsWith('lib/'))     return 'lib';
  if (nodePath.startsWith('widgets/')) return 'widgets';
  if (nodePath.startsWith('tests/'))   return 'tests';
  if (nodePath.startsWith('config/'))  return 'config';
  if (nodePath.startsWith('data/'))    return 'data';
  if (nodePath.startsWith('docs/'))    return 'docs';
  return 'root';
}

const CLUSTER_LABELS = {
  lib: 'BUILD SCRIPTS', widgets: 'BROWSER WIDGETS', tests: 'TESTS',
  config: 'CONFIG', data: 'DATA SOURCES', docs: 'DOCS', root: 'ROOT',
};

// layoutClusters — deterministic grid layout: nodes grouped by cluster,
// sorted alphabetically within each cluster, placed in a fixed-size grid.
// Same node set always produces the same coordinates — no simulation, no
// randomness, so the output is reproducible and diffable.
function layoutClusters(nodePaths) {
  const byCluster = {};
  for (const p of nodePaths.slice().sort()) {
    const c = clusterOf(p);
    (byCluster[c] = byCluster[c] || []).push(p);
  }

  const CELL = 130;
  const positions = {};
  const clusterMeta = [];
  let clusterOriginX = 0;

  for (const clusterId of Object.keys(byCluster).sort()) {
    const members = byCluster[clusterId];
    const cols = Math.max(1, Math.ceil(Math.sqrt(members.length)));
    const rows = Math.ceil(members.length / cols);

    members.forEach((p, i) => {
      const col = i % cols;
      const row = Math.floor(i / cols);
      positions[p] = {
        cluster: clusterId,
        x: clusterOriginX + 70 + col * CELL,
        y: 90 + row * CELL,
      };
    });

    const width = cols * CELL + 60;
    clusterMeta.push({
      id: clusterId,
      label: CLUSTER_LABELS[clusterId] || clusterId.toUpperCase(),
      count: members.length,
      x0: clusterOriginX,
      x1: clusterOriginX + width,
      rows,
    });
    clusterOriginX += width + 70;
  }

  return { positions, clusterMeta, totalWidth: clusterOriginX };
}

// findDebtReferences — an item's JSON blob mentions this node's basename.
// Same heuristic Session 025's terrain-map pilot used by hand; generalized
// here so it runs over every lattice node, not a curated 13.
function findDebtReferences(nodePath, items) {
  const base = path.basename(nodePath).replace(/\.(js|json)$/, '');
  if (base.length < 4) return []; // too short — avoid false positives
  const refs = [];
  for (const item of items) {
    if (JSON.stringify(item).includes(base)) {
      refs.push({ id: item.id, title: item.title });
    }
  }
  return refs;
}

// computeStatus — same three-bucket rule the pilot used: untested + open
// reference is critical; tested-but-referenced or untested-with-no-reference
// is a middle ground the pilot called warning/good respectively.
function computeStatus(node, debtRefs) {
  const tested = (node.testedBy || []).length > 0;
  const hasDebt = debtRefs.length > 0;
  if (!tested && hasDebt) return 'critical';
  if (tested && hasDebt) return 'warning';
  return 'good';
}

// computeEdges — an edge exists between two lattice nodes when one's
// reads/writes/loadedBy text mentions the other's basename. Deliberately
// coarse (same posture as lib/check-lattice-edges.js's mention check) —
// this is a map for orientation, not a certified dependency graph.
function computeEdges(latticeNodes) {
  const paths = Object.keys(latticeNodes);
  const basenames = {};
  for (const p of paths) {
    const base = path.basename(p).replace(/\.(js|json)$/, '');
    if (base.length >= 6) basenames[p] = base;
  }

  const seen = new Set();
  const edges = [];
  for (const p of paths) {
    const node = latticeNodes[p];
    const text = [...(node.reads || []), ...(node.writes || []), ...(node.loadedBy || [])].join(' ');
    for (const q of paths) {
      if (q === p || !basenames[q]) continue;
      if (!text.includes(basenames[q])) continue;
      const key = [p, q].sort().join('|');
      if (seen.has(key)) continue;
      seen.add(key);
      edges.push([p, q]);
    }
  }
  return edges.sort((a, b) => (a[0] + a[1]).localeCompare(b[0] + b[1]));
}

// findScreenshots — groups docs/screenshots/*.png by slug per the
// {slug}-{lang}.png convention scripts/screenshot-page.js writes. Files that
// don't match (like a one-off named capture) are silently skipped — this is
// discovery, not a integrity check.
function findScreenshots(files) {
  const bySlug = {};
  const re = /^(.+)-([a-z]{2})\.png$/;
  for (const f of files.slice().sort()) {
    const m = f.match(re);
    if (!m) continue;
    const [, slug, lang] = m;
    bySlug[slug] = bySlug[slug] || {};
    bySlug[slug][lang] = `docs/screenshots/${f}`;
  }
  return bySlug;
}

// buildTerrainMap — assembles the full deterministic output object.
function buildTerrainMap(latticeMap, statusItems, nodesJson, screenshotFiles) {
  const latticeNodes = latticeMap.nodes || {};
  const paths = Object.keys(latticeNodes);
  const { positions, clusterMeta } = layoutClusters(paths);
  const edges = computeEdges(latticeNodes);

  const nodes = paths.slice().sort().map(p => {
    const node = latticeNodes[p];
    const debts = findDebtReferences(p, statusItems);
    return {
      id: p,
      cluster: positions[p].cluster,
      x: positions[p].x,
      y: positions[p].y,
      role: node.role || '',
      reads: node.reads || [],
      writes: node.writes || [],
      loadedBy: node.loadedBy || [],
      testedBy: node.testedBy || [],
      debts: debts.map(d => d.id),
      status: computeStatus(node, debts),
    };
  });

  const debtTitles = {};
  for (const item of statusItems) debtTitles[item.id] = item.title;

  const screenshotsBySlug = findScreenshots(screenshotFiles);
  const nodesBySlug = {};
  for (const n of (nodesJson || [])) nodesBySlug[n.slug] = n;

  const screens = Object.keys(screenshotsBySlug).sort().map(slug => {
    const node = nodesBySlug[slug];
    return {
      slug,
      title: (node && node.title) || slug,
      arcKeys: (node && node.arcKeys) || [],
      liveUrl: `https://vgong24.github.io/Vextreme/pages/${slug}.html`,
      screenshots: screenshotsBySlug[slug],
    };
  });

  return {
    clusters: clusterMeta,
    nodes,
    edges,
    debtTitles,
    screens,
  };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const latticeMap = JSON.parse(fs.readFileSync(LATTICE_MAP_PATH, 'utf8'));
  const techDebt   = JSON.parse(fs.readFileSync(TECH_DEBT_PATH, 'utf8')).items;
  const openDisc   = JSON.parse(fs.readFileSync(OPEN_DISC_PATH, 'utf8')).items;
  const plannedEnh = JSON.parse(fs.readFileSync(PLANNED_ENH_PATH, 'utf8')).items;
  const nodesJson  = JSON.parse(fs.readFileSync(NODES_PATH, 'utf8'));
  const screenshotFiles = fs.existsSync(SCREENSHOTS_DIR)
    ? fs.readdirSync(SCREENSHOTS_DIR).filter(f => f.endsWith('.png'))
    : [];

  const statusItems = [...techDebt, ...openDisc, ...plannedEnh];
  const terrainMap = buildTerrainMap(latticeMap, statusItems, nodesJson, screenshotFiles);

  fs.writeFileSync(OUT_PATH, JSON.stringify(terrainMap, null, 2) + '\n');

  const counts = { good: 0, warning: 0, critical: 0 };
  for (const n of terrainMap.nodes) counts[n.status]++;

  console.log(`[build-terrain-map] Done.`);
  console.log(`  nodes    : ${terrainMap.nodes.length} (${counts.good} good, ${counts.warning} warning, ${counts.critical} critical)`);
  console.log(`  edges    : ${terrainMap.edges.length}`);
  console.log(`  clusters : ${terrainMap.clusters.length}`);
  console.log(`  screens  : ${terrainMap.screens.length}`);
  console.log(`  output   : ${OUT_PATH}`);
}

module.exports = {
  clusterOf, layoutClusters, computeStatus, findDebtReferences,
  computeEdges, findScreenshots, buildTerrainMap,
};

