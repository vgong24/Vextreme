#!/usr/bin/env node
/**
 * VEXTREME — lib/trace-string-usage.js
 *
 * String reverse-tracer: answers "where is this text used?" and "what does
 * this page use?" structurally instead of by tribal memory — the exact
 * question the wip/ registry-graph context notes named as a core capability
 * (getBindingsForString / getUIElementsByContext), scoped to what this repo
 * can validate today: data-i18n usage in pages/ and wip/ HTML cross-referenced
 * against data/strings/compiled/manifest.json's per-key language coverage.
 *
 * Three modes:
 *   --key=<key>    which file(s) reference this key, and its language coverage
 *   --page=<slug>  every key pages/{slug}.html references, each one's coverage,
 *                  and any key it references that doesn't exist in the manifest
 *                  (a typo'd or stale data-i18n attribute)
 *   (no args)      repo-wide summary: orphaned usages (HTML references a key
 *                  the manifest doesn't have) and unused keys (manifest has a
 *                  key no HTML file references)
 *
 * Read-only, informational — same posture as check-key-alignment.js, not a
 * blocking drift detector; a key with zero usages is often a legitimate
 * work-in-progress, not automatically dead.
 *
 * Run manually:   node lib/trace-string-usage.js
 *                 node lib/trace-string-usage.js --key=pages.foo.bar
 *                 node lib/trace-string-usage.js --page=victor-methodology-presentation
 *                 (any mode also accepts --json)
 *
 * Pure functions exported for testing:
 *   extractDataI18nKeys(html) → [key] (in document order, duplicates kept)
 *   scanUsages(files) → { key: [relPath] } (files: [{relPath, html}])
 *   traceKey(key, usages, manifest) → { key, usedIn, langs, inManifest }
 *   tracePage(relPath, usages, manifest) → { relPath, keys, orphanKeys }
 *   findOrphanUsages(usages, manifest) → [key]
 *   findUnusedKeys(usages, manifest) → [key]
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : string reverse-tracer — data-i18n usage in pages/wip HTML cross-referenced against the compiled manifest
 *   reads     : pages∕*.html (data-i18n attribute scan)
 *               wip∕*.html (data-i18n attribute scan)
 *               data/strings/compiled/manifest.json (per-key language coverage)
 *   writes    : stdout only — read-only, changes nothing
 *   loaded-by : manual run: node lib/trace-string-usage.js
 *               tests/27-trace-string-usage.test.js
 *   tested-by : tests/27-trace-string-usage.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     data/strings/compiled/manifest.json schema changed (the langs field):
 *       - traceKey()/tracePage() here read entry.langs directly
 *     a new content directory besides pages/ and wip/ can carry data-i18n attributes:
 *       - SCAN_DIRS here
 * LATTICE:END
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT           = path.join(__dirname, '..');
const MANIFEST_PATH  = path.join(ROOT, 'data', 'strings', 'compiled', 'manifest.json');
const SCAN_DIRS       = ['pages', 'wip'];

function repoPath(...parts) {
  return parts.join('/');
}

// ── Pure computation ─────────────────────────────────────────────────────────

// extractDataI18nKeys — every data-i18n="..." attribute value, in document
// order, duplicates kept (a page can legitimately reuse a key twice).
function extractDataI18nKeys(html) {
  const keys = [];
  const re = /data-i18n="([^"]+)"/g;
  let m;
  while ((m = re.exec(html))) keys.push(m[1]);
  return keys;
}

// scanUsages — builds key -> [relPath, ...] across every given file. A file
// referencing the same key twice contributes that path once (usedIn answers
// "which files", not "how many times per file").
function scanUsages(files) {
  const usages = {};
  for (const { relPath, html } of files) {
    for (const key of new Set(extractDataI18nKeys(html))) {
      (usages[key] = usages[key] || []).push(relPath);
    }
  }
  return usages;
}

// traceKey — everything known about one key: where it's used and what
// languages the manifest has for it (empty langs + inManifest:false means a
// typo'd or stale data-i18n attribute, not just an untranslated key).
function traceKey(key, usages, manifest) {
  const entry = manifest[key];
  return {
    key,
    usedIn:    (usages[key] || []).slice().sort(),
    inManifest: !!entry,
    langs:      entry ? entry.langs.slice().sort() : [],
  };
}

// tracePage — every key a specific file references, plus which of those
// keys the manifest doesn't recognize at all (orphaned within just this page).
function tracePage(relPath, usages, manifest) {
  const keys = Object.keys(usages).filter(k => usages[k].includes(relPath)).sort();
  return {
    relPath,
    keys: keys.map(k => traceKey(k, usages, manifest)),
    orphanKeys: keys.filter(k => !manifest[k]),
  };
}

// findOrphanUsages — keys some HTML file references that the manifest has
// never heard of. Usually a typo'd data-i18n value or a key renamed in
// source without updating the HTML (migrations.json handles the reverse:
// source-side renames; this catches the HTML-side half of that gap).
function findOrphanUsages(usages, manifest) {
  return Object.keys(usages).filter(k => !manifest[k]).sort();
}

// findUnusedKeys — manifest keys no scanned HTML file references. Not
// automatically dead: could be fetched by non-data-i18n JS, or legitimately
// mid-authoring. Informational only, like everything else here.
function findUnusedKeys(usages, manifest) {
  return Object.keys(manifest).filter(k => !usages[k]).sort();
}

// ── I/O ───────────────────────────────────────────────────────────────────────

function collectFiles(dirs) {
  const files = [];
  for (const dir of dirs) {
    const abs = path.join(ROOT, dir);
    if (!fs.existsSync(abs)) continue;
    for (const f of fs.readdirSync(abs)) {
      if (!f.endsWith('.html')) continue;
      const relPath = repoPath(dir, f);
      files.push({ relPath, html: fs.readFileSync(path.join(ROOT, dir, f), 'utf8') });
    }
  }
  return files;
}

if (require.main === module) {
  const args    = process.argv.slice(2);
  const jsonOut = args.includes('--json');
  const keyArg  = (args.find(a => a.startsWith('--key=')) || '').replace('--key=', '') || null;
  const pageArg = (args.find(a => a.startsWith('--page=')) || '').replace('--page=', '') || null;

  const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
  const usages   = scanUsages(collectFiles(SCAN_DIRS));

  let result;
  if (keyArg) {
    result = traceKey(keyArg, usages, manifest);
  } else if (pageArg) {
    result = tracePage(repoPath('pages', `${pageArg}.html`), usages, manifest);
  } else {
    result = {
      totalKeysInManifest: Object.keys(manifest).length,
      totalKeysUsed:       Object.keys(usages).length,
      orphanUsages:        findOrphanUsages(usages, manifest),
      unusedKeys:          findUnusedKeys(usages, manifest),
    };
  }

  if (jsonOut) {
    console.log(JSON.stringify(result, null, 2));
  } else if (keyArg) {
    console.log(`[trace-string-usage] ${result.key}`);
    console.log(`  in manifest : ${result.inManifest ? 'yes' : 'NO — key not found, check for a typo'}`);
    console.log(`  languages   : ${result.langs.join(', ') || '(none)'}`);
    console.log(`  used in     : ${result.usedIn.length ? result.usedIn.join(', ') : '(no HTML file references this key)'}`);
  } else if (pageArg) {
    console.log(`[trace-string-usage] ${result.relPath} — ${result.keys.length} key(s)`);
    for (const k of result.keys) {
      console.log(`  ${k.key} [${k.langs.join(',') || 'MISSING'}]`);
    }
    if (result.orphanKeys.length) {
      console.log(`  ${result.orphanKeys.length} orphan key(s) (referenced but not in the manifest): ${result.orphanKeys.join(', ')}`);
    }
  } else {
    console.log(`[trace-string-usage] ${result.totalKeysInManifest} key(s) in manifest, ${result.totalKeysUsed} referenced by pages/wip HTML`);
    console.log(`  orphan usages (HTML references, manifest doesn't have) : ${result.orphanUsages.length}`);
    if (result.orphanUsages.length) console.log(`    ${result.orphanUsages.join(', ')}`);
    console.log(`  unused keys (manifest has, no HTML references)        : ${result.unusedKeys.length}`);
  }
}

module.exports = { extractDataI18nKeys, scanUsages, traceKey, tracePage, findOrphanUsages, findUnusedKeys };

