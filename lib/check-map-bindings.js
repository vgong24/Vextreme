'use strict';

const fs   = require('fs');
const path = require('path');
const { parseLastSession, parseBatchRegistry, latestSessionFile } = require('./session-bootstrap');

const ROOT           = path.join(__dirname, '..');
const CLAUDE_MD      = path.join(ROOT, 'CLAUDE.md');
const INDEX_MD       = path.join(ROOT, 'docs', 'continuity', 'INDEX.md');
const CONTEXT_NOTES  = path.join(ROOT, 'docs', 'continuity', 'CONTEXT-NOTES.md');
const NOTES_DIR      = path.join(ROOT, 'docs', 'continuity', 'context-notes');

const SESSION_FILE_RE = /^(\d{4})-(\d{2})-(\d{2})-session-(\d+)\.md$/;

// ── Pure computation ─────────────────────────────────────────────────────────

// parseNoteRows — pulls `docs/continuity/context-notes/....md` paths out of
// CONTEXT-NOTES.md's Notes table (any backticked path under context-notes/).
function parseNoteRows(registryMd) {
  const rows = [];
  const re = /`(docs\/continuity\/context-notes\/[^`]+\.md)`/g;
  let m;
  while ((m = re.exec(registryMd))) rows.push(m[1]);
  return rows;
}

// checkContextNoteBindings — both directions: registry rows without files,
// note files without rows. noteFiles are bare filenames from the directory
// listing (README.md is the folder guide, not a note — exempt).
function checkContextNoteBindings(rowPaths, noteFiles) {
  const issues = [];
  const files = noteFiles.filter(f => f.endsWith('.md') && f !== 'README.md');
  const rowNames = rowPaths.map(p => p.split('/').pop());
  for (const p of rowPaths) {
    if (!files.includes(p.split('/').pop())) {
      issues.push({ check: 'context-notes', message: `CONTEXT-NOTES.md lists ${p} but the file does not exist` });
    }
  }
  for (const f of files) {
    if (!rowNames.includes(f)) {
      issues.push({ check: 'context-notes', message: `docs/continuity/context-notes/${f} has no row in CONTEXT-NOTES.md` });
    }
  }
  return issues;
}

// parseActiveBatchLine — extracts the path from INDEX.md's
// "**Active batch:** `path`" line.
function parseActiveBatchLine(indexMd) {
  const m = indexMd.match(/\*\*Active batch:\*\*\s*`([^`]+)`/);
  return m ? m[1] : null;
}

// sessionRange — parses a registry row's Sessions field like "021–030".
function sessionRange(sessionsField) {
  const m = sessionsField.match(/(\d+)\s*[–—-]\s*(\d+)/);
  return m ? { lo: parseInt(m[1], 10), hi: parseInt(m[2], 10) } : null;
}

// checkSessionFiles — every .md in a batch directory is either README.md or a
// convention-named session file whose number sits inside the declared range.
function checkSessionFiles(fileNames, range) {
  const issues = [];
  for (const f of fileNames) {
    if (!f.endsWith('.md') || f === 'README.md') continue;
    const m = f.match(SESSION_FILE_RE);
    if (!m) {
      issues.push({ check: 'batch-directory', message: `${f} does not match YYYY-MM-DD-session-0NN.md` });
      continue;
    }
    const n = parseInt(m[4], 10);
    if (range && (n < range.lo || n > range.hi)) {
      issues.push({ check: 'batch-directory', message: `${f} is session ${m[4]}, outside the registry's declared range ${String(range.lo).padStart(3, '0')}–${String(range.hi).padStart(3, '0')}` });
    }
  }
  return issues;
}

// checkLastUpdatedBinding — INDEX.md's "Last updated: Session NNN" must name
// the same session as the newest file on disk. This is the binding whose
// absence let Session 023's entries hide inside Session 021's record: the
// index claimed one "latest", the record held another, and nothing compared them.
function checkLastUpdatedBinding(lastSession, newestSessionFile) {
  if (!lastSession || !newestSessionFile) return [];
  const m = newestSessionFile.match(SESSION_FILE_RE);
  if (!m) return [];
  if (parseInt(m[4], 10) !== parseInt(lastSession, 10)) {
    return [{ check: 'last-updated', message: `INDEX.md says "Last updated: Session ${lastSession}" but the newest session file on disk is ${newestSessionFile} (session ${m[4]})` }];
  }
  return [];
}

// ── I/O — only runs when executed directly ────────────────────────────────────

function run() {
  const issues = [];

  const claudeMd   = fs.readFileSync(CLAUDE_MD, 'utf8');
  const indexMd    = fs.readFileSync(INDEX_MD, 'utf8');
  const registryMd = fs.readFileSync(CONTEXT_NOTES, 'utf8');

  // 1. CLAUDE.md → CONTEXT-NOTES.md pointer
  if (!claudeMd.includes('docs/continuity/CONTEXT-NOTES.md')) {
    issues.push({ check: 'claude-md', message: 'CLAUDE.md no longer points at docs/continuity/CONTEXT-NOTES.md' });
  }

  // 2. CONTEXT-NOTES.md ↔ context-notes/ files
  issues.push(...checkContextNoteBindings(parseNoteRows(registryMd), fs.readdirSync(NOTES_DIR)));

  // 3. Batch registry rows exist; single active row; Active batch line agrees
  const rows = parseBatchRegistry(indexMd);
  const activeRows = rows.filter(r => /active/i.test(r.status));
  for (const row of rows) {
    if (!fs.existsSync(path.join(ROOT, row.file))) {
      issues.push({ check: 'batch-registry', message: `Batch Registry row ${row.batch} points at ${row.file}, which does not exist` });
    }
  }
  if (activeRows.length !== 1) {
    issues.push({ check: 'batch-registry', message: `expected exactly 1 active batch row, found ${activeRows.length}` });
  } else if (rows[rows.length - 1] !== activeRows[0]) {
    issues.push({ check: 'batch-registry', message: `the active batch row (${activeRows[0].batch}) is not the last registry row` });
  }
  const activeLine = parseActiveBatchLine(indexMd);
  if (activeRows.length === 1 && activeLine !== activeRows[0].file) {
    issues.push({ check: 'batch-registry', message: `"Active batch:" line says ${activeLine} but the registry's active row says ${activeRows[0].file}` });
  }

  // 4 + 5. Directory-form active batch: README, naming, range, Last updated
  if (activeRows.length === 1) {
    const active = activeRows[0];
    const batchPath = path.join(ROOT, active.file);
    if (fs.existsSync(batchPath) && fs.statSync(batchPath).isDirectory()) {
      const listing = fs.readdirSync(batchPath);
      if (!listing.includes('README.md')) {
        issues.push({ check: 'batch-directory', message: `${active.file} has no README.md folder guide` });
      }
      issues.push(...checkSessionFiles(listing, sessionRange(active.sessions)));
      const newest = latestSessionFile(listing);
      if (!newest) {
        issues.push({ check: 'batch-directory', message: `${active.file} contains no YYYY-MM-DD-session-0NN.md session files` });
      }
      issues.push(...checkLastUpdatedBinding(parseLastSession(indexMd), newest));
    }
  }

  return issues;
}

if (require.main === module) {
  const issues = run();
  if (process.argv.includes('--json')) {
    console.log(JSON.stringify({ clean: issues.length === 0, issues }, null, 2));
  } else {
    console.log(`[check-map-bindings] ${issues.length === 0 ? 'All map bindings hold.' : issues.length + ' binding issue(s):'}`);
    for (const i of issues) console.log(`  [${i.check}] ${i.message}`);
  }
  if (issues.length > 0) process.exit(1);
}

module.exports = {
  parseNoteRows,
  checkContextNoteBindings,
  parseActiveBatchLine,
  sessionRange,
  checkSessionFiles,
  checkLastUpdatedBinding,
  run,
};

