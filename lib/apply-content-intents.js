#!/usr/bin/env node
/**
 * VEXTREME — lib/apply-content-intents.js
 *
 * Applies declared content-placement intents (config/content-intents.json) —
 * the "input a JSON of what you want to put where, let code place it" pattern
 * Victor asked for (Session 022), generalizing the existing vex:department/
 * vex:workType meta-tag convention (lib/auto-discover-nodes.js) to arc
 * membership as well, instead of hand-editing pages/*.html and
 * data/arcs-v2.json directly for every placement.
 *
 * A pending intent can declare any combination of:
 *   department / workType — upserted as <meta name="vex:department"/"vex:workType">
 *     tags on the target page (idempotent: replaces an existing tag, inserts
 *     a new one before </head> if absent)
 *   arcKey — adds the slug to that arc's one auto-managed section in
 *     data/arcs-v2.json (label AUTO_SECTION_LABEL below), created on first
 *     use. Before adding, the slug is removed from every OTHER arc's
 *     auto-managed section — a slug can only be auto-placed in one arc at a
 *     time, and re-declaring its placement moves it rather than duplicating
 *     it. A slug already present in some arc's hand-curated (non-auto)
 *     section is left alone entirely — this tool never touches curated
 *     placement, only the one section it owns.
 *
 * Before writing anything, each intent is validated: a declared department
 * must already be registered in data/departments.json, and a declared arcKey
 * must exist in data/arcs-v2.json. This isn't defensive hedging — running
 * this tool against real content (Session 022) found that neither condition
 * was previously enforced anywhere: lib/build-index.js's buildDepartmentMap
 * groups content by whatever string is on the meta tag with no validation,
 * but lib/build-ecosystem-hub.js's renderDepartments() only ever iterates
 * departmentMeta's *registered* keys — so an unregistered department's
 * content would exist in data/index.json but never render anywhere, with no
 * error surfaced. A rejected intent is skipped (left as status:"pending")
 * rather than applied partially, and the run exits non-zero.
 *
 * After applying, runs the real sanity check: re-run lib/build-index.js (its
 * own duplicate-slug guard is the actual BLOCK gate) and report
 * lib/check-key-alignment.js's output, the same informational check every
 * other content change in this repo is verified against.
 *
 * Pure functions exported for testing:
 *   upsertMetaTag(html, name, content) → string
 *   applyArcPlacement(arcsDef, arcKey, slug, autoLabel) → arcsDef (new object, arcsDef untouched)
 *   validateIntent(intent, departmentsDef, arcsDef) → { valid, errors: string[] }
 *   applyIntent(intent, html, arcsDef, autoLabel) → { html, arcsDef, changed: string[] }
 *
 * Run manually:   node lib/apply-content-intents.js
 *                 node lib/apply-content-intents.js --dry-run   (applies in-memory only, writes nothing)
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : applies config/content-intents.json's pending entries — mutates pages∕*.html meta tags and data/arcs-v2.json's auto-managed sections, then runs the existing sanity-check pipeline
 *   reads     : config/content-intents.json
 *               pages∕*.html (only the slugs named by pending intents)
 *               data/arcs-v2.json
 *               data/departments.json (validation only)
 *   writes    : pages∕*.html (meta tag upserts, only for slugs named by pending intents)
 *               data/arcs-v2.json (auto-managed section only)
 *               config/content-intents.json (marks applied intents, with appliedAt)
 *   loaded-by : manual run: node lib/apply-content-intents.js
 *               tests/18-apply-content-intents.test.js
 *   tested-by : tests/18-apply-content-intents.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     content-intents.json schema changed (new field, field renamed):
 *       - applyIntent() in this file
 *       - tests/18-apply-content-intents.test.js
 *     vex:department / vex:workType meta tag convention changed:
 *       - lib/auto-discover-nodes.js parsePageMeta() (must stay compatible with what this file writes)
 *     AUTO_SECTION_LABEL or arc-placement rule changed:
 *       - lib/build-archives.js getArcSlugs() / lib/build-index.js buildArcMap() — both must keep treating an auto-managed section as an ordinary explicit section (no special-casing needed as long as the label doesn't collide with a real curated label)
 * LATTICE:END
 */

'use strict';

const fs            = require('fs');
const path           = require('path');
const { execFileSync } = require('child_process');

const ROOT          = path.join(__dirname, '..');
const INTENTS_PATH   = path.join(ROOT, 'config', 'content-intents.json');
const PAGES_DIR      = path.join(ROOT, 'pages');
const ARCS_PATH      = path.join(ROOT, 'data', 'arcs-v2.json');
const DEPARTMENTS_PATH = path.join(ROOT, 'data', 'departments.json');

const AUTO_SECTION_LABEL = 'Auto-Placed (lib/apply-content-intents.js)';

// ── Pure computation ──────────────────────────────────────────────────────────

// upsertMetaTag — replaces an existing <meta name="{name}" content="..."> tag
// (any content value) with the new one, or inserts it if absent. Matches the
// exact tag shape lib/auto-discover-nodes.js's parsePageMeta() already
// parses — that function is a plain regex over the whole file, not a DOM
// query, so the tag doesn't need to sit inside a real <head> to be readable.
// This matters because many pages/*.html files are Squarespace paste-block
// fragments with no <html>/<head> wrapper at all (verified against
// pages/about-me.html and pages/connect.html, Session 022) — inserted before
// </head> when one exists, otherwise prepended at the top of the file.
function upsertMetaTag(html, name, content) {
  const re  = new RegExp(`<meta\\s+name=["']${name}["']\\s+content=["'][^"']*["']\\s*/?>`, 'i');
  const tag = `<meta name="${name}" content="${content}">`;
  if (re.test(html)) return html.replace(re, tag);
  if (/<\/head>/i.test(html)) return html.replace(/<\/head>/i, `  ${tag}\n</head>`);
  return `${tag}\n${html}`;
}

// applyArcPlacement — moves `slug` into arcKey's auto-managed section,
// removing it from every other arc's auto-managed section first. Never
// touches a hand-curated (non-auto-label) section — if the slug is already
// curated in the target arc, this is a no-op for that arc. Returns a new
// object; the input arcsDef is never mutated.
function applyArcPlacement(arcsDef, arcKey, slug, autoLabel) {
  const next = JSON.parse(JSON.stringify(arcsDef));

  for (const key of Object.keys(next)) {
    if (key.startsWith('_')) continue;
    for (const section of next[key].sections || []) {
      if (section.label === autoLabel) {
        section.slugs = (section.slugs || []).filter(s => s !== slug);
      }
    }
  }

  const target = next[arcKey];
  if (!target) throw new Error(`applyArcPlacement: unknown arc "${arcKey}"`);

  const alreadyCurated = (target.sections || []).some(
    s => s.label !== autoLabel && (s.slugs || []).includes(slug)
  );
  if (alreadyCurated) return next;

  let autoSection = (target.sections || []).find(s => s.label === autoLabel);
  if (!autoSection) {
    autoSection = { label: autoLabel, order: 'explicit', slugs: [] };
    target.sections = (target.sections || []).concat([autoSection]);
  }
  if (!autoSection.slugs.includes(slug)) autoSection.slugs.push(slug);

  return next;
}

// validateIntent — catches the two ways an intent can produce silent,
// invisible content rather than an obvious error: a department not in
// data/departments.json's registry (buildDepartmentMap groups content by
// whatever string is on the meta tag with no validation, but
// lib/build-ecosystem-hub.js's renderDepartments() only ever iterates
// departmentMeta's registered keys — an unregistered department's content
// exists in data/index.json but is never rendered anywhere, with no error),
// or an arcKey that doesn't exist in arcs-v2.json (applyArcPlacement throws,
// but a thrown error mid-loop would abort every remaining intent in the same
// run — validating up front lets one bad intent get skipped and reported
// without blocking the rest). Found by actually running this tool against
// real content (Session 022), not anticipated in advance.
function validateIntent(intent, departmentsDef, arcsDef) {
  const errors = [];
  if (intent.department) {
    const known = Object.keys(departmentsDef || {}).filter(k => !k.startsWith('_'));
    if (!known.includes(intent.department)) {
      errors.push(`department "${intent.department}" is not registered in data/departments.json (known: ${known.join(', ')})`);
    }
  }
  if (intent.arcKey) {
    const known = Object.keys(arcsDef || {}).filter(k => !k.startsWith('_'));
    if (!known.includes(intent.arcKey)) {
      errors.push(`arcKey "${intent.arcKey}" is not a real arc in data/arcs-v2.json`);
    }
  }
  return { valid: errors.length === 0, errors };
}

// applyIntent — applies one intent against in-memory html + arcsDef, returning
// the updated versions plus a human-readable log of what changed. Does no I/O.
// Caller is expected to have already validated the intent (validateIntent) —
// this function assumes department/arcKey are safe to write.
function applyIntent(intent, html, arcsDef, autoLabel) {
  let nextHtml = html;
  let nextArcs = arcsDef;
  const changed = [];

  if (intent.department) {
    nextHtml = upsertMetaTag(nextHtml, 'vex:department', intent.department);
    changed.push(`department=${intent.department}`);
  }
  if (intent.workType) {
    nextHtml = upsertMetaTag(nextHtml, 'vex:workType', intent.workType);
    changed.push(`workType=${intent.workType}`);
  }
  if (intent.arcKey) {
    nextArcs = applyArcPlacement(nextArcs, intent.arcKey, intent.slug, autoLabel);
    changed.push(`arc=${intent.arcKey} (auto-placed)`);
  }

  return { html: nextHtml, arcsDef: nextArcs, changed };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const dryRun = process.argv.includes('--dry-run');

  const manifest = JSON.parse(fs.readFileSync(INTENTS_PATH, 'utf8'));
  const pending   = manifest.intents.filter(i => i.status === 'pending');

  if (!pending.length) {
    console.log('[apply-content-intents] No pending intents. Nothing to do.');
    process.exit(0);
  }

  let arcsDef = JSON.parse(fs.readFileSync(ARCS_PATH, 'utf8'));
  const departmentsDef = JSON.parse(fs.readFileSync(DEPARTMENTS_PATH, 'utf8'));
  const pageWrites = {};
  let hadInvalid = false;

  for (const intent of pending) {
    const validation = validateIntent(intent, departmentsDef, arcsDef);
    if (!validation.valid) {
      hadInvalid = true;
      console.error(`[apply-content-intents] Rejected "${intent.id}": ${validation.errors.join('; ')}`);
      continue;
    }

    const pagePath = path.join(PAGES_DIR, `${intent.slug}.html`);
    if (!fs.existsSync(pagePath)) {
      hadInvalid = true;
      console.error(`[apply-content-intents] Skipping "${intent.id}" — no pages/${intent.slug}.html`);
      continue;
    }
    const html = pageWrites[intent.slug] || fs.readFileSync(pagePath, 'utf8');
    const result = applyIntent(intent, html, arcsDef, AUTO_SECTION_LABEL);
    pageWrites[intent.slug] = result.html;
    arcsDef = result.arcsDef;

    intent.status     = 'applied';
    intent.appliedAt  = new Date().toISOString();
    console.log(`[apply-content-intents] ${intent.slug}: ${result.changed.join(', ')}`);
  }

  if (!dryRun) {
    for (const [slug, html] of Object.entries(pageWrites)) {
      fs.writeFileSync(path.join(PAGES_DIR, `${slug}.html`), html);
    }
    fs.writeFileSync(ARCS_PATH, JSON.stringify(arcsDef, null, 2));
    fs.writeFileSync(INTENTS_PATH, JSON.stringify(manifest, null, 2));
    console.log(`[apply-content-intents] Wrote ${Object.keys(pageWrites).length} page(s), data/arcs-v2.json, and config/content-intents.json.`);

    console.log('[apply-content-intents] Running sanity check: rebuilding data/index.json...');
    execFileSync('node', [path.join(ROOT, 'lib', 'build-index.js')], { stdio: 'inherit' });

    const { checkKeyAlignment } = require('./check-key-alignment');
    const report = checkKeyAlignment();
    console.log('[apply-content-intents] check-key-alignment report:', JSON.stringify(report, null, 2));
  } else {
    console.log(`[apply-content-intents] --dry-run: computed changes for ${Object.keys(pageWrites).length} page(s), wrote nothing.`);
  }

  if (hadInvalid) {
    console.error('[apply-content-intents] One or more intents were rejected or skipped — see above. Left as status:"pending" for a corrected re-run.');
    process.exit(1);
  }
}

module.exports = { upsertMetaTag, applyArcPlacement, applyIntent, validateIntent, AUTO_SECTION_LABEL };

