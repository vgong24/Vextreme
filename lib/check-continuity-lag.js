#!/usr/bin/env node
/**
 * VEXTREME — lib/check-continuity-lag.js
 *
 * Informational check: how many PRs have merged since the newest continuity
 * session file was last touched? Built after a real gap was found by hand
 * (Session 027's cold-start review): 15 merged PRs (#76-#92), including a
 * six-PR bug chain significant enough to become standing doctrine in
 * docs/culture.md, landed with no session file and no INDEX.md refresh.
 * Every other drift detector in this repo (check-map-bindings.js,
 * check-lattice-edges.js, check-key-alignment.js, build-lattice-headers.js
 * --check) was clean the entire time, because none of them compare "how much
 * merged" against "how much narrated" — they verify internal map consistency,
 * not narrative freshness. This is that missing comparison.
 *
 * Deliberately informational, not blocking (same posture as
 * check-key-alignment.js): a PR count crossing the threshold means "a session
 * entry is probably overdue," not "this PR is wrong." Blocking on it would
 * penalize the PR that happens to be the Nth one, not the process gap that
 * let N grow unnoticed. pe-014 (renamed from pe-012 in this same session —
 * see data/status/planned-enhancements.json) names the same start-soft-
 * then-earn-trust sequencing for a different check; this follows the same
 * discipline.
 *
 * THRESHOLD is a first guess (8), not a proven number — picked to sit above
 * a normal single-session continuation cadence (Session 025's four
 * continuations spanned PRs #70-75, six PRs, in one day) and below the 15-PR
 * gap that motivated this file. Tune it if it proves noisy or too late.
 *
 * Run manually:   node lib/check-continuity-lag.js
 *                 node lib/check-continuity-lag.js --json
 *
 * Pure functions exported for testing:
 *   countMergeCommits(gitLogText) → number
 *   evaluateLag(mergeCount, threshold) → { flagged, message }
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : informational check comparing merged-PR count against how recently the newest continuity session file was touched
 *   reads     : docs/continuity/INDEX.md (batch registry, via lib/session-bootstrap.js's parseBatchRegistry)
 *               active batch directory listing (newest session file, via lib/session-bootstrap.js's latestSessionFile)
 *               git log (last commit touching the newest session file; commit range since then, via subprocess)
 *   writes    : stdout only — read-only, changes nothing; always exits 0 (informational)
 *   loaded-by : manual run: node lib/check-continuity-lag.js
 *               lib/session-bootstrap.js (report subprocess)
 *               tests/32-check-continuity-lag.test.js
 *   tested-by : tests/32-check-continuity-lag.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     INDEX.md's Batch Registry table format, or the YYYY-MM-DD-session-0NN.md filename convention, changed:
 *       - this file reuses lib/session-bootstrap.js's parseBatchRegistry()/latestSessionFile() directly — no separate parsing here, so it moves automatically with those, but confirm the shared assumption still holds
 *     THRESHOLD proves too noisy (flags normal sessions) or too late (another multi-PR gap slips through):
 *       - it's a single named constant at the top of the file, deliberately not derived from anything — retune it directly, no downstream consumers assume its value
 * LATTICE:END
 */

'use strict';

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');
const { parseBatchRegistry, latestSessionFile } = require('./session-bootstrap');

const ROOT = path.join(__dirname, '..');
const INDEX_MD = path.join(ROOT, 'docs', 'continuity', 'INDEX.md');

const THRESHOLD = 8;

// ── Pure computation ─────────────────────────────────────────────────────────

// countMergeCommits — counts "Merge pull request #N" lines in a
// `git log --oneline` text blob. Pure string parsing so it's testable
// against a fixture blob without shelling out to git.
function countMergeCommits(gitLogText) {
  if (!gitLogText) return 0;
  const lines = gitLogText.split('\n').filter(l => /Merge pull request #\d+/.test(l));
  return lines.length;
}

// evaluateLag — given a merge-commit count and threshold, decides whether
// this is worth flagging and what to say about it.
function evaluateLag(mergeCount, threshold) {
  if (mergeCount >= threshold) {
    return {
      flagged: true,
      message: `${mergeCount} PRs have merged since the newest continuity session file was last touched (threshold: ${threshold}). A session entry is likely overdue — see docs/continuity/INDEX.md's "New session file template."`,
    };
  }
  return {
    flagged: false,
    message: `${mergeCount} PR(s) merged since the newest continuity session file — under the ${threshold}-PR informational threshold.`,
  };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

function runGit(args) {
  try {
    return { ok: true, output: execFileSync('git', args, { cwd: ROOT, encoding: 'utf8' }) };
  } catch (e) {
    return { ok: false, output: ((e.stdout || '') + (e.stderr || '')).trim() };
  }
}

function run() {
  const indexMd = fs.readFileSync(INDEX_MD, 'utf8');
  const batchRows = parseBatchRegistry(indexMd);
  const activeBatch = batchRows[batchRows.length - 1] || null;

  if (!activeBatch) {
    return { skipped: true, reason: 'no active batch row found in INDEX.md' };
  }

  const batchPath = path.join(ROOT, activeBatch.file);
  if (!fs.existsSync(batchPath) || !fs.statSync(batchPath).isDirectory()) {
    return { skipped: true, reason: `active batch ${activeBatch.file} is not a directory (legacy single-file batch — lag check does not apply)` };
  }

  const newest = latestSessionFile(fs.readdirSync(batchPath));
  if (!newest) {
    return { skipped: true, reason: `${activeBatch.file} contains no session files` };
  }

  const newestRelPath = path.join(activeBatch.file, newest).split(path.sep).join('/');

  // Find the last commit that touched the newest session file — this moves
  // forward automatically as continuation blocks get appended, so an
  // in-progress session's own continuations never count as lag against
  // themselves.
  const lastTouch = runGit(['log', '-1', '--format=%H', '--', newestRelPath]);
  if (!lastTouch.ok || !lastTouch.output.trim()) {
    return { skipped: true, reason: `could not resolve last commit touching ${newestRelPath} (shallow clone or file not yet committed?)` };
  }
  const sinceHash = lastTouch.output.trim();

  const gitLog = runGit(['log', '--oneline', `${sinceHash}..HEAD`]);
  if (!gitLog.ok) {
    // A shallow checkout that doesn't contain sinceHash fails this range
    // query. Report gracefully rather than claiming a false "0 PRs" —
    // an inaccurate all-clear is worse than an honest "can't tell here."
    return { skipped: true, reason: `git log ${sinceHash}..HEAD failed — likely a shallow clone that does not contain ${sinceHash.slice(0, 7)}` };
  }

  const mergeCount = countMergeCommits(gitLog.output);
  const result = evaluateLag(mergeCount, THRESHOLD);

  return { skipped: false, newestSessionFile: newestRelPath, mergeCount, threshold: THRESHOLD, ...result };
}

if (require.main === module) {
  const report = run();

  if (process.argv.includes('--json')) {
    console.log(JSON.stringify(report, null, 2));
  } else if (report.skipped) {
    console.log(`[check-continuity-lag] Skipped — ${report.reason}`);
  } else {
    console.log(`[check-continuity-lag] ${report.flagged ? '⚠️' : '✅'} ${report.message}`);
  }

  // Informational only — never fails CI. See file header for why.
  process.exit(0);
}

module.exports = { countMergeCommits, evaluateLag, run };

