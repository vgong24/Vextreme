#!/usr/bin/env node
/**
 * VEXTREME — lib/bump-fab-version.js
 *
 * Git-diffing orchestration for lib/compute-fab-version.js's pure
 * classification logic: finds the last commit that actually bumped
 * widgets/fab-lang.js's VERSION constant, diffs the relevant data files
 * between that commit and the current working tree, classifies the
 * result into a semver tier, and — if any tier applies — rewrites
 * VERSION in place. Replaces "a human remembers to bump this" with "CI
 * computes it every run"; see lib/compute-fab-version.js's docstring for
 * the full major/minor/patch mapping and why.
 *
 * Run manually:  node lib/bump-fab-version.js            (writes if a bump applies)
 *                node lib/bump-fab-version.js --dry-run   (reports only, no write)
 * Auto-run via:  .github/workflows/build-index.yml, before the artifact-commit step
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : git-diffing orchestration for lib/compute-fab-version.js — finds the last VERSION-bump commit, diffs data files against it, applies the computed bump to widgets/fab-lang.js
 *   reads     : git log/show/diff history (via child_process, not require())
 *               data/nodes.json (current + at baseline commit)
 *               data/strings/source∕**∕*.json (current + at baseline commit)
 *               data/index.json's supportedLangs (current + at baseline commit)
 *               data/arcs-v2.json (current + at baseline commit)
 *               data/departments.json (current + at baseline commit)
 *               lib/compute-fab-version.js
 *   writes    : widgets/fab-lang.js (rewrites the VERSION line in place, if a bump tier applies)
 *   loaded-by : .github/workflows/build-index.yml ('Auto-increment fab-lang.js CDN version' step, before the artifact-commit step)
 *   tested-by : (none)
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     widgets/fab-lang.js's VERSION line format changes (e.g. quote style, variable name):
 *       - VERSION_RE here — the regex that both finds and rewrites the line
 *     a new data file needs to feed into version classification:
 *       - computeSignals() here — add the read + diff call
 *       - lib/compute-fab-version.js — add or reuse a diff helper
 *       - docs/lattice-map.json's own reads list here
 * LATTICE:END
 */

'use strict';

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const {
  bumpVersion,
  classifyChanges,
  diffNodeIdentity,
  diffStrings,
  diffKeyedCollection,
  diffLocales,
} = require('./compute-fab-version');

const ROOT      = path.join(__dirname, '..');
const FAB_LANG  = path.join(ROOT, 'widgets', 'fab-lang.js');
const VERSION_RE = /var VERSION\s*=\s*'(\d+\.\d+\.\d+)';/;

function git(args) {
  // stderr suppressed: every call site below wraps this in try/catch and
  // handles the failure case explicitly (empty baseline, missing file,
  // no commits since baseline, etc.) — git's own error text on expected
  // failures would otherwise read as a real problem in CI logs.
  return execFileSync('git', args, { cwd: ROOT, encoding: 'utf8', stdio: ['pipe', 'pipe', 'ignore'] }).trim();
}

// findBaselineCommit — the last commit whose diff touched a VERSION-shaped
// line in widgets/fab-lang.js. -G (not -S) is required here: -S only
// catches a change in how many times a string occurs, but "var VERSION"
// occurs exactly once both before and after a bump — only the number
// after it changes. -G matches on "a line matching this regex appears in
// the diff," which is what a same-line value edit actually produces.
function findBaselineCommit() {
  let sha = '';
  try {
    sha = git(['log', '-1', '--format=%H', '-G', "var VERSION\\s*=\\s*'[0-9]", '--', 'widgets/fab-lang.js']);
  } catch (e) { /* fall through to the add-commit fallback below */ }
  if (sha) return sha;

  // No bump commit found (e.g. VERSION has literally never changed since
  // the file was added) — fall back to the commit that introduced the file.
  return git(['log', '--diff-filter=A', '-1', '--format=%H', '--', 'widgets/fab-lang.js']);
}

function readFileAtCommit(sha, relPath) {
  try {
    return git(['show', `${sha}:${relPath}`]);
  } catch (e) {
    return null; // file didn't exist at that commit (expected — see git()'s stderr note)
  }
}

function readJSONAtCommit(sha, relPath, fallback) {
  const raw = readFileAtCommit(sha, relPath);
  if (raw === null) return fallback;
  try { return JSON.parse(raw); } catch (e) { return fallback; }
}

function readCurrentJSON(relPath, fallback) {
  const abs = path.join(ROOT, relPath);
  if (!fs.existsSync(abs)) return fallback;
  try { return JSON.parse(fs.readFileSync(abs, 'utf8')); } catch (e) { return fallback; }
}

// mergedStrings — flattens every data/strings/source/**/*.json file's
// string keys into one { key: text } map, skipping _meta blocks. Reads
// the real directory tree at HEAD for the "new" side and via git ls-tree
// for the "old" side (a commit's tree, not the working directory).
function mergedStringsAtCommit(sha) {
  let files;
  try {
    files = git(['ls-tree', '-r', '--name-only', sha, '--', 'data/strings/source'])
      .split('\n').filter(f => f.endsWith('.json'));
  } catch (e) {
    return {};
  }
  const merged = {};
  for (const f of files) {
    const data = readJSONAtCommit(sha, f, {});
    flattenInto(merged, data);
  }
  return merged;
}

function mergedStringsCurrent() {
  const dir = path.join(ROOT, 'data', 'strings', 'source');
  if (!fs.existsSync(dir)) return {};
  const merged = {};
  (function walk(d) {
    for (const entry of fs.readdirSync(d, { withFileTypes: true })) {
      const full = path.join(d, entry.name);
      if (entry.isDirectory()) { walk(full); continue; }
      if (!entry.name.endsWith('.json')) continue;
      try { flattenInto(merged, JSON.parse(fs.readFileSync(full, 'utf8'))); } catch (e) { /* skip unparseable */ }
    }
  }(dir));
  return merged;
}

function flattenInto(merged, data) {
  for (const [key, value] of Object.entries(data || {})) {
    if (key === '_meta') continue;
    merged[key] = value;
  }
}

// checksAlignmentTouched — did anything under lib/check-*.js or
// lib/build-lattice-headers.js change between the baseline commit and now?
function checksAlignmentTouched(sha) {
  let changed = [];
  try {
    changed = git(['diff', '--name-only', sha, 'HEAD', '--', 'lib/check-*.js', 'lib/build-lattice-headers.js'])
      .split('\n').filter(Boolean);
  } catch (e) { /* no commits since baseline, or baseline is HEAD */ }
  // Also cover uncommitted working-tree changes to the same paths (this
  // script may run before those changes are committed in the same session).
  let uncommitted = [];
  try {
    uncommitted = git(['diff', '--name-only', '--', 'lib/check-*.js', 'lib/build-lattice-headers.js'])
      .split('\n').filter(Boolean);
  } catch (e) { /* ignore */ }
  return changed.length > 0 || uncommitted.length > 0;
}

function computeSignals(baselineSha) {
  const oldNodes = readJSONAtCommit(baselineSha, 'data/nodes.json', []);
  const newNodes = readCurrentJSON('data/nodes.json', []);
  const { idsChanged, slugsRenamed } = diffNodeIdentity(oldNodes, newNodes);

  const oldStrings = mergedStringsAtCommit(baselineSha);
  const newStrings = mergedStringsCurrent();
  const { newStrings: hasNewStrings, modifiedStrings } = diffStrings(oldStrings, newStrings);

  const oldIndex = readJSONAtCommit(baselineSha, 'data/index.json', {});
  const newIndex = readCurrentJSON('data/index.json', {});
  const newLocale = diffLocales(oldIndex.supportedLangs, newIndex.supportedLangs);

  const oldArcs = readJSONAtCommit(baselineSha, 'data/arcs-v2.json', {});
  const newArcs = readCurrentJSON('data/arcs-v2.json', {});
  const newArc = diffKeyedCollection(oldArcs, newArcs);

  const oldDepts = readJSONAtCommit(baselineSha, 'data/departments.json', {});
  const newDepts = readCurrentJSON('data/departments.json', {});
  const newDepartment = diffKeyedCollection(oldDepts, newDepts);

  const checksAlignment = checksAlignmentTouched(baselineSha);

  return { idsChanged, slugsRenamed, newStrings: hasNewStrings, modifiedStrings, newLocale, newArc, newDepartment, checksAlignment };
}

function main() {
  const dryRun = process.argv.includes('--dry-run');

  const src = fs.readFileSync(FAB_LANG, 'utf8');
  const match = VERSION_RE.exec(src);
  if (!match) {
    console.error('[bump-fab-version] Could not find a VERSION line in widgets/fab-lang.js — nothing to do.');
    process.exit(1);
  }
  const currentVersion = match[1];

  const baselineSha = findBaselineCommit();
  const signals = computeSignals(baselineSha);
  const tier = classifyChanges(signals);
  const nextVersion = bumpVersion(currentVersion, tier);

  console.log('[bump-fab-version] baseline commit:', baselineSha);
  console.log('[bump-fab-version] signals:', JSON.stringify(signals));
  console.log('[bump-fab-version] tier:', tier || '(none — no bump needed)');
  console.log('[bump-fab-version] version:', currentVersion, '->', nextVersion);

  if (tier === null) {
    console.log('[bump-fab-version] No qualifying change detected. Leaving VERSION as-is.');
    return;
  }

  if (dryRun) {
    console.log('[bump-fab-version] --dry-run: not writing.');
    return;
  }

  const updated = src.replace(VERSION_RE, `var VERSION    = '${nextVersion}';`);
  fs.writeFileSync(FAB_LANG, updated);
  console.log(`[bump-fab-version] Wrote widgets/fab-lang.js: VERSION ${currentVersion} -> ${nextVersion}`);
}

if (require.main === module) {
  main();
}

module.exports = { findBaselineCommit, computeSignals };

