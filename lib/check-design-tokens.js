#!/usr/bin/env node
/**
 * VEXTREME — lib/check-design-tokens.js
 *
 * Verifies every var(--token) reference in generated pages and stylesheets
 * resolves to a token that actually exists at runtime — the automated check
 * named in od-004/od-005 (data/status/open-discussions.json), built to catch
 * the exact bug class Session 015 found by hand: lib/build-ecosystem-hub.js
 * referenced var(--stone-950) and var(--font-mono), neither of which exists
 * anywhere. Some had CSS fallback values so they partially "worked" (rendered
 * a color) while others had none and silently inherited the wrong value —
 * reading the code gave no signal either way; only resolving it against the
 * actual token set does.
 *
 * Two legitimate token families exist in this repo, both declared in
 * styles/design-system.css (see docs/architecture/12-design-system.md):
 *   - the global light theme in its :root block (9 tokens: cream, stone,
 *     muted, border, ember, ember-bg, serif, sans, mono)
 *   - the dashboard dark theme in its [data-theme="dashboard"] block (bg,
 *     surface, text, muted, ember, border, mono, sans, blue) — consolidated
 *     here in Session 017 from four identical inline :root copies
 *     (lib/build-archives.js, build-demo.js, build-specimens.js,
 *     pages/specimen-architectural-wisdoms.html; see td-007, now closed)
 * lib/build-index-page.js still defines its own small local :root (a
 * renamed restatement of the light theme, not a copy of the dark one) — a
 * file is valid if every var(--x) it uses resolves against that local block,
 * plus either shared family IF the file actually <link>s design-system.css.
 * A fallback value in var(--x, #hex) does not count as "defined" — the goal
 * is a real token resolving, not a lucky-looking accident.
 *
 * Run manually:   node lib/check-design-tokens.js
 *                 node lib/check-design-tokens.js --json
 *
 * Pure functions exported for testing:
 *   extractRootTokens(cssText) → string[]
 *   extractUsedTokens(cssText) → string[]
 *   linksStylesheet(html, filename) → boolean
 *   checkFile(content, globalTokens) → { used, defined, undefined }
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : non-blocking-in-spirit, blocking-in-practice check — every var(--token) reference in generated pages and stylesheets must resolve against a real token
 *   reads     : styles/design-system.css (the global token source)
 *               lib/build-*.js (every generator, scanned for inline <style> content)
 *               pages∕*.html (every static page, scanned for inline <style> content)
 *               styles∕*.css (every companion stylesheet, credited with global tokens unconditionally — see alwaysGlobal)
 *   writes    : stdout only — read-only, changes nothing
 *   loaded-by : manual run: node lib/check-design-tokens.js
 *               tests/13-check-design-tokens.test.js (also runs it as a CI-gating integration test)
 *   tested-by : tests/13-check-design-tokens.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     a new local dark-theme file added:
 *       - no action needed — checkFile() reads each file's own :root block dynamically, nothing to register
 *     styles/design-system.css tokens added/removed:
 *       - every file linking it may now have new violations or newly-valid usages — re-run this script
 *       - docs/architecture/12-design-system.md (token list documented there)
 *     a new companion stylesheet added under styles/:
 *       - confirm it's actually always paired with design-system.css by its loader before relying on alwaysGlobal crediting it — if not, it needs its own local :root instead
 * LATTICE:END
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT       = path.join(__dirname, '..');
const DESIGN_CSS = path.join(ROOT, 'styles', 'design-system.css');

// ── Pure computation ──────────────────────────────────────────────────────────

// extractRootTokens — every --token *declared* (not referenced) anywhere in
// this CSS text, under any selector — :root, [data-theme="..."], or a plain
// class like .vxg-doc (a real pattern in this repo's hand-authored pages,
// scoping custom properties to a wrapper element instead of :root). A
// declaration is "--name: value;"; a var(--name) reference never has a colon
// immediately after the name, so this can't confuse the two. Broadening past
// :root/[data-theme] only affects tokens that ARE declared somewhere in the
// file — it can't mask the Session 015 bug shape (a token with no
// declaration anywhere), which still correctly reports undefined.
function extractRootTokens(cssText) {
  const tokens = new Set();
  const decls = cssText.match(/--[a-zA-Z0-9_-]+(?=\s*:)/g) || [];
  for (const d of decls) tokens.add(d.replace(/^--/, ''));
  return [...tokens];
}

// extractUsedTokens — every --token referenced via var(--token...).
function extractUsedTokens(cssText) {
  const tokens = new Set();
  const uses = cssText.match(/var\(--[a-zA-Z0-9_-]+/g) || [];
  for (const u of uses) tokens.add(u.replace('var(--', ''));
  return [...tokens];
}

// linksStylesheet — true if html contains a <link rel="stylesheet" href="...filename">.
function linksStylesheet(html, filename) {
  const re = new RegExp(`<link[^>]+href=["'][^"']*${filename.replace('.', '\\.')}["']`, 'i');
  return re.test(html);
}

// checkFile — resolves used tokens against local :root tokens, plus global
// tokens if the file links design-system.css. Returns the sets involved so
// callers can report specifics.
// alwaysGlobal — true for plain .css files, which can't contain a <link> tag
// to detect pairing; their pairing with design-system.css is guaranteed by
// the loader that includes them (see lib/vextreme.js), not by anything
// checkable inside the .css file itself.
function checkFile(content, globalTokens, alwaysGlobal) {
  const localTokens = extractRootTokens(content);
  const linksGlobal  = alwaysGlobal || linksStylesheet(content, 'design-system.css');
  const defined = new Set(localTokens);
  if (linksGlobal) {
    for (const t of globalTokens) defined.add(t);
  }

  const used = extractUsedTokens(content);
  const undefinedTokens = used.filter(t => !defined.has(t));

  return { used, defined: [...defined], undefined: undefinedTokens, linksGlobal };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const jsonOut = process.argv.includes('--json');

  const globalTokens = extractRootTokens(fs.readFileSync(DESIGN_CSS, 'utf8'));

  const candidates = [];
  for (const dir of ['lib', 'pages', 'styles']) {
    const dirPath = path.join(ROOT, dir);
    for (const entry of fs.readdirSync(dirPath)) {
      if (dir === 'lib' && !(entry.startsWith('build-') && entry.endsWith('.js'))) continue;
      if (dir === 'pages' && !entry.endsWith('.html')) continue;
      if (dir === 'styles' && (!entry.endsWith('.css') || entry === 'design-system.css')) continue;
      candidates.push(path.join(dir, entry));
    }
  }

  const violations = [];
  for (const relPath of candidates) {
    const content = fs.readFileSync(path.join(ROOT, relPath), 'utf8');
    if (!content.includes('var(--')) continue;
    // Plain .css files can't contain a <link> tag; their pairing with
    // design-system.css is guaranteed by the loader that includes them.
    const alwaysGlobal = relPath.startsWith('styles' + path.sep) || relPath.startsWith('styles/');
    const result = checkFile(content, globalTokens, alwaysGlobal);
    if (result.undefined.length) {
      violations.push({ file: relPath, undefinedTokens: result.undefined });
    }
  }

  if (jsonOut) {
    console.log(JSON.stringify({ globalTokens, filesChecked: candidates.length, violations }, null, 2));
  } else {
    console.log(`[check-design-tokens] Checked ${candidates.length} files against ${globalTokens.length} global tokens.`);
    if (!violations.length) {
      console.log('  All var(--token) references resolve. No violations.');
    } else {
      console.log(`  ${violations.length} file(s) with undefined token references:`);
      for (const v of violations) {
        console.log(`    ${v.file}: ${v.undefinedTokens.map(t => '--' + t).join(', ')}`);
      }
    }
  }

  if (violations.length) process.exit(1);
}

module.exports = { extractRootTokens, extractUsedTokens, linksStylesheet, checkFile };

