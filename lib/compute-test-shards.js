'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const TESTS_DIR = path.join(ROOT, 'tests');

// How many test files a single shard should aim to cover. Tuned high on
// purpose: CI's fixed per-job overhead (checkout + setup-node, ~15-30s) is
// far larger than this repo's actual test runtime today (~28 files, ~1.6s),
// so the target stays above the current file count until the suite has
// genuinely grown enough for parallelism to pay for its own overhead.
const TARGET_FILES_PER_SHARD = 50;
const MAX_SHARDS = 6;

function listTestFiles() {
  return fs.readdirSync(TESTS_DIR)
    .filter((f) => f.endsWith('.test.js'))
    .sort();
}

function computeShardCount(fileCount, targetPerShard = TARGET_FILES_PER_SHARD, maxShards = MAX_SHARDS) {
  if (fileCount <= 0) return 1;
  const raw = Math.ceil(fileCount / targetPerShard);
  return Math.min(Math.max(raw, 1), maxShards);
}

// Deterministic round-robin so re-running with the same file list and shard
// count always produces the same partition, and every file lands in
// exactly one shard. shardIndex is 1-indexed to match GitHub Actions'
// matrix values directly.
function assignFilesToShard(files, shardIndex, totalShards) {
  if (shardIndex < 1 || shardIndex > totalShards) {
    throw new Error(`shardIndex ${shardIndex} out of range [1, ${totalShards}]`);
  }
  return files.filter((_, i) => (i % totalShards) === (shardIndex - 1));
}

function main() {
  const args = process.argv.slice(2);
  const command = args[0];
  const files = listTestFiles();

  if (command === 'matrix') {
    const total = computeShardCount(files.length);
    const shards = Array.from({ length: total }, (_, i) => i + 1);
    process.stdout.write(JSON.stringify({ shards, total }) + '\n');
    return;
  }

  if (command === 'files') {
    const shardArg = args.indexOf('--shard');
    const totalArg = args.indexOf('--total');
    if (shardArg === -1 || totalArg === -1) {
      console.error('Usage: node lib/compute-test-shards.js files --shard N --total M');
      process.exit(1);
    }
    const shardIndex = Number(args[shardArg + 1]);
    const totalShards = Number(args[totalArg + 1]);
    const assigned = assignFilesToShard(files, shardIndex, totalShards);
    process.stdout.write(assigned.map((f) => path.join('tests', f)).join('\n') + '\n');
    return;
  }

  console.error('Usage: node lib/compute-test-shards.js <matrix|files> [...]');
  process.exit(1);
}

if (require.main === module) {
  main();
}

module.exports = { computeShardCount, assignFilesToShard, listTestFiles, TARGET_FILES_PER_SHARD, MAX_SHARDS };

