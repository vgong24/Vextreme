'use strict';

const fs = require('fs');
const path = require('path');

const MARKER = '<!-- [VXG RealForever] -->';

function detectLineEnding(source) {
  return source.includes('\r\n') ? '\r\n' : '\n';
}

function trimTrailingBlankLines(text) {
  return String(text).replace(/(?:\r?\n)+$/g, '');
}

function normalizeContinuation(block, newline) {
  const normalized = trimTrailingBlankLines(block).replace(/\r?\n/g, newline);
  if (!normalized.startsWith('### Session continued')) {
    throw new Error('Continuation must start with "### Session continued"');
  }
  if (normalized.includes('\n# ') || normalized.includes('\r\n# ')) {
    throw new Error('Continuation must not start a new session record; use ### Session continued');
  }
  if (normalized.endsWith(MARKER)) return normalized;
  return normalized + newline + newline + MARKER;
}

function appendContinuation(source, block) {
  const newline = detectLineEnding(source);
  const base = trimTrailingBlankLines(source);
  if (!base.endsWith(MARKER)) {
    throw new Error(`Session file must end with ${MARKER}`);
  }
  const continuation = normalizeContinuation(block, newline);
  return base + newline + newline + continuation + newline;
}

if (require.main === module) {
  const [sessionPath, continuationPath] = process.argv.slice(2);
  if (!sessionPath || !continuationPath) {
    console.error('Usage: node lib/append-session-continuation.js <session-file.md> <continuation-file.md>');
    process.exit(1);
  }

  const absoluteSession = path.resolve(sessionPath);
  const absoluteContinuation = path.resolve(continuationPath);
  const source = fs.readFileSync(absoluteSession, 'utf8');
  const block = fs.readFileSync(absoluteContinuation, 'utf8');
  fs.writeFileSync(absoluteSession, appendContinuation(source, block));
  console.log(`[append-session-continuation] Appended ${continuationPath} to ${sessionPath}`);
}

module.exports = { MARKER, detectLineEnding, normalizeContinuation, appendContinuation };

