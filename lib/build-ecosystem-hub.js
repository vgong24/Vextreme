#!/usr/bin/env node
/**
 * VEXTREME — lib/build-ecosystem-hub.js
 *
 * Generates pages/ecosystem-hub.html — a developer-facing dashboard
 * showing the current state of the content ecosystem, system health,
 * and open architectural questions, for both the admin and any AI
 * instance arriving cold.
 *
 * The page is static chrome generated at build time. All live data
 * (index.json, status.json, narrative.json) is fetched at runtime in
 * the browser, matching the pattern established by vextreme-demo.html.
 *
 * Sections:
 *   Content Map        — node/arc/lang counts + lattice coverage (live from index.json + status.json)
 *   Current Architecture — hand-authored synthesis of system shape (live from data/status/narrative.json)
 *   System Health       — five notice categories (live from status.json); Open Discussions
 *                          renders first since those are live questions, not resolved tickets
 *
 * Page Registry has moved to pages/archives.html (unified registry).
 *
 * Session 015 note: earlier CSS used var(--stone-950)/var(--font-mono) etc,
 * tokens that don't exist anywhere in styles/design-system.css (which only
 * defines --stone, --muted, --border, --cream, --ember, --ember-bg, --mono) —
 * text rendered near-invisible against the accidental dark fallback color.
 * This version uses only variables that design-system.css actually defines,
 * same as every other generated page (see lib/build-demo.js for the pattern).
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : generates pages/ecosystem-hub.html — the admin+AI-facing system state dashboard
 *   reads     : data/index.json (live fetch, browser)
 *               data/status.json (live fetch, browser)
 *               data/status/narrative.json (live fetch, browser)
 *   writes    : pages/ecosystem-hub.html
 *   loaded-by : manual run: node lib/build-ecosystem-hub.js
 *               tests/12-ecosystem-hub.test.js
 *   tested-by : tests/12-ecosystem-hub.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     CSS custom property used in the inline <style> block:
 *       - styles/design-system.css — the property must actually be defined there (--stone, --muted, --border, --cream, --ember, --ember-bg, --mono, --serif, --sans); anything else silently renders with no fallback
 *     notice category added/removed in status.json:
 *       - CATEGORY_ORDER / CATEGORY_ICONS arrays in this file's generated JS
 *       - lib/build-status.js (the category must exist in the rollup first)
 *     data/status/narrative.json schema changed:
 *       - renderNarrative() in this file's generated JS
 *       - tests/10 (narrative.json required-fields test)
 * LATTICE:END
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT         = path.join(__dirname, '..');
const OUT_HTML     = path.join(ROOT, 'pages', 'ecosystem-hub.html');
const CDN_BASE     = 'https://cdn.jsdelivr.net/gh/vgong24/vextreme@main';
const GH_ROOT      = 'https://vgong24.github.io/Vextreme';
const ARCHIVES_URL = GH_ROOT + '/pages/archives.html';

function generateEcosystemHub() {
  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Ecosystem Hub — Vextreme</title>
  <link rel="stylesheet" href="${CDN_BASE}/styles/design-system.css">
  <link rel="stylesheet" href="${CDN_BASE}/styles/arc-nav.css">
  <style>
    body { max-width: 900px; margin: 0 auto; padding: 2rem 1.5rem 4rem; }

    /* Nav header */
    .page-nav { display: flex; gap: 20px; margin-bottom: 2rem; font-family: var(--mono); font-size: 11px; }
    .page-nav a { color: var(--muted); text-decoration: none; letter-spacing: 0.04em; }
    .page-nav a:hover { color: var(--ember); }
    .page-nav a.current { color: var(--stone); }

    .hub-header { margin-bottom: 3rem; }
    .hub-header h1 { font-size: 2rem; font-weight: 700; margin: 0 0 0.5rem; }
    .hub-header p { color: var(--muted); margin: 0; }
    .hub-header a { color: var(--ember); text-decoration: none; }
    .hub-header a:hover { text-decoration: underline; }

    /* Stat tiles */
    .stat-row { display: flex; gap: 1rem; margin: 1.5rem 0; flex-wrap: wrap; }
    .stat-tile {
      flex: 1; min-width: 120px;
      border: 1px solid var(--border);
      border-radius: 6px; padding: 1rem;
      text-align: center;
      background: var(--cream);
    }
    .stat-tile .val { font-size: 1.75rem; font-weight: 700; color: var(--ember); font-family: var(--mono); }
    .stat-tile .lbl { font-size: 0.75rem; color: var(--muted); margin-top: 0.25rem; }

    /* Section headers */
    .section { margin: 3rem 0 1.5rem; display: flex; align-items: baseline; justify-content: space-between; gap: 1rem; }
    .section h2 { font-size: 1rem; font-weight: 600; text-transform: uppercase; letter-spacing: 0.08em; color: var(--stone); border-bottom: 1px solid var(--border); padding-bottom: 0.5rem; margin: 0; flex: 1; }
    .section-link { font-size: 0.75rem; color: var(--ember); text-decoration: none; white-space: nowrap; font-family: var(--mono); }
    .section-link:hover { text-decoration: underline; }

    /* Current Architecture (narrative) */
    .narrative-body p { margin: 0 0 1rem; color: var(--stone); line-height: 1.7; }
    .narrative-body p:last-child { margin-bottom: 0; }
    .pipeline-list { list-style: none; margin: 1rem 0 0; padding: 0; counter-reset: pipeline; }
    .pipeline-list li {
      counter-increment: pipeline;
      font-family: var(--mono); font-size: 0.78rem; color: var(--stone);
      padding: 0.6rem 0.75rem 0.6rem 2.25rem; position: relative;
      border: 1px solid var(--border); border-radius: 6px; margin-bottom: 0.5rem;
      background: var(--cream);
    }
    .pipeline-list li::before {
      content: counter(pipeline);
      position: absolute; left: 0.75rem; top: 50%; transform: translateY(-50%);
      width: 1.2rem; height: 1.2rem; border-radius: 50%;
      background: var(--ember); color: #fff;
      font-family: var(--mono); font-size: 0.65rem;
      display: flex; align-items: center; justify-content: center;
    }
    .narrative-focus {
      margin-top: 1rem; padding: 0.85rem 1rem;
      border-left: 3px solid var(--ember); background: var(--ember-bg);
      font-size: 0.85rem; color: var(--stone); line-height: 1.6;
    }
    .narrative-byline {
      margin-top: 1rem; font-size: 0.72rem; color: var(--muted); font-family: var(--mono);
    }

    /* Health panels */
    .health-grid { display: flex; flex-direction: column; gap: 0.75rem; }
    details.health-panel {
      border: 1px solid var(--border);
      border-radius: 6px; overflow: hidden;
      background: var(--cream);
    }
    details.health-panel[open] { border-color: var(--ember); }
    summary.panel-head {
      display: flex; align-items: center; justify-content: space-between;
      padding: 0.85rem 1rem; cursor: pointer; list-style: none; user-select: none;
    }
    summary.panel-head::-webkit-details-marker { display: none; }
    .panel-title { font-weight: 600; font-size: 0.9rem; display: flex; align-items: center; gap: 0.6rem; color: var(--stone); }
    .panel-badge {
      font-size: 0.7rem; font-family: var(--mono);
      background: var(--border); border-radius: 999px;
      padding: 0.1rem 0.5rem; color: var(--stone);
    }
    .panel-badge.zero { background: transparent; color: var(--muted); }
    .panel-caret { color: var(--muted); font-size: 0.75rem; }
    details[open] .panel-caret::after { content: '▲'; }
    .panel-caret::after { content: '▼'; }

    .panel-body { padding: 0.75rem 1rem 1rem; border-top: 1px solid var(--border); }

    .notice-item { padding: 0.6rem 0; border-bottom: 1px solid var(--border); }
    .notice-item:last-child { border-bottom: none; }
    .notice-item .ni-title { font-weight: 600; font-size: 0.85rem; margin-bottom: 0.25rem; color: var(--stone); }
    .notice-item .ni-desc { font-size: 0.8rem; color: var(--muted); line-height: 1.5; }
    .notice-item .ni-considerations { margin: 0.5rem 0 0; padding-left: 1.1rem; font-size: 0.78rem; color: var(--muted); line-height: 1.6; }
    .notice-item .ni-considerations li { margin-bottom: 0.3rem; }
    .notice-item .ni-meta { font-size: 0.72rem; color: var(--muted); margin-top: 0.35rem; font-family: var(--mono); }
    .notice-item .ni-tag {
      display: inline-block; font-size: 0.65rem; border-radius: 3px;
      padding: 0.1rem 0.4rem; margin-right: 0.3rem; font-family: var(--mono);
    }
    .tag-high    { background: var(--ember-bg); color: var(--ember); }
    .tag-medium  { background: rgba(74,120,158,0.12); color: #3a6a8f; }
    .tag-low     { background: var(--border); color: var(--muted); }
    .tag-fixture { background: var(--border); color: var(--muted); }

    .empty-state { color: var(--muted); font-size: 0.85rem; padding: 0.5rem 0; }

    .loading { color: var(--muted); font-size: 0.85rem; }
    .error-msg { color: #b03030; font-size: 0.85rem; }
  </style>
</head>
<body>

  <nav class="page-nav">
    <a href="${GH_ROOT}/index.html">Index</a>
    <a href="${ARCHIVES_URL}">Archives</a>
    <a class="current" href="#">Ecosystem Hub</a>
  </nav>

  <div class="hub-header">
    <h1>Ecosystem Hub</h1>
    <p>System state for the admin and for any AI instance arriving cold. Full page registry at <a href="${ARCHIVES_URL}">Archives</a>.</p>
  </div>

  <!-- Content Map -->
  <div class="section">
    <h2>Content Map</h2>
    <a class="section-link" href="${ARCHIVES_URL}">See all pages →</a>
  </div>
  <div class="stat-row">
    <div class="stat-tile"><div class="val" id="stat-nodes">—</div><div class="lbl">Content Nodes</div></div>
    <div class="stat-tile"><div class="val" id="stat-arcs">—</div><div class="lbl">Arcs</div></div>
    <div class="stat-tile"><div class="val" id="stat-langs">—</div><div class="lbl">Languages</div></div>
    <div class="stat-tile"><div class="val" id="stat-lattice">—</div><div class="lbl">Lattice Coverage</div></div>
    <div class="stat-tile"><div class="val" id="stat-open">—</div><div class="lbl">Open Items</div></div>
  </div>

  <!-- Current Architecture -->
  <div class="section"><h2>Current Architecture</h2></div>
  <div id="narrative-body" class="narrative-body">
    <span class="loading">Loading…</span>
  </div>

  <!-- System Health -->
  <div class="section"><h2>System Health</h2></div>
  <div class="health-grid" id="health-grid">
    <span class="loading">Loading…</span>
  </div>

  <script>
  (function () {
    'use strict';

    var BASE          = '/Vextreme';
    var INDEX_URL     = BASE + '/data/index.json';
    var STATUS_URL    = BASE + '/data/status.json';
    var NARRATIVE_URL = BASE + '/data/status/narrative.json';

    function el(id) { return document.getElementById(id); }
    function esc(s) { return String(s == null ? '' : s).replace(/[&<>]/g, function (c) { return { '&': '&amp;', '<': '&lt;', '>': '&gt;' }[c]; }); }

    function tagClass(priority) {
      return 'ni-tag tag-' + (priority || 'low');
    }

    // Category render order — openDiscussions first: these are live questions
    // to engage with, not resolved tickets to track, so they lead the page.
    var CATEGORY_ORDER = ['openDiscussions', 'translation', 'techDebt', 'enhancements', 'assumptions'];
    var CATEGORY_ICONS = { openDiscussions: '💬', translation: '🌐', techDebt: '🔧', enhancements: '💡', assumptions: '❓' };

    function renderHealthPanel(key, data) {
      var count  = data.count || 0;
      var items  = data.items || [];
      var badge  = '<span class="panel-badge' + (count === 0 ? ' zero' : '') + '">' + count + '</span>';
      var icon   = CATEGORY_ICONS[key] || '•';

      var bodyHtml;
      if (!items.length) {
        bodyHtml = '<div class="empty-state">No open items.</div>';
      } else {
        bodyHtml = items.map(function (item) {
          var priority = item.priority || 'low';
          var meta = [];
          if (item.addedSession || item.raisedSession) meta.push('session ' + (item.addedSession || item.raisedSession));
          if (item.addedPR)      meta.push('PR #' + item.addedPR);
          if (item.context)      meta.push(item.context);
          if (item.blockedBy)    meta.push('blocked: ' + item.blockedBy);
          if (item.relatesTo)    meta.push('relates to ' + item.relatesTo);
          if (item.lang)         meta.push(item.lang.toUpperCase());
          if (item.key)          meta.push(item.key);

          var titleText = item.title || item.claim || item.key || '—';
          var descText  = item.description || item.en || '';

          var considerationsHtml = '';
          if (Array.isArray(item.considerations) && item.considerations.length) {
            considerationsHtml = '<ul class="ni-considerations">' +
              item.considerations.map(function (c) { return '<li>' + esc(c) + '</li>'; }).join('') +
              '</ul>';
          }

          return '<div class="notice-item">' +
            '<div class="ni-title">' +
              '<span class="' + tagClass(priority) + '">' + esc(priority) + '</span>' +
              esc(titleText) +
            '</div>' +
            (descText ? '<div class="ni-desc">' + esc(descText) + '</div>' : '') +
            considerationsHtml +
            (meta.length ? '<div class="ni-meta">' + meta.map(esc).join(' · ') + '</div>' : '') +
          '</div>';
        }).join('');
      }

      if (key === 'translation' && data.fixtures && data.fixtures.length) {
        bodyHtml += '<details style="margin-top:0.75rem"><summary style="cursor:pointer;color:var(--muted);font-size:0.8rem">Intentional fixtures (' + data.fixtures.length + ')</summary>' +
          data.fixtures.map(function (f) {
            return '<div class="notice-item"><div class="ni-title"><span class="ni-tag tag-fixture">fixture</span>' + esc(f.key) + '</div>' +
              (f.en ? '<div class="ni-desc">' + esc(f.en) + '</div>' : '') + '</div>';
          }).join('') +
        '</details>';
      }

      return '<details class="health-panel">' +
        '<summary class="panel-head">' +
          '<span class="panel-title">' + icon + ' ' + esc(data.label) + ' ' + badge + '</span>' +
          '<span class="panel-caret"></span>' +
        '</summary>' +
        '<div class="panel-body">' + bodyHtml + '</div>' +
      '</details>';
    }

    function renderHealth(status) {
      var n    = status.notices;
      var html = CATEGORY_ORDER.map(function (k) { return renderHealthPanel(k, n[k]); }).join('');
      el('health-grid').innerHTML = html;
      el('stat-open').textContent = status._meta.totalOpen;

      var lattice = status._meta.lattice;
      el('stat-lattice').textContent = lattice ? (lattice.mappedNodes + '/' + lattice.totalFiles) : '—';
    }

    function renderNarrative(narrative) {
      var pipelineHtml = (narrative.pipeline || []).map(function (step) {
        return '<li>' + esc(step) + '</li>';
      }).join('');

      var html = '<div class="narrative-body">' +
        '<p>' + esc(narrative.summary) + '</p>' +
        (pipelineHtml ? '<ol class="pipeline-list">' + pipelineHtml + '</ol>' : '') +
        (narrative.currentFocus ? '<div class="narrative-focus"><strong>Current focus — </strong>' + esc(narrative.currentFocus) + '</div>' : '') +
        '<div class="narrative-byline">Last synthesized by ' + esc(narrative.lastSynthesizedBy) +
          ', Session ' + esc(narrative.lastSynthesizedSession) +
          (narrative.lastSynthesizedAt ? ' (' + esc(narrative.lastSynthesizedAt) + ')' : '') +
          '. See docs/continuity for the full session log.</div>' +
      '</div>';

      el('narrative-body').innerHTML = html;
    }

    fetch(INDEX_URL)
      .then(function (r) { return r.json(); })
      .then(function (data) {
        el('stat-nodes').textContent = Object.keys(data.slugMap || {}).length;
        el('stat-arcs').textContent  = Object.keys(data.arcMap  || {}).length;
        el('stat-langs').textContent = (data.supportedLangs || []).join(', ').toUpperCase();
      })
      .catch(function (e) {
        el('stat-nodes').textContent = 'err';
        console.error('index.json failed:', e);
      });

    fetch(STATUS_URL)
      .then(function (r) { return r.json(); })
      .then(renderHealth)
      .catch(function (e) {
        el('health-grid').innerHTML = '<span class="error-msg">Failed to load status.json: ' + e.message + '</span>';
      });

    fetch(NARRATIVE_URL)
      .then(function (r) { return r.json(); })
      .then(renderNarrative)
      .catch(function (e) {
        el('narrative-body').innerHTML = '<span class="error-msg">Failed to load narrative.json: ' + e.message + '</span>';
      });

  }());
  </script>

  <script src="${CDN_BASE}/widgets/sw-register.js"></script>
</body>
</html>
<!-- [VXG RealForever] -->
`;
}

if (require.main === module) {
  const html = generateEcosystemHub();
  fs.writeFileSync(OUT_HTML, html);
  console.log('[build-ecosystem-hub] Done.');
  console.log('  output:', OUT_HTML);
}

module.exports = { generateEcosystemHub };

