#!/usr/bin/env node
/**
 * VEXTREME — lib/check-lattice-edges.js
 *
 * pe-012: verify docs/lattice-map.json's claimed edges against actual code.
 * The lattice's reads/writes/loadedBy edges are hand-written and were never
 * checked against the source they describe — Session 022 found two stale
 * claims by hand, and docs/architecture/13-intent-driven-operations.md named
 * this checker as the required step before any higher-blast-radius automation
 * (od-008) can trust the map. Three verifications:
 *
 *   1. Existence — a concrete path claimed in reads/writes exists on disk
 *   2. Mention — the node's own source (LATTICE header stripped, so the
 *      claim can't vouch for itself) mentions the claimed target's basename
 *   3. loadedBy — a claimed repo-file loader actually mentions this node;
 *      and, in reverse, every lib/tests/widgets file that require()s a
 *      mapped node appears in that node's loadedBy list
 *
 * Edges are prose, not schema: parenthetical annotations are stripped,
 * template paths ({slug}, *, 00N placeholders) and non-path claims
 * ("git log", "stdout only") are counted as skipped rather than guessed at.
 * A basename mention is deliberately weak evidence — this checker catches
 * renamed/removed targets and forgotten loaders, not semantic lies.
 *
 * Informational severity (exit 0), same posture as check-key-alignment:
 * findings are drift candidates for a human/instance to judge, because a
 * mention heuristic has false negatives that must not block CI. Use --strict
 * to exit 1 on findings (not wired into CI).
 *
 * Run manually:   node lib/check-lattice-edges.js
 *                 node lib/check-lattice-edges.js --json | --strict
 *
 * Pure functions exported for testing:
 *   pathToken(edge) → string|null       (strip annotation, reject non-paths)
 *   isTemplate(token) → boolean         ({...}, *, 00N placeholders)
 *   stripLatticeBlock(source) → string
 *   mentionName(token) → string         (basename or last dir segment)
 *   requiresOf(source, fromDir) → [repoPath]
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : pe-012 lattice-edge drift detector — verifies the map's claimed reads/writes/loadedBy edges against actual code
 *   reads     : docs/lattice-map.json (every node's claimed edges)
 *               lib/, tests/, widgets/, components/ *.js sources (mention + require() scans)
 *               any file a node names in reads/writes/loadedBy (existence + mention checks)
 *   writes    : stdout only — read-only; exit 1 only with --strict (not wired into CI)
 *   loaded-by : manual run: node lib/check-lattice-edges.js
 *               tests/23-check-lattice-edges.test.js
 *   tested-by : tests/23-check-lattice-edges.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     docs/lattice-map.json edge prose conventions changed (annotations, template placeholders, non-path claims):
 *       - pathToken()/isTemplate()/mentionName() here — they encode exactly those conventions; a new prose shape silently becomes a skipped edge, not an error
 *     a loadedBy edge describes an indirect consumer (reads the node's output, not the node):
 *       - the edge's annotation must name the mediating artifact path (e.g. 'pages/ecosystem-hub.html (live fetch of its output data/status.json)') — that is what the annotation fallback checks
 *     a new lib/tests/widgets file mentions a require() in a comment or string fixture:
 *       - comments are stripped by requiresOf(); string fixtures are not — use unmapped fixture paths in tests, per tests/23's own header note
 * LATTICE:END
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const MAP  = path.join(ROOT, 'docs', 'lattice-map.json');

// ── Pure computation ─────────────────────────────────────────────────────────

// pathToken — reduces an edge string to its path-looking head, or null when
// the edge isn't a file claim ("git log / git status (via subprocess)",
// "stdout only — read-only, changes nothing").
function pathToken(edge) {
  const head = edge.split(' (')[0].trim();
  if (!/^[\w{}*.@/-]+$/.test(head)) return null;
  // A path claim either lives in a directory or ends in a real file
  // extension — "window.VEX_STRINGS_EN" is a global, not a path.
  if (!head.includes('/') && !/\.(js|json|md|html|css|yml|png|svg)$/.test(head)) return null;
  if (head.startsWith('window.')) return null;
  return head;
}

// isTemplate — tokens with glob/placeholder segments can't be checked for
// existence ({slug}, **, batch-00N).
function isTemplate(token) {
  return /[{*]/.test(token) || /0N/.test(token);
}

// stripLatticeBlock — removes the generated LATTICE header so a node's own
// echoed claims can't satisfy the mention check.
function stripLatticeBlock(source) {
  // The marker regex is assembled from pieces so this file never contains
  // the literal sentinel text: the header generator searches for it, and a
  // literal mention in code gets treated as a real block boundary — this
  // exact corruption happened to this exact function during development.
  // See config/lessons/sentinel-text-is-hazardous-to-itself.json.
  const re = new RegExp('LATTICE' + ':BEGIN[\\s\\S]*?LATTICE' + ':END', 'g');
  return source.replace(re, '');
}

// mentionName — the fragment whose presence in source counts as a mention:
// basename for files, last segment for directories. For .js targets the
// extension is dropped, because require('./vex-config') is the idiomatic
// mention and never spells out ".js".
function mentionName(token) {
  const clean = token.replace(/\/+$/, '');
  const base = clean.split('/').pop();
  return base.endsWith('.js') ? base.slice(0, -3) : base;
}

// requiresOf — repo-relative paths require()d by a JS source file.
// Comments are stripped first: a require spelled out in prose (this very
// file's own comments once did it) is a mention, not a load — the
// sentinel-text-is-hazardous-to-itself lesson applies to this tool too.
function requiresOf(source, fromDir) {
  const out = [];
  const code = source.replace(/\/\*[\s\S]*?\*\//g, '').replace(/^\s*\/\/.*$/gm, '');
  const re = /require\(\s*['"](\.{1,2}\/[^'"]+)['"]\s*\)/g;
  let m;
  while ((m = re.exec(code))) {
    let p = path.posix.normalize(path.posix.join(fromDir, m[1]));
    if (!/\.\w+$/.test(p)) p += '.js';
    out.push(p);
  }
  return out;
}

// ── Verification (I/O) ───────────────────────────────────────────────────────

function run() {
  const map = JSON.parse(fs.readFileSync(MAP, 'utf8'));
  const nodes = map.nodes;
  const findings = [];
  let checked = 0, skipped = 0;

  const sources = {};
  const sourceOf = (repoPath) => {
    if (!(repoPath in sources)) {
      const abs = path.join(ROOT, repoPath);
      sources[repoPath] = fs.existsSync(abs) && fs.statSync(abs).isFile()
        ? fs.readFileSync(abs, 'utf8') : null;
    }
    return sources[repoPath];
  };

  for (const [nodePath, node] of Object.entries(nodes)) {
    const nodeSource = sourceOf(nodePath);
    const strippedSource = nodeSource !== null ? stripLatticeBlock(nodeSource) : null;
    const canMentionCheck = strippedSource !== null && /\.(js|css|html|md|yml)$/.test(nodePath);

    for (const kind of ['reads', 'writes']) {
      for (const edge of node[kind] || []) {
        const token = pathToken(edge);
        if (!token) { skipped++; continue; }
        if (isTemplate(token)) { skipped++; continue; }
        checked++;
        const abs = path.join(ROOT, token);
        if (kind === 'reads' && !fs.existsSync(abs)) {
          findings.push({ node: nodePath, kind, message: `claims to read ${token}, which does not exist` });
          continue;
        }
        if (canMentionCheck && !strippedSource.includes(mentionName(token))) {
          findings.push({ node: nodePath, kind, message: `claims to ${kind === 'reads' ? 'read' : 'write'} ${token}, but its source never mentions "${mentionName(token)}"` });
        }
      }
    }

    for (const edge of node.loadedBy || []) {
      const token = pathToken(edge);
      if (!token || isTemplate(token)) { skipped++; continue; }
      checked++;
      const loaderSource = sourceOf(token);
      if (loaderSource === null) {
        findings.push({ node: nodePath, kind: 'loadedBy', message: `claims to be loaded by ${token}, which does not exist` });
        continue;
      }
      // An indirect consumer edge ("pages/ecosystem-hub.html (fetches its
      // output data/status.json)") is satisfied when the loader mentions any
      // path named in the edge's annotation, not the node itself — the edge
      // must spell out the mediating artifact to pass.
      const stripped = stripLatticeBlock(loaderSource);
      const annotationPaths = (edge.match(/[\w./-]+\/[\w.{}*/-]+/g) || []).filter(t => t !== token);
      const mentioned = stripped.includes(mentionName(nodePath))
        || annotationPaths.some(t => stripped.includes(mentionName(t)));
      if (!mentioned) {
        findings.push({ node: nodePath, kind: 'loadedBy', message: `claims to be loaded by ${token}, but ${token} never mentions "${mentionName(nodePath)}" (or any path in the edge's annotation)` });
      }
    }
  }

  // Reverse: require() edges that the map doesn't acknowledge.
  const scanDirs = ['lib', 'tests', 'widgets', 'components'];
  for (const dir of scanDirs) {
    const absDir = path.join(ROOT, dir);
    if (!fs.existsSync(absDir)) continue;
    for (const f of fs.readdirSync(absDir).filter(f => f.endsWith('.js'))) {
      const loader = path.posix.join(dir, f);
      for (const target of requiresOf(sourceOf(loader) || '', dir)) {
        if (!(target in nodes) || target === loader) continue;
        checked++;
        const declared = (nodes[target].loadedBy || []).some(e => e.includes(loader));
        if (!declared) {
          findings.push({ node: target, kind: 'loadedBy', message: `is require()d by ${loader}, which is missing from its loadedBy list` });
        }
      }
    }
  }

  return { findings, checked, skipped };
}

if (require.main === module) {
  const { findings, checked, skipped } = run();
  if (process.argv.includes('--json')) {
    console.log(JSON.stringify({ clean: findings.length === 0, checked, skipped, findings }, null, 2));
  } else {
    console.log(`[check-lattice-edges] ${checked} edge(s) checked, ${skipped} skipped (non-path or template).`);
    if (findings.length === 0) {
      console.log('  All checkable lattice edges hold.');
    } else {
      console.log(`  ${findings.length} stale edge candidate(s):`);
      for (const f of findings) console.log(`  [${f.kind}] ${f.node} — ${f.message}`);
      console.log('  Informational: judge each candidate; fix docs/lattice-map.json and regenerate headers.');
    }
  }
  if (process.argv.includes('--strict') && findings.length > 0) process.exit(1);
}

module.exports = { pathToken, isTemplate, stripLatticeBlock, mentionName, requiresOf, run };

