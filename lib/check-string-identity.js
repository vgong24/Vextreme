'use strict';

const fs = require('fs');
const path = require('path');
const { collectIdentityRecords, buildIndexes } = require('./build-string-identity-index');

const ROOT = path.join(__dirname, '..');
const SOURCE_DIR = path.join(ROOT, 'data', 'strings', 'source', 'pages');
const PAGES_DIR = path.join(ROOT, 'pages');
const INDEX_PATH = path.join(ROOT, 'data', 'strings', 'compiled', 'identity-index.json');

// ── Pure computation ─────────────────────────────────────────────────────────

// checkIndexKeysResolve — every key the index claims must be a real source key.
function checkIndexKeysResolve(index, sourceKeysBySlug) {
  const allSourceKeys = new Set(Object.values(sourceKeysBySlug).flat());
  const issues = [];
  for (const key of Object.keys(index.byStringId || {})) {
    if (!allSourceKeys.has(key)) {
      issues.push({ check: 'index-key-resolution', message: `identity-index.json references "${key}", which is not a real key in any source file` });
    }
  }
  return issues;
}

// checkCanonicalConflicts — a canonicalElementId claimed by more than one
// contentNodeKey is a real conflict; claimed by many keys within the SAME
// contentNodeKey is the expected shape (a record's several fields).
function checkCanonicalConflicts(records) {
  const nodesByCanonical = new Map();
  for (const r of records) {
    if (!r.canonicalElementId) continue;
    if (!nodesByCanonical.has(r.canonicalElementId)) nodesByCanonical.set(r.canonicalElementId, new Set());
    nodesByCanonical.get(r.canonicalElementId).add(r.contentNodeKey);
  }
  const issues = [];
  for (const [canonicalId, nodes] of nodesByCanonical) {
    if (nodes.size > 1) {
      issues.push({ check: 'canonical-conflict', message: `canonicalElementId "${canonicalId}" is claimed by ${nodes.size} different content nodes: ${[...nodes].join(', ')}` });
    }
  }
  return issues;
}

// checkCoverageGaps — a source file with some but not all keys carrying an
// identity block. Informational: expected during a rollout, not a defect.
function checkCoverageGaps(sourceFiles) {
  const issues = [];
  for (const { file, data } of sourceFiles) {
    const keys = Object.keys(data).filter(k => k !== '_meta');
    const withIdentity = keys.filter(k => data[k].identity);
    if (withIdentity.length > 0 && withIdentity.length < keys.length) {
      issues.push({ check: 'coverage-gap', message: `${file}: ${withIdentity.length}/${keys.length} keys have identity metadata — partial coverage` });
    }
  }
  return issues;
}

// checkElementIdsStillReal — a recorded currentElementId should still be a
// real id="..." in the live HTML. Reuses the same (?:^|\s)id="..." matching
// discipline as discover-string-identity.js's findElementId.
function checkElementIdsStillReal(records, htmlBySlug) {
  const issues = [];
  for (const r of records) {
    if (!r.currentElementId) continue;
    const slugMatch = r.key.match(/^pages\.([^.]+)\./);
    if (!slugMatch) continue;
    const html = htmlBySlug[slugMatch[1]];
    if (html === undefined) continue;
    const re = new RegExp(`(?:^|\\s)id\\s*=\\s*"${r.currentElementId.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}"`, 'i');
    if (!re.test(html)) {
      issues.push({ check: 'stale-element-id', message: `${r.key}: recorded currentElementId "${r.currentElementId}" no longer found in pages/${slugMatch[1]}.html` });
    }
  }
  return issues;
}

// ── I/O — only runs when executed directly ────────────────────────────────────

function run() {
  const sourceFiles = fs.readdirSync(SOURCE_DIR)
    .filter(f => f.endsWith('.json'))
    .map(f => ({ file: path.join('data', 'strings', 'source', 'pages', f), data: JSON.parse(fs.readFileSync(path.join(SOURCE_DIR, f), 'utf8')) }));

  const sourceKeysBySlug = {};
  for (const { file, data } of sourceFiles) {
    sourceKeysBySlug[file] = Object.keys(data).filter(k => k !== '_meta');
  }

  const records = collectIdentityRecords(sourceFiles.map(f => ({ file: f.file, data: f.data })));

  const issues = [];

  if (!fs.existsSync(INDEX_PATH)) {
    return { skipped: true, reason: 'data/strings/compiled/identity-index.json does not exist yet — run lib/build-string-identity-index.js first', issues: [] };
  }
  const committedIndex = JSON.parse(fs.readFileSync(INDEX_PATH, 'utf8'));
  issues.push(...checkIndexKeysResolve(committedIndex, sourceKeysBySlug));
  issues.push(...checkCanonicalConflicts(records));
  issues.push(...checkCoverageGaps(sourceFiles));

  const htmlBySlug = {};
  for (const slug of new Set(records.map(r => (r.key.match(/^pages\.([^.]+)\./) || [])[1]).filter(Boolean))) {
    const htmlPath = path.join(PAGES_DIR, `${slug}.html`);
    if (fs.existsSync(htmlPath)) htmlBySlug[slug] = fs.readFileSync(htmlPath, 'utf8');
  }
  issues.push(...checkElementIdsStillReal(records, htmlBySlug));

  // Staleness: regenerate in memory, compare structurally (ignoring
  // _meta.generatedAt, which always differs) to what's committed.
  const fresh = buildIndexes(records);
  const stale = JSON.stringify(fresh) !== JSON.stringify({
    byCanonicalElementId: committedIndex.byCanonicalElementId,
    byLegacyElementId: committedIndex.byLegacyElementId,
    byContentNodeKey: committedIndex.byContentNodeKey,
    byField: committedIndex.byField,
    byStatus: committedIndex.byStatus,
    byStringId: committedIndex.byStringId,
  });
  if (stale) {
    issues.push({ check: 'stale-index', message: 'identity-index.json does not match what data/strings/source/pages/*.json identity blocks would generate — run node lib/build-string-identity-index.js' });
  }

  return { skipped: false, issues };
}

if (require.main === module) {
  const report = run();

  if (process.argv.includes('--json')) {
    console.log(JSON.stringify(report, null, 2));
  } else if (report.skipped) {
    console.log(`[check-string-identity] Skipped — ${report.reason}`);
  } else {
    console.log(`[check-string-identity] ${report.issues.length === 0 ? 'All checks pass.' : report.issues.length + ' issue(s):'}`);
    for (const i of report.issues) console.log(`  [${i.check}] ${i.message}`);
  }

  // Informational only — this is a pilot; report, never fail the build.
  process.exit(0);
}

module.exports = { checkIndexKeysResolve, checkCanonicalConflicts, checkCoverageGaps, checkElementIdsStillReal, run };

