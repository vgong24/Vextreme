#!/usr/bin/env node
/**
 * VEXTREME — lib/discover-string-identity.js
 *
 * Proposal/report tool only — never writes to a source file, never assigns
 * canonical identity on its own authority. Built as the first script of the
 * localization identity pilot (Session 027, PR #93-#94 continuation),
 * activating a canonical/observed identity split that already exists
 * unindexed in this repo: every content record in
 * pages/{slug}.html carries a `data-legacy-id` next to its `data-i18n` keys —
 * a real, pre-existing legacy identity scheme, distinct from the string-key
 * scheme, currently used for nothing.
 *
 * For each distinct content-node segment found in a page's string keys
 * (`pages.{slug}.{contentNodeKey}.{field}`), this cross-references the live
 * HTML for:
 *   - a matching `id="..."` attribute (the current/observed element identity)
 *   - a `data-legacy-id="..."` attribute on the same element (the canonical
 *     identity seed, per Vex's relay: legacy IDs predate and are more stable
 *     than the current naming, so they anchor `canonicalElementId`)
 *   - an informal `title="legacy alias: X"` tooltip (a real but
 *     non-machine-structured alias found on this page's `.fit` cards —
 *     treated as a weaker-confidence legacy source, never silently promoted
 *     to the same confidence as a real `data-legacy-id` attribute)
 *
 * Emits one candidate per content-node segment with a `status` reflecting
 * how clean the match is — `aligned` (real data-legacy-id, current id matches
 * the content-node key exactly), `needs_review` (a legacy source exists but
 * something doesn't line up — an id/key mismatch, or only an informal alias),
 * or `unresolved` (no legacy anchor found at all). Never assigns `aligned`
 * on a guess.
 *
 * This never touches data/strings/source/**. A human (or, for this pilot,
 * a reviewed-and-recorded merge) decides which candidates become real
 * `identity` blocks in the source file.
 *
 * Run manually:   node lib/discover-string-identity.js <slug>
 *                 node lib/discover-string-identity.js <slug> --json
 *
 * Pure functions exported for testing:
 *   contentNodeSegments(sourceKeys, slug) → [{ contentNodeKey, fieldKeys: [string] }]
 *   findElementId(html, contentNodeKey) → string|null
 *   findLegacyId(html, elementId) → string|null
 *   findTitleAlias(html, elementId) → string|null
 *   classifyCandidate({ contentNodeKey, elementId, legacyId, titleAlias }) → candidate object
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : localization identity pilot, first script — proposal/report tool only, cross-references a page's string keys against its live HTML's data-legacy-id/id/title-alias attributes
 *   reads     : data/strings/source/pages/{slug}.json (existing string keys)
 *               pages/{slug}.html (id, data-legacy-id, title="legacy alias:" attributes)
 *   writes    : data/strings/reports/string-identity-candidates-{slug}.json (candidate report only — never touches data/strings/source∕**)
 *   loaded-by : manual run: node lib/discover-string-identity.js <slug>
 *               tests/33-string-identity.test.js
 *   tested-by : tests/33-string-identity.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     a page's HTML restructures its id/data-legacy-id attributes:
 *       - re-run this script for that slug and review the new candidate report before touching source — lib/check-string-identity.js's checkElementIdsStillReal flags recorded ids that no longer resolve, but does not re-discover new ones automatically
 * LATTICE:END
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SOURCE_DIR = path.join(ROOT, 'data', 'strings', 'source', 'pages');
const PAGES_DIR = path.join(ROOT, 'pages');
const REPORTS_DIR = path.join(ROOT, 'data', 'strings', 'reports');

// ── Pure computation ─────────────────────────────────────────────────────────

// contentNodeSegments — groups a page's string keys by their third dot
// segment (pages.{slug}.{contentNodeKey}.{field}), preserving field order.
function contentNodeSegments(sourceKeys, slug) {
  const prefix = `pages.${slug}.`;
  const byNode = new Map();
  for (const key of sourceKeys) {
    if (!key.startsWith(prefix)) continue;
    const rest = key.slice(prefix.length);
    const dot = rest.indexOf('.');
    if (dot === -1) continue;
    const contentNodeKey = rest.slice(0, dot);
    const fieldKey = rest.slice(dot + 1);
    if (!byNode.has(contentNodeKey)) byNode.set(contentNodeKey, []);
    byNode.get(contentNodeKey).push(fieldKey);
  }
  return [...byNode.entries()].map(([contentNodeKey, fieldKeys]) => ({ contentNodeKey, fieldKeys }));
}

// findElementId — searches HTML for an id attribute matching contentNodeKey,
// case-insensitively (the .fit cards use uppercase ids like
// FIT-PLATFORM-DEVEX for a key segment fit-platform-devex). Returns the
// attribute's literal text (preserving its real case) or null.
function findElementId(html, contentNodeKey) {
  // (?:^|\s) rather than \b: a bare \b also matches the hyphen-to-letter
  // transition inside data-legacy-id="...", incorrectly treating that as an
  // "id=" match (found the hard way — note-self-demonstration's real id was
  // shadowed by its own data-legacy-id value until this was tightened).
  const re = /(?:^|\s)id\s*=\s*"([^"]+)"/g;
  let m;
  while ((m = re.exec(html))) {
    if (m[1].toLowerCase() === contentNodeKey.toLowerCase()) return m[1];
  }
  return null;
}

// findLegacyId — given a discovered elementId, looks for a data-legacy-id
// attribute on the same tag. Scans backward from the id's position to the
// start of that opening tag (or forward, since attribute order varies) —
// simplest robust approach: find the whole opening tag containing this id
// and extract data-legacy-id from within it.
function findLegacyId(html, elementId) {
  if (!elementId) return null;
  const idPos = html.indexOf(`id="${elementId}"`);
  if (idPos === -1) return null;
  const tagStart = html.lastIndexOf('<', idPos);
  const tagEnd = html.indexOf('>', idPos);
  if (tagStart === -1 || tagEnd === -1) return null;
  const tag = html.slice(tagStart, tagEnd + 1);
  const m = tag.match(/data-legacy-id\s*=\s*"([^"]+)"/);
  return m ? m[1] : null;
}

// findTitleAlias — looks for the informal `title="legacy alias: X"` pattern
// this page's .fit cards use, within a small window after the element's id
// attribute (the alias sits on a sibling <span>, not the id-bearing element
// itself, so this is intentionally looser than findLegacyId's same-tag scan).
function findTitleAlias(html, elementId) {
  if (!elementId) return null;
  const idPos = html.indexOf(`id="${elementId}"`);
  if (idPos === -1) return null;
  const window = html.slice(idPos, idPos + 400);
  const m = window.match(/title="legacy alias:\s*([^"]+)"/);
  return m ? m[1].trim() : null;
}

// classifyCandidate — decides status without ever guessing "aligned".
//   aligned      — real data-legacy-id AND elementId matches contentNodeKey
//                  (case-insensitive, since HTML ids are sometimes uppercase)
//   needs_review — some legacy source exists (real or informal) but the
//                  match isn't clean: elementId diverges from contentNodeKey,
//                  or the only legacy source is an informal title alias
//   unresolved   — no legacy source found at all
function classifyCandidate({ contentNodeKey, elementId, legacyId, titleAlias }) {
  const idMatches = elementId && elementId.toLowerCase() === contentNodeKey.toLowerCase();

  if (legacyId && idMatches) {
    return {
      contentNodeKey,
      currentElementId: elementId,
      canonicalElementId: legacyId,
      canonicalSource: 'html.data-legacy-id',
      status: 'aligned',
    };
  }
  if (legacyId && !idMatches) {
    return {
      contentNodeKey,
      currentElementId: elementId,
      canonicalElementId: legacyId,
      canonicalSource: 'html.data-legacy-id',
      status: 'needs_review',
      note: `element id "${elementId}" does not match contentNodeKey "${contentNodeKey}"`,
    };
  }
  if (titleAlias) {
    return {
      contentNodeKey,
      currentElementId: elementId,
      canonicalElementId: titleAlias,
      canonicalSource: 'html.title-alias',
      status: 'needs_review',
      note: 'legacy source is an informal title="legacy alias:" tooltip, not a real data-legacy-id attribute',
    };
  }
  return {
    contentNodeKey,
    currentElementId: elementId,
    canonicalElementId: null,
    canonicalSource: 'none',
    status: 'unresolved',
    note: 'no data-legacy-id or title alias found for this content node',
  };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

function run(slug) {
  const sourcePath = path.join(SOURCE_DIR, `${slug}.json`);
  const htmlPath = path.join(PAGES_DIR, `${slug}.html`);

  const source = JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
  const html = fs.readFileSync(htmlPath, 'utf8');
  const sourceKeys = Object.keys(source).filter(k => k !== '_meta');

  const nodes = contentNodeSegments(sourceKeys, slug);
  const candidates = nodes.map(({ contentNodeKey, fieldKeys }) => {
    const elementId = findElementId(html, contentNodeKey);
    const legacyId = findLegacyId(html, elementId);
    const titleAlias = legacyId ? null : findTitleAlias(html, elementId);
    return { ...classifyCandidate({ contentNodeKey, elementId, legacyId, titleAlias }), fieldKeys };
  });

  return {
    slug,
    generatedAt: new Date().toISOString(),
    totalContentNodes: candidates.length,
    byStatus: {
      aligned: candidates.filter(c => c.status === 'aligned').length,
      needs_review: candidates.filter(c => c.status === 'needs_review').length,
      unresolved: candidates.filter(c => c.status === 'unresolved').length,
    },
    candidates,
  };
}

if (require.main === module) {
  const slug = process.argv[2];
  if (!slug) {
    console.error('Usage: node lib/discover-string-identity.js <slug> [--json]');
    process.exit(1);
  }

  const report = run(slug);

  if (!fs.existsSync(REPORTS_DIR)) fs.mkdirSync(REPORTS_DIR, { recursive: true });
  const reportPath = path.join(REPORTS_DIR, `string-identity-candidates-${slug}.json`);
  fs.writeFileSync(reportPath, JSON.stringify(report, null, 2) + '\n');

  if (process.argv.includes('--json')) {
    console.log(JSON.stringify(report, null, 2));
  } else {
    console.log(`[discover-string-identity] ${report.totalContentNodes} content node(s) for "${slug}":`);
    console.log(`  aligned: ${report.byStatus.aligned} | needs_review: ${report.byStatus.needs_review} | unresolved: ${report.byStatus.unresolved}`);
    console.log(`  candidate report written → ${path.relative(ROOT, reportPath)}`);
    console.log('  This is a proposal only — nothing in data/strings/source/ was touched.');
  }
}

module.exports = { contentNodeSegments, findElementId, findLegacyId, findTitleAlias, classifyCandidate, run };

