'use strict';

const fs   = require('fs');
const path = require('path');

const { scanUsages, traceKey, tracePage, findOrphanUsages, findUnusedKeys } = require('./trace-string-usage');
const { findScreenshots } = require('./build-terrain-map');
const { getPageSlugs } = require('./audit-pages');

const ROOT           = path.join(__dirname, '..');
const MANIFEST_PATH  = path.join(ROOT, 'data', 'strings', 'compiled', 'manifest.json');
const INDEX_PATH     = path.join(ROOT, 'data', 'index.json');
const SCREENSHOTS_DIR = path.join(ROOT, 'docs', 'screenshots');
const SCAN_DIRS       = ['pages', 'wip'];
const OUT_PATH        = path.join(ROOT, 'data', 'analysis-index.json');

// ── Pure computation ─────────────────────────────────────────────────────────

// computeMissingLangs — supportedLangs a key has no value for yet. Order
// follows supportedLangs, not alphabetical, so "which language is missing"
// reads in the same priority order the rest of the repo already uses.
function computeMissingLangs(langs, supportedLangs) {
  const have = new Set(langs);
  return supportedLangs.filter(l => !have.has(l));
}

// buildElementIndex — one entry per key ever seen (in the manifest, used by
// HTML, or both) — a key used but not in the manifest still needs to be
// visible as a real finding, not silently dropped.
function buildElementIndex(usages, manifest, supportedLangs) {
  const allKeys = new Set([...Object.keys(manifest), ...Object.keys(usages)]);
  const elements = {};
  for (const key of allKeys) {
    const traced = traceKey(key, usages, manifest);
    elements[key] = {
      key,
      usedIn:       traced.usedIn,
      inManifest:   traced.inManifest,
      langs:        traced.langs,
      missingLangs: computeMissingLangs(traced.langs, supportedLangs),
    };
  }
  return elements;
}

// buildPageIndex — one entry per real page slug: every key it references
// (already-traced), any orphan keys, and real screenshot availability.
function buildPageIndex(usages, manifest, screenshotsBySlug, pageSlugs) {
  const pages = {};
  for (const slug of pageSlugs) {
    const relPath = `pages/${slug}.html`;
    const traced  = tracePage(relPath, usages, manifest);
    pages[slug] = {
      slug,
      keys:        traced.keys.map(k => k.key),
      orphanKeys:  traced.orphanKeys,
      screenshots: screenshotsBySlug[slug] || {},
    };
  }
  return pages;
}

// buildAnalysisIndex — full assembly. supportedLangs comes from
// data/index.json so this always compares against the same language set
// the rest of the site (fab-lang.js, build-vextreme.js) actually uses.
function buildAnalysisIndex(usages, manifest, screenshotsBySlug, pageSlugs, supportedLangs) {
  return {
    supportedLangs,
    elements: buildElementIndex(usages, manifest, supportedLangs),
    pages:    buildPageIndex(usages, manifest, screenshotsBySlug, pageSlugs),
    summary: {
      totalKeysInManifest: Object.keys(manifest).length,
      totalKeysUsed:       Object.keys(usages).length,
      orphanUsages:        findOrphanUsages(usages, manifest),
      unusedKeys:          findUnusedKeys(usages, manifest),
    },
  };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

function collectFiles(dirs) {
  const files = [];
  for (const dir of dirs) {
    const abs = path.join(ROOT, dir);
    if (!fs.existsSync(abs)) continue;
    for (const f of fs.readdirSync(abs)) {
      if (!f.endsWith('.html')) continue;
      files.push({ relPath: `${dir}/${f}`, html: fs.readFileSync(path.join(abs, f), 'utf8') });
    }
  }
  return files;
}

if (require.main === module) {
  const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
  const indexJson = JSON.parse(fs.readFileSync(INDEX_PATH, 'utf8'));
  const usages   = scanUsages(collectFiles(SCAN_DIRS));
  const pageSlugs = getPageSlugs();
  const screenshotFiles = fs.existsSync(SCREENSHOTS_DIR)
    ? fs.readdirSync(SCREENSHOTS_DIR).filter(f => f.endsWith('.png'))
    : [];
  const screenshotsBySlug = findScreenshots(screenshotFiles);

  const analysisIndex = buildAnalysisIndex(usages, manifest, screenshotsBySlug, pageSlugs, indexJson.supportedLangs || []);

  fs.writeFileSync(OUT_PATH, JSON.stringify(analysisIndex, null, 2) + '\n');

  console.log(`[build-analysis-index] Done.`);
  console.log(`  elements : ${Object.keys(analysisIndex.elements).length}`);
  console.log(`  pages    : ${Object.keys(analysisIndex.pages).length}`);
  console.log(`  orphans  : ${analysisIndex.summary.orphanUsages.length}`);
  console.log(`  unused   : ${analysisIndex.summary.unusedKeys.length}`);
  console.log(`  output   : ${OUT_PATH}`);
}

module.exports = { computeMissingLangs, buildElementIndex, buildPageIndex, buildAnalysisIndex };

