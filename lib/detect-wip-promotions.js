'use strict';

const { execFileSync } = require('child_process');

// ── Pure computation ──────────────────────────────────────────────────────────

// parseRenameStatus — parses `git diff --name-status -M<pct>` porcelain
// output (tab-separated: "R100\twip/x.html\tpages/x.html") into promotion
// records. Only keeps renames from wip/*.html to pages/*.html; every other
// change type (A/M/D, or a rename between two other paths) is ignored here —
// this file's only job is the wip→pages move, not a general diff summary.
function parseRenameStatus(porcelainOutput) {
  const lines = (porcelainOutput || '').split('\n').map(l => l.trim()).filter(Boolean);
  const promotions = [];

  for (const line of lines) {
    const fields = line.split('\t');
    if (fields.length < 3) continue;

    const [statusField, from, to] = fields;
    if (!statusField.startsWith('R')) continue;
    if (!from.startsWith('wip/') || !from.endsWith('.html')) continue;
    if (!to.startsWith('pages/') || !to.endsWith('.html')) continue;

    const similarity = parseInt(statusField.slice(1), 10) || 0;
    const fromSlug = from.replace(/^wip\//, '').replace(/\.html$/, '');
    const toSlug   = to.replace(/^pages\//, '').replace(/\.html$/, '');

    promotions.push({
      from, to, similarity,
      fromSlug, toSlug,
      slugChanged: fromSlug !== toSlug,
    });
  }

  return promotions;
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const args     = process.argv.slice(2).filter(a => a !== '--json');
  const jsonOnly = process.argv.includes('--json');
  const base     = args[0];
  const head     = args[1] || 'HEAD';

  if (!base) {
    console.error('Usage: node lib/detect-wip-promotions.js <base-ref> [<head-ref>] [--json]');
    process.exit(1);
  }

  let diffOutput = '';
  try {
    diffOutput = execFileSync(
      'git',
      ['diff', '--name-status', '-M50%', `${base}...${head}`, '--', 'wip/*.html', 'pages/*.html'],
      { encoding: 'utf8' }
    );
  } catch (e) {
    console.error('[detect-wip-promotions] git diff failed:', e.message);
    process.exit(1);
  }

  const promotions = parseRenameStatus(diffOutput);

  if (jsonOnly) {
    console.log(JSON.stringify({ promotions }, null, 2));
  } else if (!promotions.length) {
    console.log('[detect-wip-promotions] No wip/ -> pages/ moves detected.');
  } else {
    for (const p of promotions) {
      console.log(`[detect-wip-promotions] ${p.from} -> ${p.to} (similarity ${p.similarity}%)`);
    }
  }
}

module.exports = { parseRenameStatus };

