'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT      = path.join(__dirname, '..');
const PAGES_DIR = path.join(ROOT, 'pages');

// ── Pure computation ──────────────────────────────────────────────────────────

const HTML_ENTITIES = {
  '&amp;': '&', '&lt;': '<', '&gt;': '>', '&quot;': '"', '&#39;': "'",
  '&mdash;': '—', '&ndash;': '–', '&nbsp;': ' ',
};

function decodeEntities(str) {
  return str.replace(/&(?:amp|lt|gt|quot|#39|mdash|ndash|nbsp);/g, m => HTML_ENTITIES[m] || m);
}

// titleCaseFromSlug — the fallback when a page has no <title> tag at all
// (a real case: several pages/*.html files in this repo are empty stubs).
// "accountability-test-01" → "Accountability Test 01"
function titleCaseFromSlug(slug) {
  return slug
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

// parsePageTitle — reads the <title>...</title> text, decodes common HTML
// entities, falls back to a title-cased slug if absent or the file is empty.
function parsePageTitle(html, slug) {
  const match = (html || '').match(/<title>([^<]*)<\/title>/i);
  if (match && match[1].trim()) {
    return decodeEntities(match[1].trim());
  }
  return titleCaseFromSlug(slug);
}

// parsePageMeta — optional <meta name="vex:department" content="..."> and
// <meta name="vex:workType" content="..."> tags. Either or both may be
// absent; discoverOrphanNodes applies the registry default for whichever is.
function parsePageMeta(html) {
  const dept = (html || '').match(/<meta\s+name=["']vex:department["']\s+content=["']([^"']+)["']/i);
  const wt   = (html || '').match(/<meta\s+name=["']vex:workType["']\s+content=["']([^"']+)["']/i);
  return {
    department: dept ? dept[1].trim() : undefined,
    workType:   wt   ? wt[1].trim()   : undefined,
  };
}

function defaultDepartmentId(departmentsDef) {
  return Object.keys(departmentsDef || {})
    .filter(id => !id.startsWith('_'))
    .find(id => departmentsDef[id].default) || 'rd';
}

// discoverOrphanNodes — the one function everything else here builds toward.
// readPage(slug) → html string (or null/throws if missing) is injected so
// this stays testable without touching the real filesystem.
function discoverOrphanNodes(pageSlugs, existingNodeSlugs, skipPages, viewmodelSlugs, readPage, departmentsDef) {
  const nodeSet = new Set(existingNodeSlugs);
  const vmSet   = new Set(viewmodelSlugs || []);
  const skip    = skipPages || {};
  const defaultDept = defaultDepartmentId(departmentsDef);

  return pageSlugs
    .filter(slug => !nodeSet.has(slug) && !vmSet.has(slug) && !skip[slug])
    .map(slug => {
      let html = '';
      try { html = readPage(slug) || ''; } catch (e) { html = ''; }

      const title = parsePageTitle(html, slug);
      const meta  = parsePageMeta(html);

      return {
        id:             null,
        slug,
        title,
        date:           '',
        arcKeys:        [],
        department:     meta.department || defaultDept,
        workType:       meta.workType   || 'default',
        autoDiscovered: true,
        vexData:        {},
      };
    });
}

function readPageFromDisk(slug) {
  return fs.readFileSync(path.join(PAGES_DIR, slug + '.html'), 'utf8');
}

// discoverWipDrafts — a lightweight sibling of discoverOrphanNodes for
// wip/*.html files: same title/meta scraping, but deliberately a smaller
// shape (no id, no arcKeys, no autoDiscovered flag, no forced department
// default) since a draft isn't a real page yet and shouldn't be mistaken for
// one downstream. department/workType are included only if the file declares
// them via meta tag — a draft with neither is left with both undefined
// rather than defaulted, since guessing a department for content that hasn't
// been placed yet would be asserting something not yet true.
function discoverWipDrafts(wipHtmlSlugs, readWipPage) {
  return (wipHtmlSlugs || []).map(slug => {
    let html = '';
    try { html = readWipPage(slug) || ''; } catch (e) { html = ''; }

    const title = parsePageTitle(html, slug);
    const meta  = parsePageMeta(html);

    return {
      slug,
      title,
      department: meta.department,
      workType:   meta.workType,
    };
  });
}

const WIP_DIR = path.join(ROOT, 'wip');

function readWipPageFromDisk(slug) {
  return fs.readFileSync(path.join(WIP_DIR, slug + '.html'), 'utf8');
}

module.exports = {
  parsePageTitle,
  parsePageMeta,
  titleCaseFromSlug,
  discoverOrphanNodes,
  readPageFromDisk,
  discoverWipDrafts,
  readWipPageFromDisk,
};

