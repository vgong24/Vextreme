#!/usr/bin/env node
/**
 * VEXTREME — lib/check-description-freshness.js
 *
 * Nudges a PR when it edits a pages/*.html file that has no real meta
 * description, so the question "does this page's purpose stay perceivable
 * to a fresh instance, or an external AI mapping a user's question to a
 * route?" gets asked at the moment content actually changed, not left to be
 * noticed later by whoever happens to open lib/build-page-health.js's
 * output. Applies the same relay-culture instinct that shaped this PR's own
 * process changes — receive/scope drift at the point of change, not after —
 * at the PR level instead of the page-content level.
 *
 * Deliberately does not attempt true staleness detection (page content
 * changed enough that an *existing* description is now wrong) — that needs
 * a persisted content-hash-at-authoring-time and a real "what counts as
 * meaningfully changed" decision, scoped separately if it's ever built. This
 * only asks the cheap question: did this PR touch a page that has never had
 * a description at all?
 *
 * Reuses git's own diff, not a second change-tracking mechanism — same
 * reasoning lib/detect-wip-promotions.js already established for this
 * repo's other PR-diff-driven check.
 *
 * Pure function exported for testing:
 *   parseChangedPages(porcelainOutput) → string[] (pages/*.html paths)
 *
 * Run manually (from a PR branch, comparing against its base):
 *   node lib/check-description-freshness.js <base-ref> [<head-ref>]
 *   node lib/check-description-freshness.js <base-ref> --json
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : flags a PR that edits a pages∕*.html file with no meta description, so discoverability drift is caught at the moment content changes, not left for a later instance to notice in generated output
 *   reads     : git diff --name-status -M output between two refs (pages∕*.html paths only)
 *               pages∕*.html (current content at head)
 *               lib/build-page-health.js (metaDescriptionText — shared detection rule, not reimplemented here)
 *   writes    : stdout (JSON with --json flag); PR comment via .github/workflows/key-alignment.yml
 *   loaded-by : manual run: node lib/check-description-freshness.js <base-ref> [<head-ref>]
 *               .github/workflows/key-alignment.yml (CI)
 *               tests/45-check-description-freshness.test.js
 *   tested-by : tests/45-check-description-freshness.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     report shape changed (fields added/removed):
 *       - .github/workflows/key-alignment.yml (formats the PR comment section from this shape)
 *       - tests/45-check-description-freshness.test.js
 *     metaDescriptionText() detection rule changed:
 *       - lib/build-page-health.js (shared source of truth for what counts as 'has a description')
 * LATTICE:END
 */

'use strict';

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');
const { metaDescriptionText } = require('./build-page-health');

const ROOT = path.join(__dirname, '..');

// ── Pure computation ──────────────────────────────────────────────────────────

// parseChangedPages — parses `git diff --name-status -M<pct>` porcelain
// output (tab-separated) into a de-duplicated list of pages/*.html paths
// that exist at the new side of the diff. Covers add (A), modify (M), and
// rename-into-pages (R) — a page that only moved without content changes
// still deserves the same question, since it's new at that path either way.
// A page that was only deleted is excluded — nothing to review at a path
// that no longer exists.
function parseChangedPages(porcelainOutput) {
  const lines = (porcelainOutput || '').split('\n').map((l) => l.trim()).filter(Boolean);
  const pages = new Set();

  for (const line of lines) {
    const fields = line.split('\t');
    if (fields.length < 2) continue;
    const status = fields[0];
    const changedPath = fields.length >= 3 ? fields[2] : fields[1];

    if (status.startsWith('D')) continue;
    if (!changedPath.startsWith('pages/') || !changedPath.endsWith('.html')) continue;
    pages.add(changedPath);
  }

  return [...pages].sort();
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const args = process.argv.slice(2).filter((a) => a !== '--json');
  const jsonOnly = process.argv.includes('--json');
  const base = args[0];
  const head = args[1] || 'HEAD';

  if (!base) {
    console.error('Usage: node lib/check-description-freshness.js <base-ref> [<head-ref>] [--json]');
    process.exit(1);
  }

  let diffOutput = '';
  try {
    diffOutput = execFileSync(
      'git',
      ['diff', '--name-status', '-M50%', `${base}...${head}`, '--', 'pages/*.html'],
      { encoding: 'utf8' }
    );
  } catch (e) {
    console.error('[check-description-freshness] git diff failed:', e.message);
    process.exit(1);
  }

  const changedPages = parseChangedPages(diffOutput);
  const needsDescriptionReview = [];

  for (const changedPath of changedPages) {
    const fullPath = path.join(ROOT, changedPath);
    if (!fs.existsSync(fullPath)) continue;
    const html = fs.readFileSync(fullPath, 'utf8');
    if (!metaDescriptionText(html)) {
      needsDescriptionReview.push(changedPath.replace(/^pages\//, '').replace(/\.html$/, ''));
    }
  }

  const result = { changedPages: changedPages.map((p) => p.replace(/^pages\//, '').replace(/\.html$/, '')), needsDescriptionReview };

  if (jsonOnly) {
    console.log(JSON.stringify(result, null, 2));
  } else if (!needsDescriptionReview.length) {
    console.log('[check-description-freshness] No changed page is missing a meta description.');
  } else {
    for (const slug of needsDescriptionReview) {
      console.log(`[check-description-freshness] ${slug} changed and has no meta description.`);
    }
  }
}

module.exports = { parseChangedPages };

// [VXG RealForever]
