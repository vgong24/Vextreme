#!/usr/bin/env node
/**
 * VEXTREME — lib/build-lattice-headers.js
 *
 * Generates the in-file LATTICE comment block for every eligible node in
 * docs/lattice-map.json and writes it into the target file, spliced between
 * that file's own opening and closing sentinel comment lines (see MARKER
 * NOTE below for why this paragraph avoids spelling them out literally).
 *
 * This exists because lattice-map.json and in-file LATTICE headers used to
 * be two hand-maintained copies of the same facts (role, reads, writes,
 * loaded-by, tested-by, changeMap). Two hand-maintained copies drift. Every
 * other data flow in this repo is CQRS — one write-side source, a build
 * script, one generated read-side artifact (nodes.json → index.json,
 * strings/source → strings/compiled). This script applies the same shape to
 * the lattice: docs/lattice-map.json is the write side; the header block
 * inside each file is the generated read side. Never hand-edit text between
 * the sentinel lines — edit the JSON and regenerate.
 *
 * Eligible nodes are lattice-map.json keys that:
 *   - end in .js (JSON files can't hold comments — see CLAUDE.md's own rule)
 *   - resolve to a file that exists on disk
 *   - are not a build-generated artifact (sw.js, dist/* — those are fully
 *     regenerated by their own build script on every run; a second script
 *     writing into them would race with the first and the header would be
 *     wiped the next time that script runs)
 *
 * If a file already has both sentinel lines, the text between them is
 * replaced. If it has none, the block is inserted just before the closing
 * delimiter of the file's first top-of-file doc comment (auto-bootstrap —
 * no manual marker placement needed to onboard a file). A file with no top
 * doc comment at all is skipped and reported.
 *
 * MARKER NOTE: the sentinel pair this script searches for is a plain
 * substring match (see BEGIN_MARKER/END_MARKER below) — it has no concept of
 * "inside vs. outside a comment." Any file whose prose mentions both
 * sentinel strings near each other, ahead of the real pair, will corrupt
 * that file on the next run (this happened to this file's own doc comment
 * during authoring — fixed by never spelling the two sentinel strings out
 * together in prose). Keep it that way.
 *
 * Run manually:   node lib/build-lattice-headers.js
 * Check for drift (no writes, CI-friendly):
 *                 node lib/build-lattice-headers.js --check
 *
 * Pure functions exported for testing:
 *   buildLatticeBlockLines(node, prefix) → string[] (comment-prefixed, sentinel-wrapped)
 *   injectLatticeBlock(source, node) → { content, mode } — mode: 'replaced' | 'inserted' | 'no-doc-comment'
 *   isEligibleNode(file) → boolean
 *   detectPrefix(source) → string|null — comment prefix of an existing marker line
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : LATTICE header generator — reads this JSON, writes the matching in-file header into each eligible node's own file
 *   reads     : docs/lattice-map.json
 *   writes    : LATTICE:​BEGIN..LATTICE:​END block in every eligible node's own file
 *   loaded-by : tests/11-lattice-headers.test.js
 *               manual run after editing docs/lattice-map.json
 *   tested-by : tests/11-lattice-headers.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     node added/removed/edited in this JSON:
 *       - run: node lib/build-lattice-headers.js to regenerate the affected file's header
 *       - tests/11 --check enforces this in CI — a PR with JSON changes but no regenerated header fails the drift check
 *     LATTICE:​BEGIN/LATTICE:​END marker text changed:
 *       - every file already carrying markers (grep for LATTICE:​BEGIN) — old marker text will no longer be found, orphaning that block
 *       - tests/11 (marker constants)
 *     comment-prefix detection logic changed:
 *       - any node whose target file uses line-comment style instead of block-comment style (e.g. lib/strings-compile.js uses // ) — verify both styles still round-trip
 * LATTICE:END
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const { logger } = require('./logger');
const { CODES }  = require('./logger-codes');

const ROOT         = path.join(__dirname, '..');
const LATTICE_PATH = path.join(ROOT, 'docs', 'lattice-map.json');

const BEGIN_MARKER = 'LATTICE:BEGIN';
const END_MARKER   = 'LATTICE:END';

// Nodes that resolve to a real .js file but are fully regenerated by their
// own build script — a LATTICE header here would be overwritten (or would
// overwrite this script's work) on the next run of that script.
const GENERATED_EXCLUDE = new Set(['sw.js']);

// ── Pure computation functions ────────────────────────────────────────────────

// isEligibleNode — true if this lattice-map.json key should get a generated
// in-file header. False for .json nodes (no comments) and generated artifacts.
function isEligibleNode(file) {
  if (!file.endsWith('.js')) return false;
  if (GENERATED_EXCLUDE.has(file)) return false;
  if (file.startsWith('dist/')) return false;
  return true;
}

const LABELS = ['role', 'reads', 'writes', 'loaded-by', 'tested-by'];
const LABEL_WIDTH = Math.max(...LABELS.map(l => l.length)); // 'loaded-by'/'tested-by' = 9

// DEFAULT_PREFIX is used only for the auto-insert path (always inside a
// freshly-opened /** */ block). The replace path reuses whatever prefix the
// existing marker line already has, so '//'-line-comment files (e.g.
// strings-compile.js) and '/** */'-block-comment files both stay valid JS.
const DEFAULT_PREFIX = ' * ';

function detectLineEnding(source) {
  return source.includes('\r\n') ? '\r\n' : '\n';
}

// sanitizeForComment — breaks two classes of self-corruption in dynamic
// JSON content before it's spliced into a generated comment block:
//   1. literal '*/' or '/*' — closes a /** */ block early (hit this once
//      in lib/strings-compile.js with a glob pattern, and again here)
//   2. literal 'LATTICE:BEGIN' / 'LATTICE:END' — this node's own JSON entry
//      describes this tool in prose, so its 'writes' and 'changeMap' fields
//      legitimately need to say those words; unsanitized, the next
//      regeneration pass's plain indexOf() finds that prose mention instead
//      of the real sentinel line and truncates the block there
// A zero-width space inside the sentinel keeps it visually identical in the
// rendered comment while breaking the exact substring match.
function sanitizeForComment(text) {
  return String(text)
    .replace(/\*\//g, '*∕')
    .replace(/\/\*/g, '∕*')
    .replace(/LATTICE:BEGIN/g, 'LATTICE:​BEGIN')
    .replace(/LATTICE:END/g, 'LATTICE:​END');
}

function formatField(prefix, label, value) {
  const pad = label.padEnd(LABEL_WIDTH, ' ');
  const items = (Array.isArray(value) ? value : [value]).map(sanitizeForComment);
  if (!items.length) {
    return [`${prefix}  ${pad} : (none)`];
  }
  const indent = prefix + '  ' + ' '.repeat(LABEL_WIDTH) + '   '; // align continuation under first value
  return items.map((item, i) => (i === 0 ? `${prefix}  ${pad} : ${item}` : `${indent}${item}`));
}

function formatChangeMap(prefix, changeMap) {
  const lines = [prefix.trimEnd(), `${prefix}  CHANGE MAP — if you touch X here, also check:`];
  for (const [trigger, targets] of Object.entries(changeMap || {})) {
    lines.push(`${prefix}    ${sanitizeForComment(trigger)}:`);
    for (const target of targets) {
      lines.push(`${prefix}      - ${sanitizeForComment(target)}`);
    }
  }
  return lines;
}

// buildLatticeBlockLines — the full LATTICE:BEGIN..LATTICE:END block for one
// node, each line already comment-prefixed and ready to splice into the file.
// prefix defaults to ' * ' (block-comment style); pass '// ' for files whose
// existing marker line uses line comments (detected by the caller).
function buildLatticeBlockLines(node, prefix) {
  const p = prefix || DEFAULT_PREFIX;
  const lines = [];
  lines.push(`${p}${BEGIN_MARKER} — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.`);
  lines.push(...formatField(p, 'role', node.role));
  lines.push(...formatField(p, 'reads', node.reads));
  lines.push(...formatField(p, 'writes', node.writes));
  lines.push(...formatField(p, 'loaded-by', node.loadedBy));
  lines.push(...formatField(p, 'tested-by', node.testedBy));
  lines.push(...formatChangeMap(p, node.changeMap));
  lines.push(`${p}${END_MARKER}`);
  return lines;
}

// detectPrefix — reads the comment prefix (e.g. ' * ' or '// ') from the
// existing LATTICE:BEGIN marker line in source, or null if markers absent.
function detectPrefix(source) {
  const beginIdx = source.indexOf(BEGIN_MARKER);
  if (beginIdx === -1) return null;
  const lineStart = source.lastIndexOf('\n', beginIdx) + 1;
  return source.slice(lineStart, beginIdx);
}

// findLineStartDocComment — the position of a '/**' that actually opens a
// line (only whitespace between it and the previous newline, or it's at
// position 0), as opposed to a '/**' substring sitting mid-line inside a //
// comment or a string. Returns -1 if none found.
function findLineStartDocComment(source) {
  let from = 0;
  while (true) {
    const idx = source.indexOf('/**', from);
    if (idx === -1) return -1;
    const lineStart = source.lastIndexOf('\n', idx) + 1;
    if (/^\s*$/.test(source.slice(lineStart, idx))) return idx;
    from = idx + 3;
  }
}

// injectLatticeBlock — splices a generated block for `node` into source text.
// Replaces existing LATTICE:BEGIN..LATTICE:END lines if present, reusing
// their comment prefix ('// ' or ' * ') so the result stays valid JS.
// Otherwise inserts a ' * '-prefixed block before the closing `*/` of the
// first top-of-file /** */ comment.
function injectLatticeBlock(source, node) {
  const beginIdx = source.indexOf(BEGIN_MARKER);
  const endIdx   = source.indexOf(END_MARKER);

  if (beginIdx !== -1 && endIdx !== -1 && endIdx > beginIdx) {
    const prefix    = detectPrefix(source);
    const newline   = detectLineEnding(source);
    const blockText = buildLatticeBlockLines(node, prefix).join(newline);
    const lineStart = source.lastIndexOf('\n', beginIdx) + 1;
    let lineEnd = source.indexOf('\n', endIdx);
    const hasLineAfterBlock = lineEnd !== -1;
    if (hasLineAfterBlock) lineEnd += 1;
    else lineEnd = source.length;
    const content = source.slice(0, lineStart) + blockText + (hasLineAfterBlock ? newline : '') + source.slice(lineEnd);
    return { content, mode: 'replaced' };
  }

  // No markers — insert before the closing */ of the first genuine
  // top-of-file /** */ comment. A plain indexOf('/**') is not enough: a
  // file can contain the literal substring '/**' inside an unrelated //
  // line comment (this happened for real — lib/logger-codes.js has a
  // comment describing this exact hazard, containing the text '/** */').
  // Require the match to (a) start a line, not sit mid-line inside other
  // text, and (b) actually span multiple lines before its closing */ —
  // every real doc comment in this repo does; a same-line "/** */" never
  // is one.
  const docStart = findLineStartDocComment(source);
  if (docStart === -1) return { content: source, mode: 'no-doc-comment' };
  const docEnd = source.indexOf('*/', docStart);
  if (docEnd === -1 || !source.slice(docStart, docEnd).includes('\n')) {
    return { content: source, mode: 'no-doc-comment' };
  }

  const newline = detectLineEnding(source);
  const blockText = buildLatticeBlockLines(node, DEFAULT_PREFIX).join(newline);
  const closeLineStart = source.lastIndexOf('\n', docEnd) + 1;
  const insertion = ' *' + newline + blockText + newline;
  const content = source.slice(0, closeLineStart) + insertion + source.slice(closeLineStart);
  return { content, mode: 'inserted' };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const checkOnly = process.argv.includes('--check');

  const map = JSON.parse(fs.readFileSync(LATTICE_PATH, 'utf8'));

  let replaced = 0, inserted = 0, unchanged = 0, skipped = 0, drift = 0;

  for (const [file, node] of Object.entries(map.nodes)) {
    if (!isEligibleNode(file)) {
      skipped++;
      continue;
    }

    const filePath = path.join(ROOT, file);
    if (!fs.existsSync(filePath)) {
      logger.warn({ code: CODES.LATTICE_FILE_MISSING, message: `lattice node file not found — skipped`, file });
      skipped++;
      continue;
    }

    const source = fs.readFileSync(filePath, 'utf8');
    const { content, mode } = injectLatticeBlock(source, node);

    if (mode === 'no-doc-comment') {
      logger.warn({ code: CODES.LATTICE_NO_DOC_COMMENT, message: `no top /** */ comment to anchor a LATTICE block — skipped`, file });
      skipped++;
      continue;
    }

    if (content === source) {
      unchanged++;
      continue;
    }

    if (checkOnly) {
      drift++;
      console.log(`[build-lattice-headers] DRIFT: ${file} (would be ${mode})`);
      continue;
    }

    fs.writeFileSync(filePath, content);
    if (mode === 'replaced') replaced++;
    else inserted++;
  }

  console.log(`[build-lattice-headers] Done.`);
  if (checkOnly) {
    console.log(`  drift     : ${drift}`);
    console.log(`  unchanged : ${unchanged}`);
    console.log(`  skipped   : ${skipped} (not applicable — .json/generated, or no doc comment)`);
    if (drift > 0) {
      console.log(`[build-lattice-headers] Drift detected — run without --check to regenerate.`);
      process.exit(1);
    }
  } else {
    console.log(`  replaced  : ${replaced}`);
    console.log(`  inserted  : ${inserted}`);
    console.log(`  unchanged : ${unchanged}`);
    console.log(`  skipped   : ${skipped} (not applicable — .json/generated, or no doc comment)`);
  }
}

module.exports = { isEligibleNode, buildLatticeBlockLines, injectLatticeBlock, detectPrefix, sanitizeForComment, findLineStartDocComment, detectLineEnding };

