#!/usr/bin/env node
/**
 * VEXTREME — lib/compute-fab-version.js
 *
 * Pure functions for auto-incrementing widgets/fab-lang.js's CDN
 * cache-busting VERSION constant. Session 025 found that constant had
 * never been bumped since introduction — a silent-drift bug with the same
 * shape as PR #76's ARC_ORDER gap, except the "hand-maintained value
 * nobody remembers to update" was a version string, not a list. This file
 * replaces "remember to bump it" with "compute it from what actually
 * changed" — see lib/bump-fab-version.js for the git-diffing orchestration
 * that calls these functions.
 *
 * Semver-shaped, three tiers, cascading (bumping a higher tier resets all
 * lower tiers to 0 — standard semver behavior, not a Vextreme invention):
 *
 *   MAJOR — identity changes: a node's slug renamed, or a slug's id
 *           reassigned. These can break external links/bookmarks — the
 *           closest thing this system has to a breaking change.
 *   MINOR — additive, backward-compatible: new strings, a new locale, a
 *           new arc, a new department. Nothing existing changes meaning.
 *   PATCH — content correction, no structural change: an existing string's
 *           text edited, or a checks/alignment script touched.
 *
 * classifyChanges() takes a signals object (booleans) rather than raw
 * diffs so it stays a pure, trivially-testable decision table — all the
 * "how do we detect X" logic lives in the diff* functions below it and in
 * lib/bump-fab-version.js's git plumbing, kept separate on purpose.
 *
 * LATTICE:BEGIN — generated by lib/build-lattice-headers.js from docs/lattice-map.json. Do not hand-edit; edit the JSON and regenerate.
 *   role      : pure semver classification for widgets/fab-lang.js's CDN cache-busting VERSION — major/minor/patch decision table + diff helpers, no I/O
 *   reads     : (none)
 *   writes    : (none)
 *   loaded-by : lib/bump-fab-version.js
 *               tests/30-compute-fab-version.test.js
 *   tested-by : tests/30-compute-fab-version.test.js
 *
 *   CHANGE MAP — if you touch X here, also check:
 *     the 8-condition -> tier mapping changes (e.g. a condition is added or reweighted):
 *       - classifyChanges() here
 *       - tests/30 (FAB-VERSION: classifyChanges tests)
 *       - lib/bump-fab-version.js's computeSignals() — must supply a matching signal for any new condition
 * LATTICE:END
 */

'use strict';

function parseVersion(version) {
  const m = /^(\d+)\.(\d+)\.(\d+)$/.exec(version);
  if (!m) throw new Error(`Not a valid MAJOR.MINOR.PATCH version: ${version}`);
  return { major: Number(m[1]), minor: Number(m[2]), patch: Number(m[3]) };
}

function formatVersion(v) {
  return `${v.major}.${v.minor}.${v.patch}`;
}

// bumpVersion — applies one tier's increment with semver cascade (a higher
// tier bump resets every lower tier back to 0). tier is 'major'|'minor'|'patch'|null;
// null means no bump needed, returns the current version unchanged.
function bumpVersion(current, tier) {
  const v = parseVersion(current);
  if (tier === 'major') return formatVersion({ major: v.major + 1, minor: 0, patch: 0 });
  if (tier === 'minor') return formatVersion({ major: v.major, minor: v.minor + 1, patch: 0 });
  if (tier === 'patch') return formatVersion({ major: v.major, minor: v.minor, patch: v.patch + 1 });
  if (tier === null || tier === undefined) return formatVersion(v);
  throw new Error(`Unknown version tier: ${tier}`);
}

// classifyChanges — the decision table. Signals object shape:
//   { idsChanged, slugsRenamed, newStrings, modifiedStrings,
//     newLocale, newArc, newDepartment, checksAlignment }
// Returns the highest-priority tier that applies, or null if nothing
// signaled a change (no bump needed).
function classifyChanges(signals) {
  const s = signals || {};
  if (s.idsChanged || s.slugsRenamed) return 'major';
  if (s.newStrings || s.newLocale || s.newArc || s.newDepartment) return 'minor';
  if (s.modifiedStrings || s.checksAlignment) return 'patch';
  return null;
}

// ── Diff helpers — each takes an "old" and "new" snapshot of one data
// shape and returns the relevant boolean signal(s). Kept pure (no I/O,
// no git) so lib/bump-fab-version.js only has to read files/git output
// and hand them here, and so these are testable without a git repo.

// diffNodeIdentity — nodes.json is an array of {id, slug, ...}. Compares
// by id for renames (same id, different slug) and by slug for id
// reassignment (same slug, different id) — the two are independent
// signals because either one alone constitutes an identity change.
function diffNodeIdentity(oldNodes, newNodes) {
  const oldById = new Map((oldNodes || []).map(n => [n.id, n.slug]));
  const newById = new Map((newNodes || []).map(n => [n.id, n.slug]));
  const oldBySlug = new Map((oldNodes || []).map(n => [n.slug, n.id]));
  const newBySlug = new Map((newNodes || []).map(n => [n.slug, n.id]));

  let slugsRenamed = false;
  for (const [id, slug] of oldById) {
    if (newById.has(id) && newById.get(id) !== slug) { slugsRenamed = true; break; }
  }

  let idsChanged = false;
  for (const [slug, id] of oldBySlug) {
    if (newBySlug.has(slug) && newBySlug.get(slug) !== id) { idsChanged = true; break; }
  }

  return { idsChanged, slugsRenamed };
}

// diffStrings — takes two flat { key: { text, ... } | string } maps
// (already merged across all source files by the caller) and reports
// whether any key is new, and whether any shared key's text changed.
function diffStrings(oldStrings, newStrings) {
  const oldMap = oldStrings || {};
  const newMap = newStrings || {};

  let newStringsFound = false;
  for (const key of Object.keys(newMap)) {
    if (!(key in oldMap)) { newStringsFound = true; break; }
  }

  let modifiedStrings = false;
  for (const key of Object.keys(newMap)) {
    if (!(key in oldMap)) continue;
    const oldText = typeof oldMap[key] === 'string' ? oldMap[key] : oldMap[key]?.text;
    const newText = typeof newMap[key] === 'string' ? newMap[key] : newMap[key]?.text;
    if (oldText !== newText) { modifiedStrings = true; break; }
  }

  return { newStrings: newStringsFound, modifiedStrings };
}

// diffKeyedCollection — generic "did a new key appear" check, used for
// both arcs-v2.json and departments.json (same shape: object keyed by id,
// values not compared — only presence of a new key matters for MINOR).
function diffKeyedCollection(oldObj, newObj) {
  const oldKeys = new Set(Object.keys(oldObj || {}).filter(k => !k.startsWith('_')));
  const newKeys = Object.keys(newObj || {}).filter(k => !k.startsWith('_'));
  return newKeys.some(k => !oldKeys.has(k));
}

// diffLocales — supportedLangs is a flat array of lang codes.
function diffLocales(oldLangs, newLangs) {
  const oldSet = new Set(oldLangs || []);
  return (newLangs || []).some(l => !oldSet.has(l));
}

module.exports = {
  parseVersion,
  formatVersion,
  bumpVersion,
  classifyChanges,
  diffNodeIdentity,
  diffStrings,
  diffKeyedCollection,
  diffLocales,
};

