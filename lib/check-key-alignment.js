'use strict';

const fs   = require('fs');
const path = require('path');
const { SKIP_PAGES, getPageSlugs, getPageBindingReport } = require('./audit-pages');
const { discoverWipDrafts } = require('./auto-discover-nodes');

const ROOT     = path.join(__dirname, '..');
const NODES_IN      = path.join(ROOT, 'data', 'nodes.json');
const ARCS_IN       = path.join(ROOT, 'data', 'arcs-v2.json');
const INDEX_IN      = path.join(ROOT, 'data', 'index.json');
const VIEWMODELS_IN = path.join(ROOT, 'data', 'viewmodels.json');
const WIP_DIR       = path.join(ROOT, 'wip');

// ── Pure computation ──────────────────────────────────────────────────────────

// findOrphanPages — a pages/*.html file with no *hand-curated* entry: not in
// nodes.json, not in data/viewmodels.json (dev/demo pages opt in there
// instead — see its own _comment), and not in lib/audit-pages.js's
// SKIP_PAGES (generated pages have no node by design). Not actually
// invisible — lib/auto-discover-nodes.js synthesizes a placeholder node for
// it — this just flags that the placeholder's data is scraped, not curated.
// Informational, low priority: a worklist item, not a bug to block on.
function findOrphanPages(pageSlugs, nodeSlugs, skipPages, viewmodelSlugs) {
  const nodeSet = new Set(nodeSlugs);
  const vmSet   = new Set(viewmodelSlugs || []);
  const skip    = skipPages || {};
  return pageSlugs.filter(slug => !nodeSet.has(slug) && !vmSet.has(slug) && !skip[slug]);
}

// scanWipIntendedSlugs — reads every wip/*.json file's optional `_meta.slug`,
// the file's declared intent for where it'll eventually land. Files with no
// `_meta.slug` are skipped entirely — there's nothing to check yet, and
// guessing a slug from the filename would be wrong as often as right (see
// wip/silent-god.json, whose actual registered slug is "vxg-thread-round-5",
// nothing like its filename).
function scanWipIntendedSlugs(wipDir) {
  if (!fs.existsSync(wipDir)) return [];
  return fs.readdirSync(wipDir)
    .filter(f => f.endsWith('.json'))
    .map(file => {
      try {
        const data = JSON.parse(fs.readFileSync(path.join(wipDir, file), 'utf8'));
        return { file, slug: data._meta && data._meta.slug };
      } catch (e) {
        return { file, slug: undefined, parseError: true };
      }
    })
    .filter(entry => entry.slug);
}

// findWipSlugCollisions — a wip/ file's declared or derived slug already has
// a real pages/*.html file at that name. Deliberately checks pages/, not
// nodes.json: a slug matching a nodes.json entry with no page yet (like
// wip/silent-god.json matching its own already-registered placeholder,
// vxg-thread-round-5) is the *expected* linkage — that placeholder exists
// precisely so this wip file has a reserved destination, not a conflict to
// flag. Only a slug that's already live as an actual page is a genuine
// placement mistake, since placing this file there would mean overwriting
// or renaming something that already exists. Takes the combined list of
// json-declared intents and html-derived drafts — a collision is exactly as
// real regardless of which source declared the slug.
function findWipSlugCollisions(wipIntents, pageSlugs) {
  const pageSet = new Set(pageSlugs);
  return wipIntents
    .filter(entry => pageSet.has(entry.slug))
    .map(entry => ({ file: entry.file, slug: entry.slug, collidesWith: 'pages/' }));
}

// findDuplicateWipIntents — two different wip/ entries (json-declared or
// html-derived, in any combination) both claiming the same slug — a
// collision within the queue itself, before either has touched nodes.json or
// pages/ at all.
function findDuplicateWipIntents(wipIntents) {
  const bySlug = {};
  for (const entry of wipIntents) {
    (bySlug[entry.slug] = bySlug[entry.slug] || []).push(entry.file);
  }
  return Object.entries(bySlug)
    .filter(([, files]) => files.length > 1)
    .map(([slug, files]) => ({ slug, files }));
}

// scanWipHtmlDrafts — every wip/*.html file, filename treated as its working
// slug (same convention wip/*.json's _meta.slug already uses, just derived
// instead of declared). Reuses lib/auto-discover-nodes.js's discoverWipDrafts
// for the title/meta scrape rather than re-implementing it — reading each
// file from wipDir itself (not the hardcoded real wip/ directory) so this
// stays testable against a tmp directory, the same way scanWipIntendedSlugs
// already is.
function scanWipHtmlDrafts(wipDir) {
  if (!fs.existsSync(wipDir)) return [];
  const slugs = fs.readdirSync(wipDir)
    .filter(f => f.endsWith('.html'))
    .map(f => f.replace(/\.html$/, ''));
  const readPage = slug => fs.readFileSync(path.join(wipDir, slug + '.html'), 'utf8');
  return discoverWipDrafts(slugs, readPage).map(draft => ({
    file:  draft.slug + '.html',
    slug:  draft.slug,
    title: draft.title,
  }));
}

function checkKeyAlignment() {
  const nodes      = JSON.parse(fs.readFileSync(NODES_IN,  'utf8'));
  const arcs       = JSON.parse(fs.readFileSync(ARCS_IN,   'utf8'));
  const index      = JSON.parse(fs.readFileSync(INDEX_IN,  'utf8'));
  const viewmodels = fs.existsSync(VIEWMODELS_IN)
    ? JSON.parse(fs.readFileSync(VIEWMODELS_IN, 'utf8'))
    : {};
  const viewmodelSlugs = Object.keys(viewmodels).filter(k => !k.startsWith('_'));

  const nodeSlugsArr = nodes.map(n => n.slug);
  const nodeSlugs    = new Set(nodeSlugsArr);
  const indexSlugs   = new Set(Object.keys(index.slugMap || {}));

  // arcsDef is an object keyed by arc name; skip underscore-prefixed meta keys
  const arcIds      = new Set(Object.keys(arcs).filter(k => !k.startsWith('_')));
  const indexArcIds = new Set(Object.keys(index.arcMap || {}));

  const pageSlugs     = getPageSlugs();
  const orphanPages   = findOrphanPages(pageSlugs, nodeSlugsArr, SKIP_PAGES, viewmodelSlugs);
  const orphanSet     = new Set(orphanPages);
  const pageBindings  = getPageBindingReport();

  const wipJsonIntents      = scanWipIntendedSlugs(WIP_DIR);
  const wipHtmlDrafts       = scanWipHtmlDrafts(WIP_DIR);
  const wipIntents          = [...wipJsonIntents, ...wipHtmlDrafts.map(({ file, slug }) => ({ file, slug }))];
  const wipCollisions       = findWipSlugCollisions(wipIntents, pageSlugs);
  const wipDuplicateIntents = findDuplicateWipIntents(wipIntents);

  return {
    nodes: {
      total:             nodeSlugs.size,
      inIndex:           [...nodeSlugs].filter(s => indexSlugs.has(s)).length,
      missingFromIndex:  [...nodeSlugs].filter(s => !indexSlugs.has(s)),
      // Auto-discovered slugs (lib/auto-discover-nodes.js) are expected to be
      // "extra" — index.json legitimately has more slugs than nodes.json by
      // design now. Only a slug that's neither in nodes.json NOR explained by
      // auto-discovery is real drift (e.g. index.json stale after a deletion).
      extraInIndex:      [...indexSlugs].filter(s => !nodeSlugs.has(s) && !orphanSet.has(s)),
    },
    arcs: {
      total:             arcIds.size,
      inIndex:           [...arcIds].filter(a => indexArcIds.has(a)).length,
      missingFromIndex:  [...arcIds].filter(a => !indexArcIds.has(a)),
      extraInIndex:      [...indexArcIds].filter(a => !arcIds.has(a)),
    },
    pages: {
      total:    pageSlugs.length,
      orphans:  orphanPages,
      bindings: pageBindings,
    },
    wip: {
      totalDeclared:    wipIntents.length,
      collisions:       wipCollisions,
      duplicateIntents: wipDuplicateIntents,
      htmlDrafts:       wipHtmlDrafts,
    },
  };
}

// ── I/O — only runs when executed directly ────────────────────────────────────

if (require.main === module) {
  const jsonOnly = process.argv.includes('--json');

  const report = checkKeyAlignment();

  if (jsonOnly) {
    console.log(JSON.stringify(report, null, 2));
    process.exit(0);
  }

  const nd = report.nodes;
  const ar = report.arcs;

  console.log('[check-key-alignment] Results:');
  console.log(`  nodes: ${nd.total} total, ${nd.inIndex} in index`);
  if (nd.missingFromIndex.length) {
    console.warn(`  ⚠️  missing from index (${nd.missingFromIndex.length}): ${nd.missingFromIndex.join(', ')}`);
  }
  if (nd.extraInIndex.length) {
    console.warn(`  ⚠️  extra in index (${nd.extraInIndex.length}): ${nd.extraInIndex.join(', ')}`);
  }

  console.log(`  arcs:  ${ar.total} total, ${ar.inIndex} in index`);
  if (ar.missingFromIndex.length) {
    console.warn(`  ⚠️  missing from index (${ar.missingFromIndex.length}): ${ar.missingFromIndex.join(', ')}`);
  }
  if (ar.extraInIndex.length) {
    console.warn(`  ⚠️  extra in index (${ar.extraInIndex.length}): ${ar.extraInIndex.join(', ')}`);
  }

  const pg  = report.pages;
  const wip = report.wip;

  console.log(`  pages: ${pg.total} total`);
  if (pg.orphans.length) {
    console.warn(`  ⚠️  uncurated pages (${pg.orphans.length}, auto-discovered but no nodes.json entry): ${pg.orphans.join(', ')}`);
  }

  console.log(`  wip:   ${wip.totalDeclared} file(s) with a declared or derived slug (${wip.htmlDrafts.length} html draft(s))`);
  if (wip.htmlDrafts.length) {
    for (const d of wip.htmlDrafts) {
      console.log(`  📝 wip/${d.file} — draft "${d.title}" (slug: ${d.slug})`);
    }
  }
  if (wip.collisions.length) {
    for (const c of wip.collisions) {
      console.warn(`  ⚠️  wip/${c.file} declares slug "${c.slug}" — already exists in ${c.collidesWith}`);
    }
  }
  if (wip.duplicateIntents.length) {
    for (const d of wip.duplicateIntents) {
      console.warn(`  ⚠️  slug "${d.slug}" declared by multiple wip/ files: ${d.files.join(', ')}`);
    }
  }

  const hasWarnings = nd.missingFromIndex.length || nd.extraInIndex.length
    || ar.missingFromIndex.length || ar.extraInIndex.length
    || pg.orphans.length || wip.collisions.length || wip.duplicateIntents.length;

  if (!hasWarnings) {
    console.log('[check-key-alignment] OK — all keys aligned, no uncurated pages, no wip/ collisions');
  } else {
    console.log('[check-key-alignment] Warnings above. This is informational — nothing here blocks a build.');
  }
}

module.exports = { checkKeyAlignment, findOrphanPages, scanWipIntendedSlugs, scanWipHtmlDrafts, findWipSlugCollisions, findDuplicateWipIntents };

